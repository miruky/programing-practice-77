# =====================================================================
# B36: Switching Lights (A36 応用 ─ 偶奇の不変量)
# ---------------------------------------------------------------------
# 【概要】N 個のランプが各 0/1 で点いている。「2 つ選んで両方を反転する」
#         という操作を 1 回行うと、ON の個数は -2/0/+2 と必ず偶数回変化する。
#         K 回の操作後に「全消灯」にできるか判定する。
# 【アルゴリズム】「ON の個数の偶奇」が不変量。K 回の操作で増減できるのも
#                 偶数なので、初期 ON 数と K の偶奇が一致すれば Yes。
# 【計算量】O(N)。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 考察 / 偶奇 / 難易度目安: ★3
# - 要約: ON 状態のランプの個数の偶奇のみで答えが決まる不変量問題。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N, K = map(int, input().split())
S = input()

# --- ON の個数を数える ---------------------------------------------
numON = 0
for i in range(N):
    if S[i] == '1':
        numON += 1

# --- 偶奇の一致で判定 ----------------------------------------------
# numON が偶数のまま K 回後 0 個にできる ⇔ K も偶数 (両方とも偶数)
# numON が奇数 ⇔ K も奇数 (両方とも奇数)
print("Yes" if numON % 2 == K % 2 else "No")
