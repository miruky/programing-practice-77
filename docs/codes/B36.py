# =====================================================================
# B36: Switching Lights (A36 応用 ─ 偶奇の不変量)
# ---------------------------------------------------------------------
# 【概要】N 個のランプが各 0/1 で点いている。「2 つ選んで両方を反転する」
#         という操作を 1 回行うと、ON の個数は -2/0/+2 と必ず偶数回変化する。
#         K 回の操作後に「全消灯」にできるか判定する。
# 【アルゴリズム】「ON の個数の偶奇」が不変量。K 回の操作で増減できるのも
#                 偶数なので、初期 ON 数と K の偶奇が一致すれば Yes。
# 【計算量】O(N)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, K = map(int, input().split())
S = input()

# --- ON の個数を数える ---------------------------------------------
numON = 0
for i in range(N):
    if S[i] == '1':
        numON += 1

# --- 偶奇の一致で判定 ----------------------------------------------
# numON が偶数のまま K 回後 0 個にできる ⇔ K も偶数 (両方とも偶数)
# numON が奇数 ⇔ K も奇数 (両方とも奇数)
print("Yes" if numON % 2 == K % 2 else "No")
