# A26: 素数判定 (試し割り法)
# ---------------------------------------------------------------------
# 【概要】Q 個の整数について、それぞれが素数かどうか判定する。
# 【アルゴリズム】試し割り法
#   N が合成数 ⇔ N に √N 以下の約数が存在する。
#   よって 2 〜 ⌊√N⌋ で割り切れるか確かめれば十分。
# 【計算量】1 回あたり O(√N), 全体 O(Q √max(X))
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 数学 / 素数 / 難易度目安: ★1
# - 要約: 試し割り法で素数かを判定する。計算量 O(√N)。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。
# ---------------------------------------------------------------------

# N が素数のとき True を返す関数。
def isPrime(N):
    LIMIT = int(N ** 0.5)  # √N (整数)
    for i in range(2, LIMIT + 1):
        if N % i == 0:
            return False   # 割り切れた → 素数でない
    return True


# --- 入力・処理 -----------------------------------------------------
Q = int(input())
X = [None] * Q
for i in range(Q):
    X[i] = int(input())

# --- 各クエリに答える -----------------------------------------------
for i in range(Q):
    Answer = isPrime(X[i])
    if Answer == True:
        print("Yes")
    else:
        print("No")
