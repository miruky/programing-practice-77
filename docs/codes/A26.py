# =====================================================================
# A26: 素数判定 (試し割り法)
# ---------------------------------------------------------------------
# 【概要】Q 個の整数について、それぞれが素数かどうか判定する。
# 【アルゴリズム】試し割り法
#   N が合成数 ⇔ N に √N 以下の約数が存在する。
#   よって 2 〜 ⌊√N⌋ で割り切れるか確かめれば十分。
# 【計算量】1 回あたり O(√N), 全体 O(Q √max(X))
# =====================================================================

# N が素数のとき True を返す関数。
def isPrime(N):
    LIMIT = int(N ** 0.5)  # √N (整数)
    for i in range(2, LIMIT + 1):
        if N % i == 0:
            return False   # 割り切れた → 素数でない
    return True


# --- 入力・処理 -----------------------------------------------------
Q = int(input())
X = [None] * Q
for i in range(Q):
    X[i] = int(input())

# --- 各クエリに答える -----------------------------------------------
for i in range(Q):
    Answer = isPrime(X[i])
    if Answer == True:
        print("Yes")
    else:
        print("No")
