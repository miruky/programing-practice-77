# =====================================================================
# B31: Divisors Hard (A31 応用 ─ 包除原理)
# ---------------------------------------------------------------------
# 【概要】1 以上 N 以下の整数のうち、3 の倍数・5 の倍数・7 の倍数の
#         いずれかに該当するものの個数を求める。
# 【アルゴリズム】包除原理。
#         |A∪B∪C| = |A|+|B|+|C| − |A∩B|−|A∩C|−|B∩C| + |A∩B∩C|
#         「k の倍数の個数」は N // k で求まる。
# 【計算量】O(1)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())

# --- 各倍数集合の個数 ----------------------------------------------
A1 = N //   3      # 3 の倍数の個数
A2 = N //   5      # 5 の倍数の個数
A3 = N //   7      # 7 の倍数の個数
A4 = N //  15      # 3 と 5 の公倍数 (LCM = 15) の個数
A5 = N //  21      # 3 と 7 の公倍数 (LCM = 21) の個数
A6 = N //  35      # 5 と 7 の公倍数 (LCM = 35) の個数
A7 = N // 105      # 3 と 5 と 7 の公倍数 (LCM = 105) の個数

# --- 包除原理で和集合の大きさを計算 --------------------------------
print(A1 + A2 + A3 - A4 - A5 - A6 + A7)
