# C10: A Long Grid (力試し問題)
# ---------------------------------------------------------------------
# 【概要】2 行 W 列のグリッドを正しく塗り分ける方法数を mod 10⁹+7 で求める。
#         隣接 (左右・上下) は異なる色でなければならない (色は 4 色)。
# 【アルゴリズム】数学的解析: 1 列目の塗り方は 4 × 3 = 12 通り。それ以降の
#                 各列は 7 通り (前列に依存)。よって答え = 12 · 7^(W-1)。
#         7^(W-1) は繰り返し二乗法で O(log W)。
# 【計算量】O(log W)。W = 10⁹ でも約 30 回ループで終わる。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 数学 / 繰り返し二乗法 / 難易度目安: ★3
# - 要約: 2 行 W 列の塗り分け方法数 = 12 · 7^(W-1) を繰り返し二乗法で mod 計算。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。
# ---------------------------------------------------------------------

mod = 1000000007

# a^b mod m を返す関数 (繰り返し二乗法)
def Power(a, b, m):
    p = a
    Answer = 1
    for i in range(60):
        wari = 1 << i
        if (b // wari) % 2 == 1:
            Answer = (Answer * p) % m
        p = (p * p) % m
    return Answer

# --- 入力 -----------------------------------------------------------
W = int(input())

# --- 出力 -----------------------------------------------------------
print(12 * Power(7, W - 1, mod) % mod)
