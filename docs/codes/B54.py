# =====================================================================
# B54: Counting Same Values (A54 応用 ─ 連想配列)
# ---------------------------------------------------------------------
# 【概要】数列 A から「同じ値を持つペア (i, j)、i < j」の個数を数える。
# 【アルゴリズム】辞書 Map で「これまでに各値が出現した回数」を保持し、
#                 新しい a が出るたびに「過去の同値出現回数」を答えに加える。
#                 加えた後、Map[a] を 1 増やす。
# 【計算量】O(N)。
# =====================================================================

Map = {}

# --- 入力 -----------------------------------------------------------
N = int(input())
A = [int(input()) for _ in range(N)]

# --- 累積で同値ペア数を計算 ----------------------------------------
Answer = 0
for a in A:
    Answer += Map.get(a, 0)              # 過去に a が現れた回数だけペアができる
    Map[a] = Map.get(a, 0) + 1

# --- 出力 -----------------------------------------------------------
print(Answer)
