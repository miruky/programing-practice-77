# # B54: Counting Same Values (A54 応用 ─ 連想配列)
#
# ## 概要
#
# 数列 A から「同じ値を持つペア (i, j)、i < j」の個数を数える。
#
# ## アルゴリズム
#
# 辞書 Map で「これまでに各値が出現した回数」を保持し、
#                 新しい a が出るたびに「過去の同値出現回数」を答えに加える。
#                 加えた後、Map[a] を 1 増やす。
#
# ## 計算量
#
# O(N)。
#
# ## 読み方の地図
#
# - 問題の型: データ構造 / 辞書 / 難易度目安: ★2
# - 要約: 同じ値のペア数を、辞書で出現回数を持ちながら累積で数える。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。

Map = {}

# --- 入力 -----------------------------------------------------------
N = int(input())
A = [int(input()) for _ in range(N)]

# --- 累積で同値ペア数を計算 ----------------------------------------
Answer = 0
for a in A:
    Answer += Map.get(a, 0)              # 過去に a が現れた回数だけペアができる
    Map[a] = Map.get(a, 0) + 1

# --- 出力 -----------------------------------------------------------
print(Answer)
