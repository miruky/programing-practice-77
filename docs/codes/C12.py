# =====================================================================
# C12: Taro the Novel Writer (力試し問題)
# ---------------------------------------------------------------------
# 【概要】N ページの小説を K 章に分割する。M 個の「つながり (A_j, B_j)」が
#         あり、l〜r ページが同じ章に入ると 1 つ得点。得点最大化。
# 【アルゴリズム】2 次元 DP。
#         dp[i][j] = 「i 章まで使い、j ページ目で章が終わる」最大得点
#         遷移: dp[i][j] = max_{k=0..j-1} (dp[i-1][k] + tsunagari(k+1, j))
#         (k+1 ページ目〜j ページ目を 1 章にまとめると tsunagari つながる)
# 【計算量】O(K · N² · M)。N, K, M が小さい場合限定 (50〜300 程度を想定)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, M, K = map(int, input().split())
A = [0] * (M + 1)
B = [0] * (M + 1)
for i in range(1, M + 1):
    A[i], B[i] = map(int, input().split())

# [l, r] ページに完全に含まれるつながりの数を返す
def tsunagari(l, r):
    return sum(1 for i in range(1, M + 1) if l <= A[i] and B[i] <= r)

# --- DP -------------------------------------------------------------
NEG = -10 ** 9
dp = [[NEG] * (N + 1) for _ in range(K + 1)]
dp[0][0] = 0

for i in range(1, K + 1):
    for j in range(1, N + 1):
        # k = 「前の章の終わりのページ」
        for k in range(0, j):
            v = dp[i - 1][k] + tsunagari(k + 1, j)
            if v > dp[i][j]:
                dp[i][j] = v

# --- 出力 -----------------------------------------------------------
print(dp[K][N])
