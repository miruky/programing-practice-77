# # C12: Taro the Novel Writer (力試し問題)
#
# ## 概要
#
# N ページの小説を K 章に分割する。M 個の「つながり (A_j, B_j)」が
#         あり、l〜r ページが同じ章に入ると 1 つ得点。得点最大化。
#
# ## アルゴリズム
#
# 2 次元 DP。
#         dp[i][j] = 「i 章まで使い、j ページ目で章が終わる」最大得点
#         遷移: dp[i][j] = max_{k=0..j-1} (dp[i-1][k] + tsunagari(k+1, j))
#         (k+1 ページ目〜j ページ目を 1 章にまとめると tsunagari つながる)
#
# ## 計算量
#
# O(K · N² · M)。N, K, M が小さい場合限定 (50〜300 程度を想定)。
#
# ## 読み方の地図
#
# - 問題の型: DP / 区間 / 難易度目安: ★4
# - 要約: 本を K 章に分割し、章内に含まれるつながり総数を最大化する 2 次元 DP。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。

# --- 入力 -----------------------------------------------------------
N, M, K = map(int, input().split())
A = [0] * (M + 1)
B = [0] * (M + 1)
for i in range(1, M + 1):
    A[i], B[i] = map(int, input().split())

# [l, r] ページに完全に含まれるつながりの数を返す
def tsunagari(l, r):
    return sum(1 for i in range(1, M + 1) if l <= A[i] and B[i] <= r)

# --- DP -------------------------------------------------------------
NEG = -10 ** 9
dp = [[NEG] * (N + 1) for _ in range(K + 1)]
dp[0][0] = 0

for i in range(1, K + 1):
    for j in range(1, N + 1):
        # k = 「前の章の終わりのページ」
        for k in range(0, j):
            v = dp[i - 1][k] + tsunagari(k + 1, j)
            if v > dp[i][j]:
                dp[i][j] = v

# --- 出力 -----------------------------------------------------------
print(dp[K][N])
