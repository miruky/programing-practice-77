# # A32: 後退解析ゲーム
#
# ## 概要
#
# 石が N 個。各手番で A 個か B 個取れる。取れない方が負け。
#         先手必勝なら First, 後手必勝なら Second を出力する。
#
# ## アルゴリズム
#
# 後退解析 DP
#   dp[i] = "残り i 個のとき、手番の人が勝てるか"
#   dp[i] = True ⇔ "相手を負け状態 (dp[?] = False) に追い込める手がある"
#
# ## 計算量
#
# O(N)
#
# ## 読み方の地図
#
# - 問題の型: ゲーム / DP / 難易度目安: ★2
# - 要約: 勝ち負け状態を DP で後退解析する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。

# --- 入力 -----------------------------------------------------------
N, A, B = map(int, input().split())

# dp[i] = True ⇔ "残り i 個で手番の人が勝てる"
dp = [None] * (N + 1)

# 小さい方から決める (後退解析: 終端 0 が負け状態, dp[0]=False)
for i in range(N + 1):
    if i >= A and dp[i - A] == False:
        # A 個取って相手を負けに追い込めるなら勝ち
        dp[i] = True
    elif i >= B and dp[i - B] == False:
        # B 個取って相手を負けに追い込めるなら勝ち
        dp[i] = True
    else:
        # どちらの手も相手の負けにならない → この状態は負け
        dp[i] = False

# --- 出力 -----------------------------------------------------------
if dp[N] == True:
    print("First")
else:
    print("Second")
