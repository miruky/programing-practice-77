# # B64: Shortest Path with Restoration (A64 応用 ─ ダイクストラ + 復元)
#
# ## 概要
#
# 重み付き無向グラフで、頂点 1 から N までの最短経路を 1 本復元
#         して頂点列で出力する。
#
# ## アルゴリズム
#
# N から逆向きにダイクストラ法を実行し、各頂点について
#                 「最短経路で来たときの直前の頂点 back[]」を覚えておく。
#                 復元時は頂点 1 から back を辿るだけ。
#
# ## 計算量
#
# O((N + M) log N)。

from heapq import heappush, heappop
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: グラフ / ダイクストラ / 経路復元 / 難易度目安: ★5
# - 要約: ダイクストラ法で最短経路長と経路そのものを復元して出力する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - cur[v] は「今わかっている最短距離」。確定前なので後から小さくなる可能性がある。
# - ヒープから距離最小の頂点を取り出した瞬間、その頂点の距離を確定させる。
# - 古い候補がヒープに残るので、確定済みならスキップする行が必要になる。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N, M = map(int, input().split())
g = [[] for _ in range(N)]
for _ in range(M):
    A, B, C = map(int, input().split())
    A -= 1
    B -= 1
    g[A].append((B, C))
    g[B].append((A, C))

INF = 1 << 61
cost = [INF] * N
back = [-1] * N
q = []


# 「頂点 i にコスト c (前頂点 prev) で来る」共通処理
def push(prev: int, i: int, c: int):
    if cost[i] <= c:
        return
    cost[i] = c
    back[i] = prev
    heappush(q, (c, i))


# --- N-1 始点で逆向きダイクストラ ----------------------------------
# 経路復元の都合で「ゴール側から」探索する。
push(-1, N - 1, 0)
while q:
    c, x = heappop(q)
    if cost[x] != c:
        continue                          # 同じ頂点の古いエントリ ─ 捨てる
    for j, d in g[x]:
        push(x, j, c + d)

# --- 頂点 0 から back を辿って経路復元 -----------------------------
ans = [0]
while ans[-1] != N - 1:
    ans.append(back[ans[-1]])

# --- 出力 -----------------------------------------------------------
for x in ans:
    print(x + 1)
