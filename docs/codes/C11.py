# =====================================================================
# C11: Election (力試し問題)
# ---------------------------------------------------------------------
# 【概要】N 政党それぞれの得票数 A[i] と総議席数 K が与えられる。
#         ドント方式: 各政党の議席は floor(A[i] / x) (x は「割り算の値」)。
#         合計議席数がちょうど K となる最大の x を求め、その x で割って
#         得られる各政党の議席数を出力する。
# 【アルゴリズム】x を二分探索 (実数)。 sum(A[i] // x) は x の単調非増加関数。
# 【計算量】O(N · 反復回数 60)。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 二分探索 / 実数 / 難易度目安: ★3
# - 要約: ドント方式の議席配分 ─ 「割り算の値 x」を実数二分探索で求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 二分探索は値を探すというより、条件が NG から OK に切り替わる境目を探す。
# - bisect_left は「その値を入れてもソートが崩れない一番左の位置」を返す。
# - LIS や座標圧縮では、配列そのものではなく順位・置き場所を管理する。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N, K = map(int, input().split())
A = [0.0] + list(map(float, input().split()))

# 割り算の値が x のときの議席数の合計
def check(x):
    return sum(int(A[i] / x) for i in range(1, N + 1))

# --- 実数の二分探索 -------------------------------------------------
Left = 1.0
Right = 1_000_000_000.0
Border = 0.0                              # K 以上を達成できた最大の x

for _ in range(60):
    Mid = (Left + Right) / 2.0
    if check(Mid) >= K:
        Left = Mid                        # 議席が K 以上なら更に大きい x を試せる
        if Border < Mid:
            Border = Mid
    else:
        Right = Mid

# --- 出力 (各政党の議席数を空白区切りで) ----------------------------
print(" ".join(str(int(A[i] / Border)) for i in range(1, N + 1)))
