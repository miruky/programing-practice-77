# # A64: ダイクストラ法 (重み付き最短距離)
#
# ## 概要
#
# 非負重みグラフで頂点 1 から各頂点までの最短距離を求める。
#         到達できない頂点は -1 を出力。
#
# ## アルゴリズム
#
# ダイクストラ法 (優先度付きキューを使う実装)
#   - 確定: 既に最短距離が確定した頂点
#   - 未確定の中から最小距離の頂点を取り出し、隣接頂点を更新する
#
# ## 計算量
#
# O((N + M) log N)
#
# ## ポイント
#
# 既に確定済みの頂点をキューから取り出したらスキップする。
#
# ## 読み方の地図
#
# - 問題の型: グラフ / ダイクストラ / 難易度目安: ★4
# - 要約: ヒープを使ったダイクストラ法で重み付き最短経路を求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - cur[v] は「今わかっている最短距離」。確定前なので後から小さくなる可能性がある。
# - ヒープから距離最小の頂点を取り出した瞬間、その頂点の距離を確定させる。
# - 古い候補がヒープに残るので、確定済みならスキップする行が必要になる。

import heapq

# --- 入力 -----------------------------------------------------------
N, M = map(int, input().split())
edges = [list(map(int, input().split())) for _ in range(M)]

# --- 隣接リスト (重み付き) ----------------------------------------
G = [list() for _ in range(N + 1)]
for a, b, c in edges:
    G[a].append((b, c))
    G[b].append((a, c))

# --- 初期化 -------------------------------------------------------
INF = 10 ** 10
kakutei = [False] * (N + 1)  # True なら最短距離が確定
cur = [INF] * (N + 1)        # 暫定的な最短距離
cur[1] = 0
Q = []
heapq.heappush(Q, (cur[1], 1))  # (距離, 頂点番号)

# --- ダイクストラ法 ------------------------------------------------
while len(Q) >= 1:
    # 最小値を取り出す
    pos = heapq.heappop(Q)[1]

    # 既に確定済みの頂点ならスキップ
    if kakutei[pos] == True:
        continue

    kakutei[pos] = True
    # 隣接頂点の暫定距離を更新
    for e in G[pos]:
        if cur[e[0]] > cur[pos] + e[1]:
            cur[e[0]] = cur[pos] + e[1]
            heapq.heappush(Q, (cur[e[0]], e[0]))

# --- 出力 -----------------------------------------------------------
for i in range(1, N + 1):
    if cur[i] != INF:
        print(cur[i])
    else:
        print("-1")
