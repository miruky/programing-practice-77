# =====================================================================
# A09: 2次元いもす法
# ---------------------------------------------------------------------
# 【概要】N 個の矩形領域への "+1" 操作を行い、最終的な各セルの値を答える。
# 【アルゴリズム】2 次元いもす法
#   矩形 (A,B)-(C,D) への加算は、4 隅への符号付き加算で表現できる:
#     X[A][B]   += 1
#     X[A][D+1] -= 1
#     X[C+1][B] -= 1
#     X[C+1][D+1] += 1
#   その後 2 次元累積和を取れば、各セルの最終値が得られる。
# 【計算量】O(N + HW)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 累積和 / いもす法 / 2次元 / 難易度目安: ★3
# - 要約: 矩形領域への加算を四隅の操作だけで表現し、2 次元累積和で復元。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 長方形の全マスを直接更新せず、四隅に +1/-1 の印だけ置く。
# - 印を置いた時点では答えではない。横方向・縦方向の累積和で面に戻す。
# - 閉区間か半開区間かで、-1 を置く座標が 1 つずれるので最初に確認する。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
H, W, N = map(int, input().split())
A = [None] * N
B = [None] * N
C = [None] * N
D = [None] * N
for t in range(N):
    A[t], B[t], C[t], D[t] = map(int, input().split())

# --- 差分配列に矩形加算を 4 隅でエンコード --------------------------
# サイズに余裕を持たせて D[t]+1, C[t]+1 のアクセスを安全にする。
X = [[0] * (W + 2) for _ in range(H + 2)]
Z = [[0] * (W + 2) for _ in range(H + 2)]
for t in range(N):
    X[A[t]][B[t]]         += 1
    X[A[t]][D[t] + 1]     -= 1
    X[C[t] + 1][B[t]]     -= 1
    X[C[t] + 1][D[t] + 1] += 1

# --- 2 次元累積和で復元 --------------------------------------------
# 差分配列 X から、各マスが何回加算されたかを復元する。
# Z[i][j] は「左上から (i,j) までの X の合計」なので、A08 と同じ式で作れる。
for i in range(1, H + 1):
    for j in range(1, W + 1):
        Z[i][j] = X[i][j] + Z[i - 1][j] + Z[i][j - 1] - Z[i - 1][j - 1]

# --- 出力 -----------------------------------------------------------
# 行ごとに空白区切りで出力する。
for i in range(1, H + 1):
    for j in range(1, W + 1):
        if j >= 2:
            print(" ", end="")
        print(Z[i][j], end="")
    print("")
