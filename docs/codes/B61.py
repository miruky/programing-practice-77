# =====================================================================
# B61: Influencer (A61 応用 ─ 隣接リストと次数)
# ---------------------------------------------------------------------
# 【概要】N 人 M 組の友達関係から、最も友達が多い人 (タイ時は番号が最小)
#         を 1 番から数えた番号で出力する。
# 【アルゴリズム】各辺 (A, B) を読みながら friend[A], friend[B] を +1。
#                 後で最大値とその index を取れば良い。
# 【計算量】O(N + M)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, M = map(int, input().split())

# --- 各人の友達数 (次数) -------------------------------------------
friend = [0] * N
for _ in range(M):
    A, B = map(int, input().split())
    A -= 1
    B -= 1
    friend[A] += 1
    friend[B] += 1

# --- 最大値とその index ---------------------------------------------
mx = max(friend)
ans = friend.index(mx)                    # 同点の場合は index が小さい方が先
print(ans + 1)
