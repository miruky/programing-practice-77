# # B61: Influencer (A61 応用 ─ 隣接リストと次数)
#
# ## 概要
#
# N 人 M 組の友達関係から、最も友達が多い人 (タイ時は番号が最小)
#         を 1 番から数えた番号で出力する。
#
# ## アルゴリズム
#
# 各辺 (A, B) を読みながら friend[A], friend[B] を +1。
#                 後で最大値とその index を取れば良い。
#
# ## 計算量
#
# O(N + M)。
#
# ## 読み方の地図
#
# - 問題の型: グラフ / 難易度目安: ★2
# - 要約: 辺の本数から各頂点の友達数を集計し、最大の頂点を答える。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 入力をまず隣接リストやグリッドの隣接関係に変換している部分を見る。
# - visited や dist は「もう見たか」「何手で来たか」を記録するための配列。
# - DFS は深く進む、BFS は近い順に広がる。この違いだけ先に押さえる。

# --- 入力 -----------------------------------------------------------
N, M = map(int, input().split())

# --- 各人の友達数 (次数) -------------------------------------------
friend = [0] * N
for _ in range(M):
    A, B = map(int, input().split())
    A -= 1
    B -= 1
    friend[A] += 1
    friend[B] += 1

# --- 最大値とその index ---------------------------------------------
mx = max(friend)
ans = friend.index(mx)                    # 同点の場合は index が小さい方が先
print(ans + 1)
