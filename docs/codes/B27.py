# # B27: Calculate LCM (A27 応用 ─ 最小公倍数)
#
# ## 概要
#
# 2 つの正整数 A, B の最小公倍数 (LCM) を求める。
#
# ## アルゴリズム
#
# LCM(A, B) = A · B ÷ GCD(A, B)。
#                 GCD はユークリッド互除法で O(log min(A, B))。
#
# ## 計算量
#
# O(log min(A, B))。
#
# ## ポイント
#
# A * B を先に計算してオーバーフローしないか確認 (Python は
#             多倍長整数なので問題ない)。割り算は // で整数除算。
#
# ## 読み方の地図
#
# - 問題の型: 数学 / LCM / GCD / 難易度目安: ★2
# - 要約: GCD を求め、A×B÷GCD で最小公倍数 (LCM) を計算する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。

# 最大公約数 (GCD) ─ 互除法をループで書いた版。
def GCD(A, B):
    while A >= 1 and B >= 1:
        if A >= B:
            A = A % B                     # 大きい方を小さい方で割った余りに置換
        else:
            B = B % A
    # 片方が 0 になったら、もう片方が GCD。
    if A >= 1:
        return A
    return B

# --- 入力 & 出力 ---------------------------------------------------
A, B = map(int, input().split())
print(A * B // GCD(A, B))
