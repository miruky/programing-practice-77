# B27: Calculate LCM (A27 応用 ─ 最小公倍数)
# ---------------------------------------------------------------------
# 【概要】2 つの正整数 A, B の最小公倍数 (LCM) を求める。
# 【アルゴリズム】LCM(A, B) = A · B ÷ GCD(A, B)。
#                 GCD はユークリッド互除法で O(log min(A, B))。
# 【計算量】O(log min(A, B))。
# 【ポイント】A * B を先に計算してオーバーフローしないか確認 (Python は
#             多倍長整数なので問題ない)。割り算は // で整数除算。
# =====================================================================

# 最大公約数 (GCD) ─ 互除法をループで書いた版。
def GCD(A, B):
    while A >= 1 and B >= 1:
        if A >= B:
            A = A % B                     # 大きい方を小さい方で割った余りに置換
        else:
            B = B % A
    # 片方が 0 になったら、もう片方が GCD。
    if A >= 1:
        return A
    return B

# --- 入力 & 出力 ---------------------------------------------------
A, B = map(int, input().split())
print(A * B // GCD(A, B))
