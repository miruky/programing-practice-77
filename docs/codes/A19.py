# =====================================================================
# A19: ナップサック問題
# ---------------------------------------------------------------------
# 【概要】N 個の品物 (重さ w[i], 価値 v[i]) から、重量制限 W 以下で
#         価値合計を最大化する選び方を求める。
# 【アルゴリズム】2 次元 DP
#   dp[i][j] = 最初の i 個から合計重さ j を作れる時の最大価値
#   選ばない: dp[i][j] = dp[i-1][j]
#   選ぶ    : dp[i][j] = dp[i-1][j-w[i]] + v[i]   (j>=w[i] のとき)
# 【計算量】O(NW)
# 【ポイント】到達不可能な状態は -∞ で初期化し、max が壊れないようにする。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, W = map(int, input().split())
w = [None] * (N + 1)
v = [None] * (N + 1)
for i in range(1, N + 1):
    w[i], v[i] = map(int, input().split())

# --- DP テーブル初期化 ----------------------------------------------
# 到達不能を「とても小さい値」で初期化する典型テクニック。
dp = [[-10 ** 15] * (W + 1) for _ in range(N + 1)]
dp[0][0] = 0

# --- DP 遷移 -------------------------------------------------------
for i in range(1, N + 1):
    for j in range(0, W + 1):
        if j < w[i]:
            # i 番目を選ぶ余裕がない
            dp[i][j] = dp[i - 1][j]
        if j >= w[i]:
            # 選ばない vs 選ぶ の大きい方を取る
            dp[i][j] = max(dp[i - 1][j], dp[i - 1][j - w[i]] + v[i])

# --- 出力 (重量上限以内の最大価値) ----------------------------------
print(max(dp[N]))
