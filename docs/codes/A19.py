# =====================================================================
# A19: ナップサック問題
# ---------------------------------------------------------------------
# 【概要】N 個の品物 (重さ w[i], 価値 v[i]) から、重量制限 W 以下で
#         価値合計を最大化する選び方を求める。
# 【アルゴリズム】2 次元 DP
#   dp[i][j] = 最初の i 個から合計重さ j を作れる時の最大価値
#   選ばない: dp[i][j] = dp[i-1][j]
#   選ぶ    : dp[i][j] = dp[i-1][j-w[i]] + v[i]   (j>=w[i] のとき)
# 【計算量】O(NW)
# 【ポイント】到達不可能な状態は -∞ で初期化し、max が壊れないようにする。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: DP / ナップサック / 難易度目安: ★3
# - 要約: 重量制限のもとで価値を最大化する典型 DP。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N, W = map(int, input().split())
w = [None] * (N + 1)
v = [None] * (N + 1)
for i in range(1, N + 1):
    w[i], v[i] = map(int, input().split())

# --- DP テーブル初期化 ----------------------------------------------
# 到達不能を「とても小さい値」で初期化する典型テクニック。
dp = [[-10 ** 15] * (W + 1) for _ in range(N + 1)]
dp[0][0] = 0

# --- DP 遷移 -------------------------------------------------------
for i in range(1, N + 1):
    for j in range(0, W + 1):
        if j < w[i]:
            # i 番目を選ぶ余裕がない
            dp[i][j] = dp[i - 1][j]
        if j >= w[i]:
            # 選ばない vs 選ぶ の大きい方を取る
            dp[i][j] = max(dp[i - 1][j], dp[i - 1][j - w[i]] + v[i])

# --- 出力 (重量上限以内の最大価値) ----------------------------------
print(max(dp[N]))
