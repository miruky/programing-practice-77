# =====================================================================
# B21: Longest Subpalindrome (A21 応用 ─ 区間 DP)
# ---------------------------------------------------------------------
# 【概要】文字列 S の中の最長回文「部分列」の長さを求める。連続でなくてよい。
# 【アルゴリズム】区間 DP。
#         dp[l][r] = S[l..r] の最長回文部分列の長さ。
#         S[l] == S[r] のとき: max(dp[l][r-1], dp[l+1][r], dp[l+1][r-1] + 2)
#         そうでないとき: max(dp[l][r-1], dp[l+1][r])
# 【計算量】O(N²)。
# 【ポイント】区間長 LEN を外側でループするのが区間 DP の定石。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())
S = input()

# --- DP の初期状態 (長さ 1 と長さ 2) -------------------------------
dp = [[None] * N for _ in range(N)]
for i in range(N):
    dp[i][i] = 1                          # 長さ 1 は必ず回文
for i in range(N - 1):
    dp[i][i + 1] = 2 if S[i] == S[i + 1] else 1

# --- 区間長を 2 から N まで増やしながら遷移 ------------------------
for LEN in range(2, N):
    for l in range(N - LEN):
        r = l + LEN
        if S[l] == S[r]:
            dp[l][r] = max(dp[l][r - 1],
                           dp[l + 1][r],
                           dp[l + 1][r - 1] + 2)
        else:
            dp[l][r] = max(dp[l][r - 1],
                           dp[l + 1][r])

# --- 出力 -----------------------------------------------------------
print(dp[0][N - 1])
