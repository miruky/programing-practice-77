# =====================================================================
# A16: ダンジョン1 (基礎 DP)
# ---------------------------------------------------------------------
# 【概要】部屋 1 から部屋 N まで、1 個進む (cost A) か 2 個進む (cost B)
#         を選んで進む。最小コストを求める。
# 【アルゴリズム】1 次元動的計画法
#   dp[i] = 部屋 i にたどり着く最小コスト
#   dp[i] = min(dp[i-1] + A[i-2], dp[i-2] + B[i-3])
# 【計算量】O(N)
# 【ポイント】配列 A, B が 0-indexed なので添字は -2, -3 とずれる。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: DP / 難易度目安: ★2
# - 要約: 1 次元 DP の基本問題。最小コストで部屋 N にたどり着く。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))
B = list(map(int, input().split()))

# --- DP --------------------------------------------------------------
# dp[1] = 0 (スタート), dp[2] = A[0] (1 → 2 で確定)
dp = [None] * (N + 1)
dp[1] = 0
dp[2] = A[0]
for i in range(3, N + 1):
    # i-1 から 1 進む  or  i-2 から 2 進む
    dp[i] = min(dp[i - 1] + A[i - 2], dp[i - 2] + B[i - 3])

# --- 出力 -----------------------------------------------------------
print(dp[N])
