# =====================================================================
# A16: ダンジョン1 (基礎 DP)
# ---------------------------------------------------------------------
# 【概要】部屋 1 から部屋 N まで、1 個進む (コスト A_i) か
#         2 個進む (コスト B_i) を選んで進む。最小の合計コストを求める。
# 【アルゴリズム】1 次元動的計画法
#   dp[i] = 部屋 i にたどり着くまでの最小コスト
#   dp[i] = min(dp[i-1] + A[i], dp[i-2] + B[i])
# 【計算量】O(N)
# 【実装メモ】書籍の数式は 1-indexed (A[i], B[i] が「部屋 i に到着するコスト」)。
#             Python の list は 0-indexed なので、先頭にダミー 0 を詰めて
#             A[2..N], B[3..N] を本と同じ添字で読めるようにする。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: DP / 難易度目安: ★2
# - 要約: 1 次元 DP の基本問題。最小コストで部屋 N にたどり着く。
# - dp[i] の意味を 1 行で固定する: 「部屋 i に到着するまでの最小コスト」。
# - 初期状態は dp[1] = 0 (まだ動いていない) と dp[2] = A[2] (1→2 で確定) のみ。
# - 漸化式は「i-1 から 1 進む or i-2 から 2 進む」の小さい方。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N = int(input())
# A の i 番目 (i = 2..N) は「部屋 i-1 から部屋 i へ進むコスト」。
# B の i 番目 (i = 3..N) は「部屋 i-2 から部屋 i へ進むコスト」。
# 入力は 0-indexed で並んでいるため、先頭にダミーを詰めて 1-indexed に揃える。
A = [0, 0] + list(map(int, input().split()))      # A[2], A[3], ..., A[N]
B = [0, 0, 0] + list(map(int, input().split()))   # B[3], B[4], ..., B[N]

# --- DP --------------------------------------------------------------
dp = [0] * (N + 1)
dp[1] = 0          # スタート地点。まだ動いていないのでコスト 0。
dp[2] = A[2]       # 部屋 2 へは 1 → 2 と 1 個進むしかない。
for i in range(3, N + 1):
    # i-1 から 1 進む (コスト A[i])  or  i-2 から 2 進む (コスト B[i])
    dp[i] = min(dp[i - 1] + A[i], dp[i - 2] + B[i])

# --- 出力 -----------------------------------------------------------
print(dp[N])
