# =====================================================================
# A73: 2 目的のダイクストラ (距離 + 木の本数)
# ---------------------------------------------------------------------
# 【概要】複数の道路で N 頂点を結ぶ。距離の最小化を最優先、
#         同距離の中で木の本数 (d=1 の本数) も最少化したい。
# 【アルゴリズム】"重み = 距離 * 10000 - (木の有無)" として 1 本のダイクストラに集約。
#   - 距離は最大 10000 倍されるため、最優先の評価基準として効く。
#   - その下位ビットで木の有無を表現する。
# 【計算量】O((N+M) log N)
# =====================================================================

import heapq

# --- 入力 -----------------------------------------------------------
N, M = map(int, input().split())
roads = [list(map(int, input().split())) for _ in range(M)]

# --- グラフ構築 -------------------------------------------------
# d == 1 (木がある) なら 10000c - 1, そうでなければ 10000c に
G = [list() for _ in range(N + 1)]
for a, b, c, d in roads:
    if d == 1:
        G[a].append((b, 10000 * c - 1))
        G[b].append((a, 10000 * c - 1))
    else:
        G[a].append((b, 10000 * c))
        G[b].append((a, 10000 * c))

# --- ダイクストラ (詳細は A64 参照) ------------------------------
INF = 10 ** 10
kakutei = [False] * (N + 1)
cur = [INF] * (N + 1)
cur[1] = 0
Q = []
heapq.heappush(Q, (cur[1], 1))
while len(Q) >= 1:
    pos = heapq.heappop(Q)[1]
    if kakutei[pos] == True:
        continue
    kakutei[pos] = True
    for e in G[pos]:
        if cur[e[0]] > cur[pos] + e[1]:
            cur[e[0]] = cur[pos] + e[1]
            heapq.heappush(Q, (cur[e[0]], e[0]))

# --- 結果の解読 -------------------------------------------------
# distance = 上位 (10000 で割って切り上げ)
# num_trees = 下位 ((distance * 10000) と cur[N] の差)
distance = (cur[N] + 9999) // 10000
num_trees = distance * 10000 - cur[N]
print(distance, num_trees)
