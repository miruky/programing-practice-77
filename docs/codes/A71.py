# =====================================================================
# A71: 並び替えによる積和の最小化
# ---------------------------------------------------------------------
# 【概要】2 つの長さ N の配列 A, B を並び替えて A[i]*B[i] の総和を
#         最小化する。
# 【アルゴリズム】貪欲法 (並び替え不等式)
#   - 一方を昇順、もう一方を降順に並べると積和は最小。
#   - 直観: 大きい数 × 大きい数 を作らない。
# 【計算量】O(N log N)
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))
B = list(map(int, input().split()))

# --- 並び替え ---------------------------------------------------
A.sort()                  # 昇順
B.sort(reverse=True)      # 降順

# --- 答えは A[i] * B[i] の総和 -----------------------------------
print(sum([A[i] * B[i] for i in range(N)]))
