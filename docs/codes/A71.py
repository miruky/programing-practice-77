# =====================================================================
# A71: 並び替えによる積和の最小化
# ---------------------------------------------------------------------
# 【概要】2 つの長さ N の配列 A, B を並び替えて A[i]*B[i] の総和を
#         最小化する。
# 【アルゴリズム】貪欲法 (並び替え不等式)
#   - 一方を昇順、もう一方を降順に並べると積和は最小。
#   - 直観: 大きい数 × 大きい数 を作らない。
# 【計算量】O(N log N)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 貪欲 / ソート / 難易度目安: ★2
# - 要約: 大小の積を最小化するときは昇順×降順の足し算が最小。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 貪欲法は、何を基準に並べるか・どの候補を先に取るかが本体。
# - コードより先に「その選択をしても後で損しない理由」をコメントで確認する。
# - ソート後は、左から順に決めて戻らない構造になっているかを見る。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))
B = list(map(int, input().split()))

# --- 並び替え ---------------------------------------------------
A.sort()                  # 昇順
B.sort(reverse=True)      # 降順

# --- 答えは A[i] * B[i] の総和 -----------------------------------
print(sum([A[i] * B[i] for i in range(N)]))
