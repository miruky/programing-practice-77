# =====================================================================
# A40: 個数集計と組合せ
# ---------------------------------------------------------------------
# 【概要】配列 A から、同じ値の 3 つの組 (i<j<k) の選び方の数を求める。
# 【アルゴリズム】値ごとに個数 cnt を数え、組合せ C(cnt, 3) を合計する。
#   C(n, 3) = n*(n-1)*(n-2) / 6
# 【計算量】O(N + 100)
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- 値ごとの出現回数を数える ----------------------------------------
cnt = [0] * 101
for i in range(N):
    cnt[A[i]] += 1

# --- C(cnt[i], 3) の総和を答える ------------------------------------
Answer = 0
for i in range(1, 101):
    Answer += cnt[i] * (cnt[i] - 1) * (cnt[i] - 2) // 6
print(Answer)
