# =====================================================================
# A40: 個数集計と組合せ
# ---------------------------------------------------------------------
# 【概要】配列 A から、同じ値の 3 つの組 (i<j<k) の選び方の数を求める。
# 【アルゴリズム】値ごとに個数 cnt を数え、組合せ C(cnt, 3) を合計する。
#   C(n, 3) = n*(n-1)*(n-2) / 6
# 【計算量】O(N + 100)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 数え上げ / 組合せ / 難易度目安: ★2
# - 要約: 値ごとの個数を数え、組合せ的に数え上げる。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- 値ごとの出現回数を数える ----------------------------------------
cnt = [0] * 101
for i in range(N):
    cnt[A[i]] += 1

# --- C(cnt[i], 3) の総和を答える ------------------------------------
Answer = 0
for i in range(1, 101):
    Answer += cnt[i] * (cnt[i] - 1) * (cnt[i] - 2) // 6
print(Answer)
