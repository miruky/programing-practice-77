# # A21: 区間 DP (ブロック取りゲーム)
#
# ## 概要
#
# N 個のブロックがあり、それぞれに P[i] と A[i] の情報がある。
#         ブロックを両端から取り除くタイプの最大スコア問題。
#
# ## アルゴリズム
#
# 区間 DP
#   dp[l][r] = "現在残っている区間が [l, r]" のときの最大累積スコア
#   遷移は両端の取り外しを試す典型形。
#
# ## 計算量
#
# O(N^2)
#
# ## ポイント
#
# 区間長 LEN を外ループ、左端 l を内ループにする発想。
#
# ## 読み方の地図
#
# - 問題の型: DP / 区間DP / 難易度目安: ★4
# - 要約: 区間 [l,r] を状態にとる典型 DP（ブロック取りゲーム）。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。

# --- 入力 -----------------------------------------------------------
N = int(input())
P = [None] * (N + 1)
A = [None] * (N + 1)
for i in range(1, N + 1):
    P[i], A[i] = map(int, input().split())

# --- DP 配列の初期化 -------------------------------------------------
# dp[1][N] = 0: まだ何も取り除いていない状態
dp = [[None] * (N + 1) for _ in range(N + 1)]
dp[1][N] = 0

# 区間長を表す LEN を大きい方から小さい方へ動かす (区間を縮めていく)。
for LEN in reversed(range(0, N - 1)):
    for l in range(1, N - LEN + 1):
        r = l + LEN

        # score1: l-1 番目を「取り外す」ときに得られる得点
        # 条件 l<=P[l-1]<=r は「外す位置が現在の区間内にある」ということ
        score1 = 0
        if l >= 2 and l <= P[l - 1] and P[l - 1] <= r:
            score1 = A[l - 1]

        # score2: r+1 番目を「取り外す」ときに得られる得点
        score2 = 0
        if r <= N - 1 and l <= P[r + 1] and P[r + 1] <= r:
            score2 = A[r + 1]

        # 左端を縮める / 右端を縮める のうち最大値を採用
        if l == 1:
            dp[l][r] = dp[l][r + 1] + score2
        elif r == N:
            dp[l][r] = dp[l - 1][r] + score1
        else:
            dp[l][r] = max(dp[l - 1][r] + score1, dp[l][r + 1] + score2)

# --- 出力 (1 マスだけ残った状態のうち最大) --------------------------
Answer = 0
for i in range(1, N + 1):
    Answer = max(Answer, dp[i][i])
print(Answer)
