# # C09: Taro's Vacation (力試し問題)
#
# ## 概要
#
# N 日間あり、各日働けば A[i] の収入、ただし前日働いたら今日は休む
#         必要がある。N 日間の最大収入を求める。
#
# ## アルゴリズム
#
# 2 状態 DP。
#         dp1[i] = i 日目に「働く」場合の最大収入
#         dp2[i] = i 日目に「休む」場合の最大収入
#         遷移: dp1[i] = dp2[i-1] + A[i]      (休んだ翌日にしか働けない)
#               dp2[i] = max(dp1[i-1], dp2[i-1])
#
# ## 計算量
#
# O(N)。
#
# ## 読み方の地図
#
# - 問題の型: DP / 難易度目安: ★3
# - 要約: 「働く / 休む」の 2 状態 DP で N 日間の最大収入を求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。

# --- 入力 -----------------------------------------------------------
N = int(input())
A = [0] + list(map(int, input().split()))    # 1-indexed

# --- DP 初期化 ------------------------------------------------------
dp1 = [0] * (N + 1)                          # 「働く」状態の最大収入
dp2 = [0] * (N + 1)                          # 「休む」状態の最大収入

# --- 動的計画法 -----------------------------------------------------
for i in range(1, N + 1):
    dp1[i] = dp2[i - 1] + A[i]               # 直前は休み必須
    dp2[i] = max(dp1[i - 1], dp2[i - 1])     # 直前はどちらでも OK

# --- 出力 (N 日目に働くか休むかの大きい方) -------------------------
print(max(dp1[N], dp2[N]))
