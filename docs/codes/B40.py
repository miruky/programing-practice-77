# =====================================================================
# B40: Divide by 100 (A40 応用 ─ 余りグルーピング)
# ---------------------------------------------------------------------
# 【概要】N 個の整数 A から 2 つ選び、その和が 100 の倍数となる組の数を求める。
# 【アルゴリズム】「100 で割った余り」ごとに個数 cnt[r] を集計。
#         (i, 100-i) のペアは cnt[i] * cnt[100-i] 通り (i ≠ 50)。
#         余り 0 同士、余り 50 同士は同類選び C(cnt, 2) = cnt*(cnt-1)/2。
# 【計算量】O(N + 100)。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 数え上げ / mod / 難易度目安: ★3
# - 要約: 100 で割った余りごとに個数を数えて、和が 100 の倍数になる組を数え上げる。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- 100 で割った余りごとに個数を集計 ------------------------------
cnt = [0] * 100
for i in range(N):
    cnt[A[i] % 100] += 1

# --- ペアの個数を計算 ----------------------------------------------
Answer = 0
for i in range(1, 50):
    Answer += cnt[i] * cnt[100 - i]      # i と (100-i) は異なる余り
Answer += cnt[0]  * (cnt[0]  - 1) // 2   # 余り 0 同士
Answer += cnt[50] * (cnt[50] - 1) // 2   # 余り 50 同士 (50+50 = 100)

# --- 出力 -----------------------------------------------------------
print(Answer)
