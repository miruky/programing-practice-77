# # B33: Game 6 (A33 応用 ─ 2 次元 Nim)
#
# ## 概要
#
# H×W の盤面に N 個の駒が置かれている。各駒は左 or 下に好きな
#         距離だけ動かせる。動かせなくなった方が負け。先後判定。
#
# ## アルゴリズム
#
# 各駒について「左に動ける距離 = A[i]-1」「下に動ける距離
#                 = B[i]-1」を 2 山の Nim と見れば、(A[i]-1) と (B[i]-1) を
#                 まとめて XOR し、全駒の総 XOR が 0 でなければ先手必勝。
#
# ## 計算量
#
# O(N)。
#
# ## 読み方の地図
#
# - 問題の型: ゲーム / Nim / 難易度目安: ★4
# - 要約: 2 次元 Nim を、各駒の x-1, y-1 の XOR で勝敗判定する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 整数 state を 2 進数として見て、各 bit が ON/OFF の集合を表す。
# - 1 << k は k 番目だけが ON の集合。OR は追加、XOR は反転として読む。
# - 状態数は 2^N。N が小さいからこそ成立する全探索 + DP/探索。

# --- 入力 -----------------------------------------------------------
N, H, W = map(int, input().split())
A = [None] * N
B = [None] * N
for i in range(N):
    A[i], B[i] = map(int, input().split())

# --- ニム和を取る (各駒について A-1 と B-1 を XOR) -----------------
XOR_Sum = 0
for i in range(N):
    XOR_Sum ^= (A[i] - 1)
    XOR_Sum ^= (B[i] - 1)

# --- 出力 -----------------------------------------------------------
# Nim の基本定理: 総 XOR が 0 ⇔ 後手必勝
print("First" if XOR_Sum != 0 else "Second")
