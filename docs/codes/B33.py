# =====================================================================
# B33: Game 6 (A33 応用 ─ 2 次元 Nim)
# ---------------------------------------------------------------------
# 【概要】H×W の盤面に N 個の駒が置かれている。各駒は左 or 下に好きな
#         距離だけ動かせる。動かせなくなった方が負け。先後判定。
# 【アルゴリズム】各駒について「左に動ける距離 = A[i]-1」「下に動ける距離
#                 = B[i]-1」を 2 山の Nim と見れば、(A[i]-1) と (B[i]-1) を
#                 まとめて XOR し、全駒の総 XOR が 0 でなければ先手必勝。
# 【計算量】O(N)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, H, W = map(int, input().split())
A = [None] * N
B = [None] * N
for i in range(N):
    A[i], B[i] = map(int, input().split())

# --- ニム和を取る (各駒について A-1 と B-1 を XOR) -----------------
XOR_Sum = 0
for i in range(N):
    XOR_Sum ^= (A[i] - 1)
    XOR_Sum ^= (B[i] - 1)

# --- 出力 -----------------------------------------------------------
# Nim の基本定理: 総 XOR が 0 ⇔ 後手必勝
print("First" if XOR_Sum != 0 else "Second")
