# =====================================================================
# C05: Lucky Numbers (力試し問題)
# ---------------------------------------------------------------------
# 【概要】「4 と 7 だけからなる 10 桁以下の整数」を小さい順に並べたとき、
#         N 番目の数を求める。
# 【アルゴリズム】N - 1 を 10 桁の 2 進数とみなし、「0 → 4」「1 → 7」に
#                 置き換えれば N 番目のラッキーナンバーが得られる。
#         (1 番目=4444444444, 2 番目=4444444447, 3 番目=4444444474, ...)
# 【計算量】O(10)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())
N -= 1                                   # 0-indexed に直す

# --- 上位桁から順に 2 進数を読み、0→'4'、1→'7' に変換 -------------
out = []
for x in range(9, -1, -1):
    wari = 1 << x                        # 2^x
    if (N // wari) % 2 == 0:
        out.append("4")
    else:
        out.append("7")

# --- 出力 -----------------------------------------------------------
print("".join(out))
