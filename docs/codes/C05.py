# # C05: Lucky Numbers (力試し問題)
#
# ## 概要
#
# 「4 と 7 だけからなる 10 桁以下の整数」を小さい順に並べたとき、
#         N 番目の数を求める。
#
# ## アルゴリズム
#
# N - 1 を 10 桁の 2 進数とみなし、「0 → 4」「1 → 7」に
#                 置き換えれば N 番目のラッキーナンバーが得られる。
#         (1 番目=4444444444, 2 番目=4444444447, 3 番目=4444444474, ...)
#
# ## 計算量
#
# O(10)。
#
# ## 読み方の地図
#
# - 問題の型: 数学 / ビット / 難易度目安: ★2
# - 要約: 4 と 7 だけからなる数の N 番目を、N-1 の 2 進法で求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。

# --- 入力 -----------------------------------------------------------
N = int(input())
N -= 1                                   # 0-indexed に直す

# --- 上位桁から順に 2 進数を読み、0→'4'、1→'7' に変換 -------------
out = []
for x in range(9, -1, -1):
    wari = 1 << x                        # 2^x
    if (N // wari) % 2 == 0:
        out.append("4")
    else:
        out.append("7")

# --- 出力 -----------------------------------------------------------
print("".join(out))
