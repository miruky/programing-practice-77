# =====================================================================
# B08: Counting Points (A08 応用 ─ 2 次元累積和)
# ---------------------------------------------------------------------
# 【概要】2 次元平面上の N 個の点と、Q 個の矩形クエリ [a,b,c,d] (左下 (a,b)、
#         右上 (c,d)) が与えられる。各矩形内に含まれる点の個数を答える。
# 【アルゴリズム】格子 S[x][y] に「(x,y) にある点の個数」を入れ、横→縦の
#                 順に累積和を取って T を構築。包除原理で矩形和を O(1) 計算。
# 【計算量】前計算 O(座標範囲²) ≒ 1500² = 2.25×10⁶。クエリ O(1)。
# =====================================================================

# --- 入力 (前半: 点) -----------------------------------------------
N = int(input())
X = [None] * N
Y = [None] * N
for i in range(N):
    X[i], Y[i] = map(int, input().split())

# --- 入力 (後半: クエリ) -------------------------------------------
Q = int(input())
A = [None] * Q
B = [None] * Q
C = [None] * Q
D = [None] * Q
for i in range(Q):
    A[i], B[i], C[i], D[i] = map(int, input().split())

# --- 点の出現回数を格子に書き込む ----------------------------------
# 座標は 1..1500 を想定。0 行/0 列を番兵として用意する。
S = [[0] * 1501 for _ in range(1501)]
for i in range(N):
    S[X[i]][Y[i]] += 1

# --- 2 次元累積和の構築 (まず横方向、次に縦方向) ------------------
T = [[0] * 1501 for _ in range(1501)]
for i in range(1, 1501):
    for j in range(1, 1501):
        T[i][j] = T[i][j - 1] + S[i][j]      # 横方向の累積
for j in range(1, 1501):
    for i in range(1, 1501):
        T[i][j] = T[i - 1][j] + T[i][j]      # その結果を縦方向に累積

# --- クエリ処理 (包除で矩形和) -------------------------------------
# 矩形 [a,b]〜[c,d] の和 = T[c][d] − T[a-1][d] − T[c][b-1] + T[a-1][b-1]
for i in range(Q):
    print(T[C[i]][D[i]] + T[A[i] - 1][B[i] - 1] - T[A[i] - 1][D[i]] - T[C[i]][B[i] - 1])
