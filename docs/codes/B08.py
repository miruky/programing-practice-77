# # B08: Counting Points (A08 応用 ─ 2 次元累積和)
#
# ## 概要
#
# 2 次元平面上の N 個の点と、Q 個の矩形クエリ [a,b,c,d] (左下 (a,b)、
#         右上 (c,d)) が与えられる。各矩形内に含まれる点の個数を答える。
#
# ## アルゴリズム
#
# 格子 S[x][y] に「(x,y) にある点の個数」を入れ、
#                 T[x][y] = 左下ではなく「左上から (x,y) まで」の個数として作る。
#                 A08 と同じく、包除原理で矩形内の点数を O(1) 計算する。
#
# ## 計算量
#
# 前計算 O(座標範囲²) ≒ 1500² = 2.25×10⁶。クエリ O(1)。
#
# ## 読み方の地図
#
# - 問題の型: 累積和 / 2次元 / 難易度目安: ★3
# - 要約: 2 次元累積和で矩形領域に含まれる点の個数を O(1) で求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - Z[i][j] は左上から (i,j) までの合計。0 行目・0 列目は番兵の 0。
# - 作る式は「今 + 上 + 左 - 左上」。左上は上と左で 2 回足されるので引く。
# - 答える式は「右下 - 上 - 左 + 左上」。左上は 2 回引いたので戻す。

# --- 入力 (前半: 点) -----------------------------------------------
N = int(input())
points = [tuple(map(int, input().split())) for _ in range(N)]

# --- 入力 (後半: クエリ) -------------------------------------------
Q = int(input())
queries = [tuple(map(int, input().split())) for _ in range(Q)]

# --- 点の出現回数を格子に書き込む ----------------------------------
# 座標は 1..1500 を想定。0 行/0 列を番兵として用意する。
S = [[0] * 1501 for _ in range(1501)]
for x, y in points:
    S[x][y] += 1

# --- 2 次元累積和の構築 --------------------------------------------
# T[x][y] は「x 以下、y 以下の座標にある点の個数」。
# A08 と同じ 1 式で作ると、重複して足した左上を引く構造が見えやすい。
T = [[0] * 1501 for _ in range(1501)]
for x in range(1, 1501):
    for y in range(1, 1501):
        T[x][y] = S[x][y] + T[x - 1][y] + T[x][y - 1] - T[x - 1][y - 1]

# --- クエリ処理 (包除で矩形和) -------------------------------------
# 矩形 [a,b]〜[c,d] の和 = T[c][d] − T[a-1][d] − T[c][b-1] + T[a-1][b-1]
for a, b, c, d in queries:
    answer = T[c][d] - T[a - 1][d] - T[c][b - 1] + T[a - 1][b - 1]
    print(answer)
