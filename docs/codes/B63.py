# # B63: 幅優先探索 (A63 応用 ─ グリッド BFS)
#
# ## 概要
#
# H×W のグリッドが '.' (通路) と '#' (壁) で与えられる。
#         スタート (sx, sy) からゴール (gx, gy) までの最短歩数を求める。
#
# ## アルゴリズム
#
# BFS。各マスの最小コストを cost[][] に持ち、上下左右に
#                 +1 ずつ広げる。
#
# ## 計算量
#
# O(H · W)。
#
# ## 読み方の地図
#
# - 問題の型: グラフ / BFS / 迷路 / 難易度目安: ★3
# - 要約: 迷路を BFS で探索し、スタートからゴールまでの最短歩数を求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 入力をまず隣接リストやグリッドの隣接関係に変換している部分を見る。
# - visited や dist は「もう見たか」「何手で来たか」を記録するための配列。
# - DFS は深く進む、BFS は近い順に広がる。この違いだけ先に押さえる。

# --- 入力 -----------------------------------------------------------
H, W = map(int, input().split())
sx, sy = map(int, input().split()); sx -= 1; sy -= 1
gx, gy = map(int, input().split()); gx -= 1; gy -= 1
S = [input() for _ in range(H)]

INF = 1 << 61
cost = [[INF] * W for _ in range(H)]
q = []


# 「(x, y) を コスト c で訪問しようとする」共通処理。
# - 壁 '#' なら無視
# - すでにそれ以下のコストで到達済みなら無視
def push(x: int, y: int, c: int):
    if S[x][y] == '#':
        return
    if cost[x][y] <= c:
        return
    cost[x][y] = c
    q.append((x, y))


# --- BFS 本体 ------------------------------------------------------
push(sx, sy, 0)
# キュー (= リスト) を for で回しながら追加すると、Python では BFS 順に処理できる。
for x, y in q:
    c2 = cost[x][y] + 1
    push(x - 1, y, c2)
    push(x, y - 1, c2)
    push(x + 1, y, c2)
    push(x, y + 1, c2)

# --- 出力 -----------------------------------------------------------
print(cost[gx][gy])
