# # A65: 木上の DP (部下数を数える)
#
# ## 概要
#
# 社員 1 を根とする木が与えられ、各社員の部下の総数を求める。
#
# ## アルゴリズム
#
# 木 DP (葉から根に向かって集計)
#   dp[i] = 社員 i の (直接 + 間接) 部下の数
#   dp[i] = Σ (dp[子] + 1)
#   全社員を i=N から 1 へ逆順に走査するだけで葉から計算できる
#   (上司 < 部下 という入力の性質を利用)。
#
# ## 計算量
#
# O(N)
#
# ## 読み方の地図
#
# - 問題の型: グラフ / 木DP / 難易度目安: ★3
# - 要約: 木の葉から根へ向かって部下数を集計する木 DP。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。

# --- 入力 (A[2..N] が「i の上司」を表す) -----------------------------
N = int(input())
# A[0], A[1] はダミー (社員 1 には上司なし)
A = [0] * 2 + list(map(int, input().split()))

# --- 隣接リストの構築 (上司 → 部下) -------------------------------
G = [list() for _ in range(N + 1)]
for i in range(2, N + 1):
    G[A[i]].append(i)

# --- 木 DP (逆順で葉から計算) -------------------------------------
dp = [0] * (N + 1)
for i in range(N, 0, -1):
    for j in G[i]:
        dp[i] += (dp[j] + 1)  # +1 は子自身を含めるため

# --- 出力 (dp[1..N] を空白区切り) ---------------------------------
print(*dp[1:])
