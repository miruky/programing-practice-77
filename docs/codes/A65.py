# =====================================================================
# A65: 木上の DP (部下数を数える)
# ---------------------------------------------------------------------
# 【概要】社員 1 を根とする木が与えられ、各社員の部下の総数を求める。
# 【アルゴリズム】木 DP (葉から根に向かって集計)
#   dp[i] = 社員 i の (直接 + 間接) 部下の数
#   dp[i] = Σ (dp[子] + 1)
#   全社員を i=N から 1 へ逆順に走査するだけで葉から計算できる
#   (上司 < 部下 という入力の性質を利用)。
# 【計算量】O(N)
# =====================================================================

# --- 入力 (A[2..N] が「i の上司」を表す) -----------------------------
N = int(input())
# A[0], A[1] はダミー (社員 1 には上司なし)
A = [0] * 2 + list(map(int, input().split()))

# --- 隣接リストの構築 (上司 → 部下) -------------------------------
G = [list() for _ in range(N + 1)]
for i in range(2, N + 1):
    G[A[i]].append(i)

# --- 木 DP (逆順で葉から計算) -------------------------------------
dp = [0] * (N + 1)
for i in range(N, 0, -1):
    for j in G[i]:
        dp[i] += (dp[j] + 1)  # +1 は子自身を含めるため

# --- 出力 (dp[1..N] を空白区切り) ---------------------------------
print(*dp[1:])
