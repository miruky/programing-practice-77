# # A76: 川渡り (累積和を使った DP 高速化)
#
# ## 概要
#
# 足場 [0, X_1, ..., X_N, W] の上をジャンプ ([L, R] の範囲) で渡る。
#         西岸 → 東岸 へ到達する経路数を 10^9+7 で割った余りで求める。
#
# ## アルゴリズム
#
# DP + 累積和
#   dp[i] = 足場 i に到達する経路数
#   dp[i] = sum(dp[k])   ただし足場 k から i にジャンプできる範囲
#   素直に計算すると O(N^2) だが、累積和 dpsum を使うことで O(N log N) に。
#   ジャンプ可能な範囲の端は二分探索 (bisect) で求める。
#
# ## 計算量
#
# O(N log N)
#
# ## 読み方の地図
#
# - 問題の型: DP / 累積和 / 難易度目安: ★4
# - 要約: DP 遷移を累積和で O(1) 化することで全体を O(N log N) に。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - S[i] は先頭から i 個分の合計。S[0]=0 を置くと端の例外処理が消える。
# - 区間和は「右端までの合計 - 左手前までの合計」と読む。
# - 入力が 1-indexed、Python 配列が 0-indexed なので添字の変換だけ注意する。

import bisect

# --- 入力 -----------------------------------------------------------
N, W, L, R = map(int, input().split())
X = list(map(int, input().split()))

# 西岸を足場 0、東岸を足場 N+1 として X を拡張
X = [0] + X + [W]

# --- DP + 累積和 -------------------------------------------------
MOD = 10 ** 9 + 7
dp = [0] * (N + 2)
dpsum = [0] * (N + 2)
dp[0] = 1     # 西岸スタート
dpsum[0] = 1

for i in range(1, N + 2):
    # 足場 i にジャンプ可能な始点 k の範囲を求める
    # 条件: X[i] - R <= X[k] <= X[i] - L
    posl = bisect.bisect_left(X, X[i] - R)       # 範囲の最左
    posr = bisect.bisect_left(X, X[i] - L + 1) - 1  # 範囲の最右
    # dp[i] = sum(dp[posl..posr]) = dpsum[posr] - dpsum[posl-1]
    dp[i] = (dpsum[posr] if posr >= 0 else 0) - (dpsum[posl - 1] if posl >= 1 else 0)
    dp[i] %= MOD
    # 累積和を更新
    dpsum[i] = dpsum[i - 1] + dp[i]
    dpsum[i] %= MOD

# --- 出力 -----------------------------------------------------------
print(dp[N + 1])
