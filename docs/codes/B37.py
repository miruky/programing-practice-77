# =====================================================================
# B37: Sum of Digits (A37 応用 ─ 桁ごとの貢献度)
# ---------------------------------------------------------------------
# 【概要】1 以上 N 以下の整数全ての桁の和の合計を求める。
# 【アルゴリズム】各桁 i (下から数えて i 桁目) に数字 j が現れる回数 R[i][j]
#                 を求め、Σ j · R[i][j] を答えとする。これが「貢献度の独立計算」。
#                 N の i 桁目の値 Digit によって場合分けすることで R[i][j] を
#                 閉じた式で求められる。
# 【計算量】O(D · 10)、ここで D は N の桁数 (≦ 16)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())

# --- 10 の i 乗を前計算 ---------------------------------------------
Power10 = [None] * 17
for i in range(17):
    Power10[i] = 10 ** i

# --- R[i][j] = 「1..N の整数のうち、下から i+1 桁目が j のものの個数」-
R = [[None] * 10 for _ in range(17)]
for i in range(16):
    # N の下から i+1 桁目の数字
    Digit = (N // Power10[i]) % 10
    for j in range(10):
        if j < Digit:
            R[i][j] = (N // Power10[i + 1] + 1) * Power10[i]
        elif j == Digit:
            R[i][j] = (N // Power10[i + 1]) * Power10[i] + (N % Power10[i]) + 1
        else:  # j > Digit
            R[i][j] = (N // Power10[i + 1]) * Power10[i]

# --- 各桁・各数字 j を独立に足し上げる -----------------------------
Answer = 0
for i in range(16):
    for j in range(10):
        Answer += j * R[i][j]

# --- 出力 -----------------------------------------------------------
print(Answer)
