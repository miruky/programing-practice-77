# =====================================================================
# A77: 答えで二分探索 (棒の切り方)
# ---------------------------------------------------------------------
# 【概要】長さ L の棒に N 個の "印" がある。そのうち K 個を選んで
#         切ったとき、できる N+1 個の区間の最小長を最大化する。
# 【アルゴリズム】答えで二分探索
#   - 「最小区間長を x 以上にできるか?」という判定問題に帰着する。
#   - 印を左から見て、前回の切れ目から x 以上離れていれば切る (貪欲)。
#   - 切った数が K 以上なら OK。x について単調なので二分探索が効く。
# 【計算量】O(N log(10^9))
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 二分探索 / 貪欲 / 難易度目安: ★3
# - 要約: 切れ目の最小区間長を二分探索で最大化する典型問題。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 二分探索は値を探すというより、条件が NG から OK に切り替わる境目を探す。
# - bisect_left は「その値を入れてもソートが崩れない一番左の位置」を返す。
# - LIS や座標圧縮では、配列そのものではなく順位・置き場所を管理する。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N, L = map(int, input().split())
K = int(input())
A = list(map(int, input().split()))


def check(x):
    """最小区間長を x 以上にできるかを判定する関数"""
    cnt = 0           # ここまで何回切ったか
    last_kireme = 0   # 直前の切れ目の位置
    for i in range(N):
        # 左端からの距離 と 右端までの距離 がともに x 以上なら切る
        if A[i] - last_kireme >= x and L - A[i] >= x:
            cnt += 1
            last_kireme = A[i]
    return cnt >= K


# --- 二分探索 (左閉右閉) ----------------------------------------
# left: 達成可能な値 (確定済み), right: 不明領域の右端
left, right = 1, 10 ** 9
while left < right:
    mid = (left + right + 1) // 2  # 上に切り上げ (空白回避)
    answer = check(mid)
    if answer == False:
        right = mid - 1  # 答えは mid より小さい
    else:
        left = mid       # 答えは mid 以上

print(left)
