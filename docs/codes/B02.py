# B02: Divisor Check (A02 応用)
# ---------------------------------------------------------------------
# 【概要】A 以上 B 以下の整数の中に 100 の約数があれば "Yes"、
#         なければ "No" を出力する。
# 【アルゴリズム】A から B までを全て試し (線形探索)、100 % i == 0 を判定。
# 【計算量】O(B - A + 1)。B ≦ 100 程度なら一瞬で終わる。
# 【ポイント】100 の約数は {1,2,4,5,10,20,25,50,100} の 9 個しかない。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 全探索 / 約数 / 難易度目安: ★2
# - 要約: A 以上 B 以下の整数の中に 100 の約数があるかを線形探索で判定。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
A, B = map(int, input().split())

# --- 線形探索 -------------------------------------------------------
# Answer は「条件を満たす i が見つかったか」のフラグ。最初は False。
Answer = False
for i in range(A, B + 1):
    # 100 が i で割り切れる ⇔ i は 100 の約数。
    if 100 % i == 0:
        Answer = True

# --- 出力 -----------------------------------------------------------
if Answer:
    print("Yes")
else:
    print("No")
