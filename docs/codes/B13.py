# =====================================================================
# B13: Supermarket 2 (A13 応用 ─ しゃくとり法)
# ---------------------------------------------------------------------
# 【概要】配列 A について、連続部分列 [l, r] の和が K 以下となる組の個数を
#         数える。
# 【アルゴリズム】各左端 i について、和が K 以下に保てる最大の R[i] を
#                 求める ─ これはしゃくとり法で全体 O(N) で計算可能。
#                 i ごとに (R[i] − i + 1) を足し合わせれば答え。
# 【計算量】O(N)。累積和で区間和を O(1) 取得しているのが効く。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: しゃくとり法 / 累積和 / 難易度目安: ★4
# - 要約: 合計が K 以下となる連続部分列の個数をしゃくとり法で数える。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - S[i] は先頭から i 個分の合計。S[0]=0 を置くと端の例外処理が消える。
# - 区間和は「右端までの合計 - 左手前までの合計」と読む。
# - 入力が 1-indexed、Python 配列が 0-indexed なので添字の変換だけ注意する。
# ---------------------------------------------------------------------

# 累積和 S を使って A[l..r] の和を O(1) で取得する関数。
def sum_range(l, r, S):
    return S[r + 1] - S[l]

# --- 入力 -----------------------------------------------------------
N, K = map(int, input().split())
A = list(map(int, input().split()))

# --- 累積和の構築 ---------------------------------------------------
S = [0] * (N + 1)
for i in range(1, N + 1):
    S[i] = S[i - 1] + A[i - 1]

# --- しゃくとり法で各左端 i に対する右端 R[i] を求める -------------
# R[i] は「左端 i のときに K 以下で取れる最大の右端」
R = [None] * N
for i in range(N):
    if i == 0:
        R[i] = -1                                # まだ何も含まない状態
    else:
        R[i] = R[i - 1]                          # 左を進めた分は右も維持できる (単調性)
    # 右端を 1 つ伸ばしても K を超えない間、伸ばし続ける
    while R[i] < N - 1 and sum_range(i, R[i] + 1, S) <= K:
        R[i] += 1

# --- 各左端ごとの個数を合計 ----------------------------------------
Answer = 0
for i in range(N):
    Answer += (R[i] - i + 1)
print(Answer)
