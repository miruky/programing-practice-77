# # C18: Pick Two (力試し問題)
#
# ## 概要
#
# 2N 個の数 A[1..2N] を 2 個ずつ N 回取り除く。取り除く際の
#         コストは「2 つの差の絶対値」。隣接する 2 個しか取り除けない
#         (取り除くと両側の数字が新たな隣接になる)。総コストの最小値。
#
# ## アルゴリズム
#
# 区間 DP。dp[l][r] = 区間 [l..r] を全部取り除く最小コスト。
#         遷移: l, r を直接ペアにする / 区間 [l, m] と [m+1, r] に分割。
#
# ## 計算量
#
# O(N³)。
#
# ## 読み方の地図
#
# - 問題の型: DP / 区間DP / 難易度目安: ★5
# - 要約: 隣接 2 つを取り除くゲームの最小コスト ─ 区間 DP で計算する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。

# --- 入力 -----------------------------------------------------------
N = int(input())
A = [0] + list(map(int, input().split()))     # 1-indexed, 長さは 2N

L = 2 * N
INF = 10 ** 9
dp = [[INF] * (L + 2) for _ in range(L + 2)]

# --- 長さ 2 の区間を直接計算 ---------------------------------------
for i in range(1, L):
    dp[i][i + 1] = abs(A[i] - A[i + 1])

# --- 区間長を増やしながら遷移 --------------------------------------
for LEN in range(2, L):
    for l in range(1, L - LEN + 1):
        r = l + LEN

        # (a) l 番目と r 番目をまとめて消す ⇒ 内部 dp[l+1][r-1] が先に消えてる前提
        dp[l][r] = min(dp[l][r], dp[l + 1][r - 1] + abs(A[l] - A[r]))

        # (b) 区間 [l, m] と [m+1, r] に分割して別々に消す
        for m in range(l, r):
            dp[l][r] = min(dp[l][r], dp[l][m] + dp[m + 1][r])

# --- 出力 -----------------------------------------------------------
print(dp[1][2 * N])
