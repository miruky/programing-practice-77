# =====================================================================
# C18: Pick Two (力試し問題)
# ---------------------------------------------------------------------
# 【概要】2N 個の数 A[1..2N] を 2 個ずつ N 回取り除く。取り除く際の
#         コストは「2 つの差の絶対値」。隣接する 2 個しか取り除けない
#         (取り除くと両側の数字が新たな隣接になる)。総コストの最小値。
# 【アルゴリズム】区間 DP。dp[l][r] = 区間 [l..r] を全部取り除く最小コスト。
#         遷移: l, r を直接ペアにする / 区間 [l, m] と [m+1, r] に分割。
# 【計算量】O(N³)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())
A = [0] + list(map(int, input().split()))     # 1-indexed, 長さは 2N

L = 2 * N
INF = 10 ** 9
dp = [[INF] * (L + 2) for _ in range(L + 2)]

# --- 長さ 2 の区間を直接計算 ---------------------------------------
for i in range(1, L):
    dp[i][i + 1] = abs(A[i] - A[i + 1])

# --- 区間長を増やしながら遷移 --------------------------------------
for LEN in range(2, L):
    for l in range(1, L - LEN + 1):
        r = l + LEN

        # (a) l 番目と r 番目をまとめて消す ⇒ 内部 dp[l+1][r-1] が先に消えてる前提
        dp[l][r] = min(dp[l][r], dp[l + 1][r - 1] + abs(A[l] - A[r]))

        # (b) 区間 [l, m] と [m+1, r] に分割して別々に消す
        for m in range(l, r):
            dp[l][r] = min(dp[l][r], dp[l][m] + dp[m + 1][r])

# --- 出力 -----------------------------------------------------------
print(dp[1][2 * N])
