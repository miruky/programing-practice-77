# # A30: 二項係数 mod p
#
# ## 概要
#
# nCr を 10^9+7 で割った余りを求める。
#
# ## アルゴリズム
#
# フェルマーの小定理によるモジュラ逆元
#   - p が素数のとき a^(p-1) ≡ 1 (mod p) なので、 a^(p-2) が a の逆元。
#   - nCr = n! / (r! * (n-r)!) を、逆元を使った乗算で計算する。
#
# ## 計算量
#
# O(n + log p)
#
# ## 読み方の地図
#
# - 問題の型: 数学 / modular逆元 / 難易度目安: ★3
# - 要約: フェルマーの小定理で逆元を求め、nCr を計算する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。

# a^b mod m
def Power(a, b, m):
    p = a
    Answer = 1
    for i in range(30):
        wari = 2 ** i
        if (b // wari) % 2 == 1:
            Answer = (Answer * p) % m
        p = (p * p) % m
    return Answer


# a / b mod m  (b の逆元を a に掛ける)
def Division(a, b, m):
    return (a * Power(b, m - 2, m)) % m


# --- 入力 -----------------------------------------------------------
n, r = map(int, input().split())
M = 1000000007

# --- 分子 n! を mod M で計算 ----------------------------------------
a = 1
for i in range(1, n + 1):
    a = (a * i) % M

# --- 分母 r! * (n-r)! を mod M で計算 ------------------------------
b = 1
for i in range(1, r + 1):
    b = (b * i) % M
for i in range(1, n - r + 1):
    b = (b * i) % M

# --- 答え (a / b) mod M --------------------------------------------
print(Division(a, b, M))
