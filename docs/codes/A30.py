# =====================================================================
# A30: 二項係数 mod p
# ---------------------------------------------------------------------
# 【概要】nCr を 10^9+7 で割った余りを求める。
# 【アルゴリズム】フェルマーの小定理によるモジュラ逆元
#   - p が素数のとき a^(p-1) ≡ 1 (mod p) なので、 a^(p-2) が a の逆元。
#   - nCr = n! / (r! * (n-r)!) を、逆元を使った乗算で計算する。
# 【計算量】O(n + log p)
# =====================================================================

# a^b mod m
def Power(a, b, m):
    p = a
    Answer = 1
    for i in range(30):
        wari = 2 ** i
        if (b // wari) % 2 == 1:
            Answer = (Answer * p) % m
        p = (p * p) % m
    return Answer


# a / b mod m  (b の逆元を a に掛ける)
def Division(a, b, m):
    return (a * Power(b, m - 2, m)) % m


# --- 入力 -----------------------------------------------------------
n, r = map(int, input().split())
M = 1000000007

# --- 分子 n! を mod M で計算 ----------------------------------------
a = 1
for i in range(1, n + 1):
    a = (a * i) % M

# --- 分母 r! * (n-r)! を mod M で計算 ------------------------------
b = 1
for i in range(1, r + 1):
    b = (b * i) % M
for i in range(1, n - r + 1):
    b = (b * i) % M

# --- 答え (a / b) mod M --------------------------------------------
print(Division(a, b, M))
