# =====================================================================
# B22: Frog Jump 2 (A22 応用 ─ 前向き遷移 DP)
# ---------------------------------------------------------------------
# 【概要】N 個のマスを進む。i 番目から +1 進むコストが A[i]、+2 進む
#         コストが B[i]。マス 0 からマス N-1 までの最小コストを求める。
# 【アルゴリズム】1 次元 DP を「前向き遷移」(自分→次) で書く別解。
#         dp[i] が確定したら dp[i+1] と dp[i+2] を更新する。
# 【計算量】O(N)。
# 【ポイント】後ろ向き「dp[i] = min(dp[i-1]+A, dp[i-2]+B)」と等価だが、
#             前向きの方が「いま見えている地点から先に配るイメージ」で
#             直感的に書ける場面もある。
# =====================================================================

# --- 入力 (A, B は 0-indexed) --------------------------------------
N = int(input())
A = list(map(int, input().split()))
B = list(map(int, input().split()))

# --- DP 初期化 ------------------------------------------------------
INF = 2 * 10 ** 9
dp = [INF] * N
dp[0] = 0

# --- 前向き DP ------------------------------------------------------
for i in range(N):
    if i <= N - 2:                       # +1 マスへ進む
        dp[i + 1] = min(dp[i + 1], dp[i] + A[i])
    if i <= N - 3:                       # +2 マスへ進む
        dp[i + 2] = min(dp[i + 2], dp[i] + B[i])

# --- 出力 -----------------------------------------------------------
print(dp[N - 1])
