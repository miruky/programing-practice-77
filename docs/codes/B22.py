# # B22: Frog Jump 2 (A22 応用 ─ 前向き遷移 DP)
#
# ## 概要
#
# N 個のマスを進む。i 番目から +1 進むコストが A[i]、+2 進む
#         コストが B[i]。マス 0 からマス N-1 までの最小コストを求める。
#
# ## アルゴリズム
#
# 1 次元 DP を「前向き遷移」(自分→次) で書く別解。
#         dp[i] が確定したら dp[i+1] と dp[i+2] を更新する。
#
# ## 計算量
#
# O(N)。
#
# ## ポイント
#
# 後ろ向き「dp[i] = min(dp[i-1]+A, dp[i-2]+B)」と等価だが、
#             前向きの方が「いま見えている地点から先に配るイメージ」で
#             直感的に書ける場面もある。
#
# ## 読み方の地図
#
# - 問題の型: DP / 難易度目安: ★4
# - 要約: 前向き遷移で書いた、すごろく型 DP の練習問題。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。

# --- 入力 (A, B は 0-indexed) --------------------------------------
N = int(input())
A = list(map(int, input().split()))
B = list(map(int, input().split()))

# --- DP 初期化 ------------------------------------------------------
INF = 2 * 10 ** 9
dp = [INF] * N
dp[0] = 0

# --- 前向き DP ------------------------------------------------------
for i in range(N):
    if i <= N - 2:                       # +1 マスへ進む
        dp[i + 1] = min(dp[i + 1], dp[i] + A[i])
    if i <= N - 3:                       # +2 マスへ進む
        dp[i + 2] = min(dp[i + 2], dp[i] + B[i])

# --- 出力 -----------------------------------------------------------
print(dp[N - 1])
