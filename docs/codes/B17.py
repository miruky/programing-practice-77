# =====================================================================
# B17: Frog 1 with Restoration (A17 応用 ─ DP + 経路復元)
# ---------------------------------------------------------------------
# 【概要】B16 と同じ問題に加え、最小コストで使った足場の列を出力する。
# 【アルゴリズム】まず通常の DP で dp[] を求める。次にゴールから逆方向に、
#                 「dp[i] が dp[i-1]+|H[i-1]-H[i]| と一致するか」で
#                 どの遷移で来たかを判定して足場列を組み立てる。
# 【計算量】DP O(N) + 復元 O(N)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())
H = list(map(int, input().split()))

# --- DP (B16 と同じ) ------------------------------------------------
dp = [None] * N
dp[0] = 0
dp[1] = abs(H[0] - H[1])
for i in range(2, N):
    dp[i] = min(dp[i - 1] + abs(H[i - 1] - H[i]),
                dp[i - 2] + abs(H[i - 2] - H[i]))

# --- 経路復元 -------------------------------------------------------
# ゴール N-1 から始めて、どこから来たかを順次判定する。
Answer = []
Place = N - 1
while True:
    Answer.append(Place + 1)             # 1-indexed で記録
    if Place == 0:
        break
    # 1 個前のマスからの遷移か？ (一致すればそれを使う)
    if Place >= 1 and dp[Place - 1] + abs(H[Place - 1] - H[Place]) == dp[Place]:
        Place -= 1
    else:
        Place -= 2
Answer.reverse()                          # 0→N-1 の順に並べ替え

# --- 出力 -----------------------------------------------------------
print(len(Answer))
print(" ".join(map(str, Answer)))
