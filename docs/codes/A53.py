# =====================================================================
# A53: 優先度付きキュー (最小ヒープ)
# ---------------------------------------------------------------------
# 【概要】3 種類のクエリ:
#   "1 x": 値 x を入れる / "2": 最小値を表示 / "3": 最小値を取り除く
# 【データ構造】heapq モジュール (二分ヒープ)
#   Python の heapq は "最小ヒープ" のみ。最大値が欲しいときは符号反転。
# 【計算量】挿入・削除 O(log N), 最小値参照 O(1)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: データ構造 / ヒープ / 難易度目安: ★2
# - 要約: ヒープで最小値を取り出す優先度付きキュー。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

import heapq

# --- 入力 -----------------------------------------------------------
Q = int(input())
queries = [input().split() for _ in range(Q)]

# --- クエリ処理 -----------------------------------------------------
T = []
for q in queries:
    if q[0] == "1":
        heapq.heappush(T, int(q[1]))   # 挿入
    if q[0] == "2":
        print(T[0])                    # 最小値の参照 (= ヒープ先頭)
    if q[0] == "3":
        heapq.heappop(T)               # 最小値を取り除く

# 注意: T[1] 等が "2 番目に小さい要素" になるとは限らないので注意!
