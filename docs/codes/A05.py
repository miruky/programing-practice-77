# # A05: 三重ループ全探索
#
# ## 概要
#
# x + y + z = K となる (x, y, z) の組数を数える。
#         x, y, z は 1 以上 N 以下の整数。
#
# ## アルゴリズム
#
# 二重ループで (x, y) を全探索し、z は K-x-y で確定。
#
# ## 計算量
#
# O(N^2) ─ 内側で z を計算するだけなので 3 重ループ不要。
#
# ## ポイント
#
# "自由度を 1 減らす" のは競プロでの典型的なテクニック。
#
# ## 読み方の地図
#
# - 問題の型: 全探索 / 難易度目安: ★1
# - 要約: x+y+z=K となる組合せを三重ループで数える。計算量 O(N^3)。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。

N, K = map(int, input().split())
Answer = 0

# x, y を全探索。z は K - x - y で一意に決まる。
for x in range(1, N + 1):
    for y in range(1, N + 1):
        z = K - x - y
        # z が範囲 [1, N] に収まるときだけカウントする。
        if z >= 1 and z <= N:
            Answer += 1

print(Answer)
