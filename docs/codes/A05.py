# =====================================================================
# A05: 三重ループ全探索
# ---------------------------------------------------------------------
# 【概要】x + y + z = K となる (x, y, z) の組数を数える。
#         x, y, z は 1 以上 N 以下の整数。
# 【アルゴリズム】二重ループで (x, y) を全探索し、z は K-x-y で確定。
# 【計算量】O(N^2) ─ 内側で z を計算するだけなので 3 重ループ不要。
# 【ポイント】"自由度を 1 減らす" のは競プロでの典型的なテクニック。
# =====================================================================

N, K = map(int, input().split())
Answer = 0

# x, y を全探索。z は K - x - y で一意に決まる。
for x in range(1, N + 1):
    for y in range(1, N + 1):
        z = K - x - y
        # z が範囲 [1, N] に収まるときだけカウントする。
        if z >= 1 and z <= N:
            Answer += 1

print(Answer)
