# =====================================================================
# C17: Strange Data Structure (力試し問題)
# ---------------------------------------------------------------------
# 【概要】特殊なキュークエリ。
#         A x: 最後尾に x を追加
#         B x: 中央 (前半デックの末尾) に x を追加
#         C : 先頭を取り除く
#         D : 先頭を出力する
# 【アルゴリズム】2 つのデック Z1, Z2 を使う:
#         Z1 = 前半、Z2 = 後半。サイズ差が ±1 以内になるよう毎クエリ後に
#         リバランスする。先頭 = Z1.front()。
# 【計算量】各クエリ償却 O(1)。
# =====================================================================
from collections import deque
import sys
input = sys.stdin.readline                # 高速化
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: データ構造 / デック / 難易度目安: ★5
# - 要約: 前後 2 つのデックで「先頭/中央/末尾」操作を償却 O(1) で実装する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
Q = int(input())
queries = [input().split() for _ in range(Q)]

# --- 2 つのデック ---------------------------------------------------
Z1 = deque()                              # 前半 (先頭側)
Z2 = deque()                              # 後半

out = []
for q in queries:
    kind = q[0]
    if kind == 'A':
        Z2.append(q[1])
    elif kind == 'B':
        Z1.append(q[1])
    elif kind == 'C':
        Z1.popleft()
    elif kind == 'D':
        out.append(Z1[0])

    # --- リバランス: Z1 が Z2 より多すぎる場合 ----------------------
    while len(Z1) - len(Z2) >= 2:
        Z2.appendleft(Z1.pop())
    # --- リバランス: Z2 が Z1 より多すぎる場合 ----------------------
    while len(Z1) - len(Z2) <= -1:
        Z1.append(Z2.popleft())

print("\n".join(out))
