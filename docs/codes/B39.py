# =====================================================================
# B39: Taro's Job (A39 応用 ─ 締切付きジョブの貪欲)
# ---------------------------------------------------------------------
# 【概要】N 件のジョブ i は「日 X[i] 以降ならいつでも実行できる」「報酬 Y[i]」。
#         1 日に 1 件のジョブしかできない。D 日間で得られる報酬の最大値。
# 【アルゴリズム】貪欲。各日 i について「まだ未消化で、開始日 X[j] ≦ i の
#                 ジョブ」のうち報酬が最大のものを選ぶ。
# 【計算量】O(N · D)。小規模ならこれで十分 (本格的にはヒープで O((N+D) log N))。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, D = map(int, input().split())
X = [None] * N
Y = [None] * N
for i in range(N):
    X[i], Y[i] = map(int, input().split())

# --- どのジョブを使ったかを記録するフラグ --------------------------
used = [False] * N

# --- 1 日ずつ貪欲に最大報酬を選ぶ ----------------------------------
Answer = 0
for i in range(1, D + 1):
    maxValue = 0
    maxID = -1
    for j in range(N):
        if not used[j] and maxValue < Y[j] and X[j] <= i:
            maxValue = Y[j]
            maxID = j
    # 選べるジョブがあれば消化する
    if maxID != -1:
        Answer += maxValue
        used[maxID] = True

# --- 出力 -----------------------------------------------------------
print(Answer)
