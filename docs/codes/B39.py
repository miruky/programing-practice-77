# # B39: Taro's Job (A39 応用 ─ 締切付きジョブの貪欲)
#
# ## 概要
#
# N 件のジョブ i は「日 X[i] 以降ならいつでも実行できる」「報酬 Y[i]」。
#         1 日に 1 件のジョブしかできない。D 日間で得られる報酬の最大値。
#
# ## アルゴリズム
#
# 貪欲。各日 i について「まだ未消化で、開始日 X[j] ≦ i の
#                 ジョブ」のうち報酬が最大のものを選ぶ。
#
# ## 計算量
#
# O(N · D)。小規模ならこれで十分 (本格的にはヒープで O((N+D) log N))。
#
# ## 読み方の地図
#
# - 問題の型: 貪欲 / 難易度目安: ★3
# - 要約: 締切付きジョブから、毎日「現在解禁中で最大給料のジョブ」を貪欲に選ぶ。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 貪欲法は、何を基準に並べるか・どの候補を先に取るかが本体。
# - コードより先に「その選択をしても後で損しない理由」をコメントで確認する。
# - ソート後は、左から順に決めて戻らない構造になっているかを見る。

# --- 入力 -----------------------------------------------------------
N, D = map(int, input().split())
X = [None] * N
Y = [None] * N
for i in range(N):
    X[i], Y[i] = map(int, input().split())

# --- どのジョブを使ったかを記録するフラグ --------------------------
used = [False] * N

# --- 1 日ずつ貪欲に最大報酬を選ぶ ----------------------------------
Answer = 0
for i in range(1, D + 1):
    maxValue = 0
    maxID = -1
    for j in range(N):
        if not used[j] and maxValue < Y[j] and X[j] <= i:
            maxValue = Y[j]
            maxID = j
    # 選べるジョブがあれば消化する
    if maxID != -1:
        Answer += maxValue
        used[maxID] = True

# --- 出力 -----------------------------------------------------------
print(Answer)
