# # A43: 東西移動の上限
#
# ## 概要
#
# 長さ L の道路上に N 人がいて、それぞれ東 (E) か西 (W) へ歩く。
#         最後に出会う 2 人の組がいる最大時間を求める。
#
# ## アルゴリズム
#
# 考察
#   - 西向きの最大値 max(A[i])  と
#   - 東向きの「L - A[i]」の最大値
#   のうち、大きい方が答え。
#
# ## 計算量
#
# O(N)
#
# ## 読み方の地図
#
# - 問題の型: 考察 / 難易度目安: ★2
# - 要約: 各物体の最短距離の上限を考え、最大を答える。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 入力をまず隣接リストやグリッドの隣接関係に変換している部分を見る。
# - visited や dist は「もう見たか」「何手で来たか」を記録するための配列。
# - DFS は深く進む、BFS は近い順に広がる。この違いだけ先に押さえる。

# --- 入力 -----------------------------------------------------------
N, L = map(int, input().split())
A = [None] * N
B = [None] * N
for i in range(N):
    A[i], B[i] = input().split()
    A[i] = int(A[i])  # 位置

# --- 各方向ごとに最大時間を更新 -----------------------------------
Answer = 0
for i in range(N):
    if B[i] == 'E':
        Answer = max(Answer, L - A[i])  # 東向き: 端 L までの距離
    if B[i] == 'W':
        Answer = max(Answer, A[i])      # 西向き: 端 0 までの距離

# --- 出力 -----------------------------------------------------------
print(Answer)
