# =====================================================================
# A43: 東西移動の上限
# ---------------------------------------------------------------------
# 【概要】長さ L の道路上に N 人がいて、それぞれ東 (E) か西 (W) へ歩く。
#         最後に出会う 2 人の組がいる最大時間を求める。
# 【アルゴリズム】考察
#   - 西向きの最大値 max(A[i])  と
#   - 東向きの「L - A[i]」の最大値
#   のうち、大きい方が答え。
# 【計算量】O(N)
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, L = map(int, input().split())
A = [None] * N
B = [None] * N
for i in range(N):
    A[i], B[i] = input().split()
    A[i] = int(A[i])  # 位置

# --- 各方向ごとに最大時間を更新 -----------------------------------
Answer = 0
for i in range(N):
    if B[i] == 'E':
        Answer = max(Answer, L - A[i])  # 東向き: 端 L までの距離
    if B[i] == 'W':
        Answer = max(Answer, A[i])      # 西向き: 端 0 までの距離

# --- 出力 -----------------------------------------------------------
print(Answer)
