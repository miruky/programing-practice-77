# =====================================================================
# A70 (補足版): XOR を使った高速版
# ---------------------------------------------------------------------
# 【概要】A70 の状態 BFS の高速版。
# 【高速化の鍵】「ビット k を反転する」 = 「2^k を XOR する」
#   つまり get_next 関数を単純な XOR 1 つに置き換えられる:
#     nex = pos ^ (1 << x) ^ (1 << y) ^ (1 << z)
#   さらにグラフを実体化せず、BFS 内で直接遷移を計算する。
# 【計算量】A70 と同じだが定数倍が約 1/5 と高速。
# =====================================================================

from collections import deque

# --- 入力 -----------------------------------------------------------
N, M = map(int, input().split())
A = list(map(int, input().split()))
actions = [list(map(lambda x: int(x) - 1, input().split())) for _ in range(M)]

# --- スタート・ゴール ----------------------------------------------
start = sum(A[i] * (2 ** i) for i in range(N))
goal = 2 ** N - 1

# --- BFS の初期化 -------------------------------------------------
dist = [-1] * (2 ** N)
dist[start] = 0
Q = deque()
Q.append(start)

# --- BFS (グラフを持たず、その場で遷移を計算) ---------------------
while len(Q) >= 1:
    pos = Q.popleft()
    for x, y, z in actions:
        # 3 ビット同時反転 = 3 つの 2^k を XOR
        nex = pos ^ (1 << x) ^ (1 << y) ^ (1 << z)
        if dist[nex] == -1:
            dist[nex] = dist[pos] + 1
            Q.append(nex)

print(dist[goal])
