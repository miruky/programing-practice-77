# # B51: Bracket (A51 応用 ─ スタックでの括弧対応)
#
# ## 概要
#
# '(' と ')' のみからなる文字列 S が与えられる。括弧の対応を
#         「(の位置 )の位置」の形で全て出力する。
#
# ## アルゴリズム
#
# 左から走査し、'(' ならスタックに位置を積む。')' を見たら
#                 スタックから 1 つ取り出して対応ペアとして出力する。
#
# ## 計算量
#
# O(|S|)。
#
# ## 読み方の地図
#
# - 問題の型: データ構造 / スタック / 難易度目安: ★2
# - 要約: 括弧列を 1 文字ずつ走査し、スタックで対応する '(' の位置を取り出す。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。

# --- 入力 -----------------------------------------------------------
S = input()

# --- スタックで括弧対応を取る (位置は 1-indexed で出力) -----------
Stack = []
for i in range(len(S)):
    if S[i] == '(':
        Stack.append(i + 1)               # 開き括弧の位置を記録
    if S[i] == ')':
        print(Stack.pop(), i + 1)         # 直近の '(' とペアにする
