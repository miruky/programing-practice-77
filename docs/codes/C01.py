# =====================================================================
# C01: Tax Rate (力試し問題)
# ---------------------------------------------------------------------
# 【概要】消費税率 10% のとき、税抜価格 N 円の税込価格 (1.1N 円) を出力する。
#         制約より N は 100 の倍数なので、1.1N は必ず整数になる。
# 【アルゴリズム】N * 11 // 10 を計算するだけ。
# 【計算量】O(1)。
# 【ポイント】Python は浮動小数で計算すると精度の問題が出るので、
#             N * 11 // 10 のように整数演算で書くのが安全。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 演習 / 数学 / 難易度目安: ★1
# - 要約: 税抜価格 N 円の税込価格 1.1N を整数で出力。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N = int(input())

# --- 出力 (1.1N を整数で計算) ---------------------------------------
print(N * 11 // 10)
