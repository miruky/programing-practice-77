# =====================================================================
# B28: Fibonacci Easy (mod 1000000007) (A28 応用 ─ DP + mod)
# ---------------------------------------------------------------------
# 【概要】フィボナッチ数列 a_1=a_2=1, a_n = a_{n-1} + a_{n-2} について
#         a_N を 10⁹+7 で割った余りを出力する。
# 【アルゴリズム】単純な DP。各遷移で mod を取ることでオーバーフロー
#                 (Python では精度ではなく速度のため) を防ぐ。
# 【計算量】O(N)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())

# --- DP -------------------------------------------------------------
# 1-indexed で持つので長さは N+1。
a = [None] * (N + 1)
a[1] = 1
a[2] = 1
for i in range(3, N + 1):
    a[i] = (a[i - 1] + a[i - 2]) % 1000000007

# --- 出力 -----------------------------------------------------------
print(a[N])
