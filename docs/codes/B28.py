# # B28: Fibonacci Easy (mod 1000000007) (A28 応用 ─ DP + mod)
#
# ## 概要
#
# フィボナッチ数列 a_1=a_2=1, a_n = a_{n-1} + a_{n-2} について
#         a_N を 10⁹+7 で割った余りを出力する。
#
# ## アルゴリズム
#
# 単純な DP。各遷移で mod を取ることでオーバーフロー
#                 (Python では精度ではなく速度のため) を防ぐ。
#
# ## 計算量
#
# O(N)。
#
# ## 読み方の地図
#
# - 問題の型: 数学 / mod / DP / 難易度目安: ★3
# - 要約: N 番目のフィボナッチ数を mod 10^9+7 で求める基本 DP。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。

# --- 入力 -----------------------------------------------------------
N = int(input())

# --- DP -------------------------------------------------------------
# 1-indexed で持つので長さは N+1。
a = [None] * (N + 1)
a[1] = 1
a[2] = 1
for i in range(3, N + 1):
    a[i] = (a[i - 1] + a[i - 2]) % 1000000007

# --- 出力 -----------------------------------------------------------
print(a[N])
