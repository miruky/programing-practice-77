# # B20: Edit Distance (A20 応用 ─ 編集距離)
#
# ## 概要
#
# 2 つの文字列 S, T の編集距離 (= 1 文字の挿入/削除/置換のみで
#         S を T に変換する最小回数) を求める。
#
# ## アルゴリズム
#
# 2 次元 DP。
#         dp[i][j] = 「S の先頭 i 文字を T の先頭 j 文字にする最小操作数」
#         遷移は: 挿入 dp[i][j-1]+1, 削除 dp[i-1][j]+1,
#                  置換 dp[i-1][j-1]+0 or 1 (同字なら +0)。
#
# ## 計算量
#
# O(|S| · |T|)。
#
# ## 読み方の地図
#
# - 問題の型: DP / 編集距離 / 難易度目安: ★4
# - 要約: 編集距離（レーベンシュタイン距離）を 2 次元 DP で求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。

# --- 入力 -----------------------------------------------------------
S = input()
T = input()

# --- DP テーブル ---------------------------------------------------
dp = [[None] * (len(T) + 1) for _ in range(len(S) + 1)]

# 空文字列同士の編集距離は 0。
dp[0][0] = 0

# --- DP 本体 --------------------------------------------------------
# 境界 (i=0 or j=0) も含めて 1 つのループで書いている。
for i in range(len(S) + 1):
    for j in range(len(T) + 1):
        if i >= 1 and j >= 1 and S[i - 1] == T[j - 1]:
            # 同じ文字 ─ 置換不要で対角線方向の値をそのまま受け継げる
            dp[i][j] = min(dp[i - 1][j] + 1,
                           dp[i][j - 1] + 1,
                           dp[i - 1][j - 1])
        elif i >= 1 and j >= 1:
            # 異なる文字 ─ 置換コスト 1 で対角線方向
            dp[i][j] = min(dp[i - 1][j] + 1,
                           dp[i][j - 1] + 1,
                           dp[i - 1][j - 1] + 1)
        elif i >= 1:
            dp[i][j] = dp[i - 1][j] + 1   # 上端 (T が空) ─ S を全削除
        elif j >= 1:
            dp[i][j] = dp[i][j - 1] + 1   # 左端 (S が空) ─ T を全挿入

# --- 出力 -----------------------------------------------------------
print(dp[len(S)][len(T)])
