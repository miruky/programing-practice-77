# # A28: 余りの計算
#
# ## 概要
#
# 黒板の数字に対して +A[i] / -A[i] / *A[i] の操作を順に行い、
#         各操作後に 10000 で割った余りを出力する。
#
# ## アルゴリズム
#
# 各演算後すぐに % 10000 を取る。
#   この方式により値が爆発せず、安全に計算できる。
#
# ## ポイント
#
# Python は多倍長整数なのでオーバーフローはないが、
#             巨大な数の演算は遅くなるため毎回 mod を取るのは大事。
#
# ## 計算量
#
# O(N)
#
# ## 読み方の地図
#
# - 問題の型: 数学 / mod / 難易度目安: ★2
# - 要約: 各操作のたびに mod を取り、オーバーフロー（あるいは桁爆発）を防ぐ。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。

# --- 入力 -----------------------------------------------------------
N = int(input())
T = [None] * N
A = [None] * N
for i in range(N):
    T[i], A[i] = input().split()
    A[i] = int(A[i])  # A は数値、T は演算子の文字

# --- 演算ループ ----------------------------------------------------
Answer = 0
for i in range(N):
    if T[i] == '+':
        Answer += A[i]
    if T[i] == '-':
        Answer -= A[i]
    if T[i] == '*':
        Answer *= A[i]

    # 引き算で負になったら 10000 を足して非負に戻す。
    if Answer < 0:
        Answer += 10000

    # 毎回余りを取る (これにより大きくなりすぎない)。
    Answer %= 10000
    print(Answer)
