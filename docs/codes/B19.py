# =====================================================================
# B19: Knapsack 2 (A19 応用 ─ 価値添字のナップサック DP)
# ---------------------------------------------------------------------
# 【概要】N 個の品物 (重み w[i], 価値 v[i]) と容量 W のナップサックで、
#         価値の総和を最大化する。W は最大 10⁹ なので、通常の重み添字 DP
#         (O(NW)) では間に合わない。
# 【アルゴリズム】価値の合計を添字に取り、最小重みを管理する DP に切り替える。
#                 dp[i][j] = 「i 個目まで見て合計価値 j を達成するときの最小重さ」
#                 → 最後に dp[N][j] ≦ W となる最大 j を答えとして出力。
# 【計算量】O(N · sum(v))。v_i ≦ 1000 なら 10⁵ 程度に抑えられる。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, W = map(int, input().split())
w = [None] * (N + 1)
v = [None] * (N + 1)
for i in range(1, N + 1):
    w[i], v[i] = map(int, input().split())

# --- DP テーブル (添字: 価値, 値: 重みの最小) ----------------------
INF = 10 ** 15
dp = [[INF] * 100001 for _ in range(N + 1)]
dp[0][0] = 0
for i in range(1, N + 1):
    for j in range(100001):
        if j < v[i]:
            dp[i][j] = dp[i - 1][j]
        else:
            # 取らない / 取って重み w[i] を加算 の min を取る。
            dp[i][j] = min(dp[i - 1][j], dp[i - 1][j - v[i]] + w[i])

# --- W を超えない範囲で達成できる最大の価値 j を探す ---------------
Answer = 0
for j in range(100001):
    if dp[N][j] <= W:
        Answer = j
print(Answer)
