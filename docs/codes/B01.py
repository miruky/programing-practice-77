# =====================================================================
# B01: A+B Problem (A01 応用)
# ---------------------------------------------------------------------
# 【概要】2 つの整数 A, B を読み込み、A + B を出力する。
# 【目的】input().split() + map(int, ...) という Python での「複数整数を
#         1 行で受け取る」イディオムを身につける (A01 では 1 数だけだった)。
# 【計算量】O(1)。
# =====================================================================

# input().split() は 1 行の文字列を空白で分割し、リストとして返す。
# それを map(int, ...) で各要素を int に変換し、A, B にそれぞれ代入する。
A, B = map(int, input().split())

# 計算結果をそのまま出力するだけ。print は末尾改行を自動で付ける。
print(A + B)
