# # B01: A+B Problem (A01 応用)
#
# ## 概要
#
# 2 つの整数 A, B を読み込み、A + B を出力する。
#
# ## 目的
#
# input().split() + map(int, ...) という Python での「複数整数を
#         1 行で受け取る」イディオムを身につける (A01 では 1 数だけだった)。
#
# ## 計算量
#
# O(1)。
#
# ## 読み方の地図
#
# - 問題の型: 入出力 / 難易度目安: ★2
# - 要約: 整数 A, B を読み込み A+B を出力する。標準入出力の更なる練習。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。

# input().split() は 1 行の文字列を空白で分割し、リストとして返す。
# それを map(int, ...) で各要素を int に変換し、A, B にそれぞれ代入する。
A, B = map(int, input().split())

# 計算結果をそのまま出力するだけ。print は末尾改行を自動で付ける。
print(A + B)
