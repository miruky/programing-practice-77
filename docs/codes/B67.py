# =====================================================================
# B67: Max MST (A67 応用 ─ 最大全域木)
# ---------------------------------------------------------------------
# 【概要】N 頂点 M 辺のグラフに対し、辺の総和が最大となる全域木 (MST) の
#         重みを求める。
# 【アルゴリズム】クラスカル法は最小全域木のアルゴリズムだが、辺を「長さの
#                 降順」にソートすれば最大全域木を求められる ─ 全く同じ
#                 ロジックで OK。
# 【計算量】O(M log M + M α(N))。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: グラフ / MST / クラスカル / 難易度目安: ★5
# - 要約: 辺重みの降順でソートしクラスカル法を適用すれば最大全域木が得られる。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力をまず隣接リストやグリッドの隣接関係に変換している部分を見る。
# - visited や dist は「もう見たか」「何手で来たか」を記録するための配列。
# - DFS は深く進む、BFS は近い順に広がる。この違いだけ先に押さえる。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N, M = map(int, input().split())
edge = []
for _ in range(M):
    A, B, C = map(int, input().split())
    A -= 1
    B -= 1
    edge.append((A, B, C))

# --- 辺を長さの降順でソート (MST → 最大化版) ----------------------
edge.sort(key=lambda e: -e[2])

# --- Union-Find -----------------------------------------------------
uf = [-1] * N


def root(i: int) -> int:
    while True:
        if uf[i] < 0:
            return i
        if uf[uf[i]] < 0:
            return uf[i]
        uf[i] = uf[uf[i]]
        i = uf[i]


def unite(i: int, j: int) -> bool:
    i = root(i)
    j = root(j)
    if i == j:
        return False                      # 既に同じ連結成分 → 追加しない
    if uf[i] > uf[j]:
        i, j = j, i
    uf[i] += uf[j]
    uf[j] = i
    return True


# --- クラスカル法本体 ----------------------------------------------
ans = 0
for A, B, C in edge:
    if unite(A, B):
        ans += C                          # サイクルを作らない辺だけ採用

# --- 出力 -----------------------------------------------------------
print(ans)
