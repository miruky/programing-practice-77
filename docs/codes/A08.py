# =====================================================================
# A08: 2次元累積和
# ---------------------------------------------------------------------
# 【概要】H×W のグリッドに対し、矩形領域の和を高速に答える。
# 【アルゴリズム】2 次元累積和 Z[i][j] = 左上から (i, j) までの和。
#   矩形 (A,B)-(C,D) の和 = Z[C][D] - Z[A-1][D] - Z[C][B-1] + Z[A-1][B-1]
#   (引きすぎた部分を足し戻す "包除" の図形版)
# 【計算量】前計算 O(HW) + クエリ O(Q)。
# 【ポイント】Z は (H+1)×(W+1) で確保、0 行・0 列を番兵にする。
# =====================================================================

# --- 入力 (前半) ----------------------------------------------------
H, W = map(int, input().split())
X = [None] * (H)  # 元のグリッド (0-indexed)
Z = [[0] * (W + 1) for _ in range(H + 1)]  # 累積和 (1-indexed)
for i in range(H):
    X[i] = list(map(int, input().split()))

# --- 入力 (後半: クエリ) -------------------------------------------
Q = int(input())
A = [None] * Q
B = [None] * Q
C = [None] * Q
D = [None] * Q
for i in range(Q):
    A[i], B[i], C[i], D[i] = map(int, input().split())

# --- 2 次元累積和の構築 --------------------------------------------
# まず横方向に累積和を取り、その後縦方向に累積和を取る (2 段階で OK)。
# X[i-1][j-1] と添字をずらしているのは、Z が 1-indexed で X が 0-indexed のため。
for i in range(1, H + 1):
    for j in range(1, W + 1):
        Z[i][j] = Z[i][j - 1] + X[i - 1][j - 1]

# 縦方向の累積和を上書き的に取る。
for j in range(1, W + 1):
    for i in range(1, H + 1):
        Z[i][j] = Z[i - 1][j] + Z[i][j]

# --- クエリ処理 -----------------------------------------------------
# 矩形 (A[i], B[i])-(C[i], D[i]) の和を 4 つの値で計算 (包除)。
for i in range(Q):
    print(Z[C[i]][D[i]] + Z[A[i] - 1][B[i] - 1] - Z[A[i] - 1][D[i]] - Z[C[i]][B[i] - 1])
