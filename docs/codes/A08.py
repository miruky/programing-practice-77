# =====================================================================
# A08: 2次元累積和
# ---------------------------------------------------------------------
# 【概要】H×W のグリッドに対し、矩形領域の和を高速に答える。
# 【アルゴリズム】2 次元累積和 Z[i][j] = 左上から (i, j) までの和。
#   作る式:
#     Z[i][j] = X[i-1][j-1] + Z[i-1][j] + Z[i][j-1] - Z[i-1][j-1]
#   答える式:
#     矩形 (A,B)-(C,D) の和 = Z[C][D] - Z[A-1][D] - Z[C][B-1] + Z[A-1][B-1]
#   どちらも「重なった左上を 1 回引く/戻す」と読む。
# 【計算量】前計算 O(HW) + クエリ O(Q)。
# 【ポイント】Z は (H+1)×(W+1) で確保、0 行・0 列を番兵にする。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 累積和 / 2次元 / 難易度目安: ★2
# - 要約: 矩形領域の和を O(1) で求めるために 2 次元累積和を構築。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - Z[i][j] は左上から (i,j) までの合計。0 行目・0 列目は番兵の 0。
# - 作る式は「今 + 上 + 左 - 左上」。左上は上と左で 2 回足されるので引く。
# - 答える式は「右下 - 上 - 左 + 左上」。左上は 2 回引いたので戻す。
# ---------------------------------------------------------------------

# --- 入力 (前半) ----------------------------------------------------
H, W = map(int, input().split())
X = [list(map(int, input().split())) for _ in range(H)]  # 元のグリッド (0-indexed)

# --- 入力 (後半: クエリ) -------------------------------------------
Q = int(input())
queries = [tuple(map(int, input().split())) for _ in range(Q)]

# --- 2 次元累積和の構築 --------------------------------------------
# Z[i][j] は「X の 0 行目〜i-1 行目、0 列目〜j-1 列目の合計」。
# 0 行目・0 列目を 0 のまま残すので、上端/左端でも場合分けが不要になる。
Z = [[0] * (W + 1) for _ in range(H + 1)]  # 累積和 (1-indexed)
for i in range(1, H + 1):
    for j in range(1, W + 1):
        # 今のマス + 上側の合計 + 左側の合計 - 左上の重複ぶん
        Z[i][j] = X[i - 1][j - 1] + Z[i - 1][j] + Z[i][j - 1] - Z[i - 1][j - 1]

# --- クエリ処理 -----------------------------------------------------
# 矩形 (A, B)-(C, D) の和を 4 つの累積和で計算する。
# 大きく Z[C][D] を取り、上と左の余計な部分を引き、左上の引きすぎを戻す。
for A, B, C, D in queries:
    answer = Z[C][D] - Z[A - 1][D] - Z[C][B - 1] + Z[A - 1][B - 1]
    print(answer)
