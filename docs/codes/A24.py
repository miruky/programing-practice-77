# =====================================================================
# A24: 最長増加部分列 (LIS) ─ O(N log N) 版
# ---------------------------------------------------------------------
# 【概要】数列 A の最長 (狭義) 増加部分列の長さを求める。
# 【アルゴリズム】長さを管理する配列 L を使った O(N log N) 法
#   各 i について、L 内で A[i] を入れる位置 (bisect_left) を求める。
#   - その位置が末尾 (LEN 以降) なら L を伸ばす。
#   - そうでなければその位置の値を A[i] に置き換える (より小さい値で更新)。
#   配列 L の長さ LEN がそのまま LIS の長さになる。
# 【計算量】O(N log N)
# 【補足】dp[i] は「i 番目で終わる LIS の長さ」を別途記録している。
# =====================================================================

import bisect

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- DP の準備 -----------------------------------------------------
LEN = 0           # 現在の LIS 候補の長さ
L = []            # L[k] = "長さ k+1 の LIS の末尾の最小値"
dp = [None] * N   # dp[i] = i 番目までで作れる LIS の長さ (-1)

# --- メインループ --------------------------------------------------
for i in range(N):
    pos = bisect.bisect_left(L, A[i])
    dp[i] = pos

    # L を更新: 末尾より大きければ追加、そうでなければ置換。
    if dp[i] >= LEN:
        L.append(A[i])
        LEN += 1
    else:
        L[dp[i]] = A[i]

# --- 答えを出力 -----------------------------------------------------
print(LEN)
