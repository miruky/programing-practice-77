# # A24: 最長増加部分列 (LIS) ─ O(N log N) 版
#
# ## 概要
#
# 数列 A の最長 (狭義) 増加部分列の長さを求める。
#
# ## アルゴリズム
#
# 長さを管理する配列 L を使った O(N log N) 法
#   各 i について、L 内で A[i] を入れる位置 (bisect_left) を求める。
#   - その位置が末尾 (LEN 以降) なら L を伸ばす。
#   - そうでなければその位置の値を A[i] に置き換える (より小さい値で更新)。
#   配列 L の長さ LEN がそのまま LIS の長さになる。
#
# ## 計算量
#
# O(N log N)
#
# ## 補足
#
# dp[i] は「i 番目で終わる LIS の長さ」を別途記録している。
#
# ## 読み方の地図
#
# - 問題の型: DP / LIS / 二分探索 / 難易度目安: ★4
# - 要約: LIS を二分探索を使って O(N log N) で求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 二分探索は値を探すというより、条件が NG から OK に切り替わる境目を探す。
# - bisect_left は「その値を入れてもソートが崩れない一番左の位置」を返す。
# - LIS や座標圧縮では、配列そのものではなく順位・置き場所を管理する。

import bisect

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- DP の準備 -----------------------------------------------------
LEN = 0           # 現在の LIS 候補の長さ
L = []            # L[k] = "長さ k+1 の LIS の末尾としてありえる最小値"
dp = [None] * N   # dp[i] = A[i] が L のどの位置に入ったか (復元しないなら省略可能)

# --- メインループ --------------------------------------------------
for i in range(N):
    pos = bisect.bisect_left(L, A[i])
    dp[i] = pos

    # L を更新: 末尾より大きければ追加、そうでなければ置換。
    if dp[i] >= LEN:
        L.append(A[i])
        LEN += 1
    else:
        L[dp[i]] = A[i]

# --- 答えを出力 -----------------------------------------------------
print(LEN)
