# =====================================================================
# A42: 2 軸を全探索
# ---------------------------------------------------------------------
# 【概要】点 (A[i], B[i]) があり、矩形 [a, a+K] x [b, b+K] に
#         含まれる点数の最大値を求める。
# 【アルゴリズム】(a, b) を 100x100 通り全探索し、各組で点数を数える。
# 【計算量】O(101^2 * N)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 全探索 / 難易度目安: ★2
# - 要約: (a,b) を全探索し、各組に対して条件を満たす要素を数える。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

def GetScore(a, b, A, B, K):
    """矩形 [a, a+K] x [b, b+K] に含まれる点数"""
    cnt = 0
    for i in range(N):
        if a <= A[i] and A[i] <= a + K and b <= B[i] and B[i] <= b + K:
            cnt += 1
    return cnt


# --- 入力 -----------------------------------------------------------
N, K = map(int, input().split())
A = [None] * N
B = [None] * N
for i in range(N):
    A[i], B[i] = map(int, input().split())

# --- (a, b) を全探索して最大点数を求める ----------------------------
Answer = 0
for a in range(1, 101):
    for b in range(1, 101):
        Score = GetScore(a, b, A, B, K)
        Answer = max(Answer, Score)

# --- 出力 -----------------------------------------------------------
print(Answer)
