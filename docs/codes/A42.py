# =====================================================================
# A42: 2 軸を全探索
# ---------------------------------------------------------------------
# 【概要】点 (A[i], B[i]) があり、矩形 [a, a+K] x [b, b+K] に
#         含まれる点数の最大値を求める。
# 【アルゴリズム】(a, b) を 100x100 通り全探索し、各組で点数を数える。
# 【計算量】O(101^2 * N)
# =====================================================================

def GetScore(a, b, A, B, K):
    """矩形 [a, a+K] x [b, b+K] に含まれる点数"""
    cnt = 0
    for i in range(N):
        if a <= A[i] and A[i] <= a + K and b <= B[i] and B[i] <= b + K:
            cnt += 1
    return cnt


# --- 入力 -----------------------------------------------------------
N, K = map(int, input().split())
A = [None] * N
B = [None] * N
for i in range(N):
    A[i], B[i] = map(int, input().split())

# --- (a, b) を全探索して最大点数を求める ----------------------------
Answer = 0
for a in range(1, 101):
    for b in range(1, 101):
        Score = GetScore(a, b, A, B, K)
        Answer = max(Answer, Score)

# --- 出力 -----------------------------------------------------------
print(Answer)
