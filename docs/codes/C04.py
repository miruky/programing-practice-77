# # C04: Divisor Enumeration (力試し問題)
#
# ## 概要
#
# 整数 N の約数を昇順にすべて列挙する。
#
# ## アルゴリズム
#
# i = 1, 2, ..., √N を試し、N が i で割り切れるなら
#                 i と N/i を約数として記録 (i == N/i のときは重複に注意)。
#                 最後にソートして出力。
#
# ## 計算量
#
# O(√N · log(約数の個数))。N ≦ 10¹² でも余裕。
#
# ## 読み方の地図
#
# - 問題の型: 数学 / 約数 / 難易度目安: ★2
# - 要約: √N までの試し割りで N の約数を列挙し、昇順に出力。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。

# --- 入力 -----------------------------------------------------------
N = int(input())

# --- 約数列挙 -------------------------------------------------------
Yakusuu = []
i = 1
while i * i <= N:
    if N % i == 0:
        Yakusuu.append(i)
        if i != N // i:                  # 平方数 (i == N/i) のとき重複させない
            Yakusuu.append(N // i)
    i += 1

# --- 昇順にソートして出力 ------------------------------------------
Yakusuu.sort()
print("\n".join(map(str, Yakusuu)))
