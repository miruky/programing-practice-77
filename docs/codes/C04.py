# =====================================================================
# C04: Divisor Enumeration (力試し問題)
# ---------------------------------------------------------------------
# 【概要】整数 N の約数を昇順にすべて列挙する。
# 【アルゴリズム】i = 1, 2, ..., √N を試し、N が i で割り切れるなら
#                 i と N/i を約数として記録 (i == N/i のときは重複に注意)。
#                 最後にソートして出力。
# 【計算量】O(√N · log(約数の個数))。N ≦ 10¹² でも余裕。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())

# --- 約数列挙 -------------------------------------------------------
Yakusuu = []
i = 1
while i * i <= N:
    if N % i == 0:
        Yakusuu.append(i)
        if i != N // i:                  # 平方数 (i == N/i) のとき重複させない
            Yakusuu.append(N // i)
    i += 1

# --- 昇順にソートして出力 ------------------------------------------
Yakusuu.sort()
print("\n".join(map(str, Yakusuu)))
