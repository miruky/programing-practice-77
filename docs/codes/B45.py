# # B45: Blackboard 2 (A45 応用 ─ 不変量)
#
# ## 概要
#
# 3 つの整数 (a, b, c) に対して「2 つを選んで両方に +1 / 両方に -1」
#         のような操作を繰り返すと、総和 a+b+c が不変。よって、最終的に
#         全部 0 にできる ⇔ 最初から a+b+c = 0 のときに限る。
#
# ## アルゴリズム
#
# 総和を取って 0 か判定するだけ。
#
# ## 計算量
#
# O(1)。
#
# ## 教訓
#
# 操作のもとで保たれる量 (不変量) を見抜くと、答えが一発で決まる。
#
# ## 読み方の地図
#
# - 問題の型: 考察 / 不変量 / 難易度目安: ★4
# - 要約: 操作で総和が変わらないという不変量から、3 数の和が 0 か否かで判定する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。

# --- 入力 -----------------------------------------------------------
a, b, c = map(int, input().split())

# --- 出力 -----------------------------------------------------------
print("Yes" if a + b + c == 0 else "No")
