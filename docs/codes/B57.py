# B57: Calculator (A57 応用 ─ ダブリング)
# ---------------------------------------------------------------------
# 【概要】整数 x に対して「x → x − (x の各桁の和)」という操作を考える。
#         各 num = 1, 2, ..., N に対し、操作を K 回繰り返した値を答える。
# 【アルゴリズム】K は最大 10^9 のように大きいので、毎回シミュレートすると
#                 間に合わない。ダブリングで「2^d 回後の値」を前計算しておき、
#                 K の 2 進表現に従って合成する。
# 【計算量】前計算 O(N · 30) + 各クエリ O(30)。
# =====================================================================

# dp[d][i] = 「i を 2^d 回操作した結果の値」
dp = [[0] * 300009 for _ in range(30)]

# --- 入力 -----------------------------------------------------------
N, K = map(int, input().split())

# --- d = 0 (1 回操作後) を直接計算 ---------------------------------
for i in range(N + 1):
    dp[0][i] = i - sum(map(int, str(i)))

# --- ダブリング: 2^(d+1) 回後 = 2^d 回後を 2 回繰り返したもの ------
for d in range(29):
    for i in range(N + 1):
        dp[d + 1][i] = dp[d][dp[d][i]]

# --- 各 num について K 回後を求めて出力 ---------------------------
for num in range(1, N + 1):
    for d in range(30):
        if K & (1 << d):                 # K の d ビット目が立っていれば適用
            num = dp[d][num]
    print(num)
