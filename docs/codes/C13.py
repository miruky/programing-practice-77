# # C13: Select 2 (力試し問題)
#
# ## 概要
#
# 整数列 A[1..N] と整数 P が与えられる。i < j で
#         A[i] · A[j] ≡ P (mod 10⁹+7) となるペアの個数を求める。
#
# ## アルゴリズム
#
# 各 i について、「j < i かつ A[j] = P · A[i]⁻¹」となる j の
#                 個数を辞書 Count から取り出して加算 → A[i] を Count に追加。
#         逆元は フェルマーの小定理で a^(mod-2) を繰り返し二乗法で求める。
#         A[i] = 0 の特殊ケース: P = 0 ならどんな j とも条件を満たす。
#
# ## 計算量
#
# O(N · log mod)。
#
# ## 読み方の地図
#
# - 問題の型: 数学 / modular逆元 / 累積カウント / 難易度目安: ★4
# - 要約: mod 上で A[i]·A[j] ≡ P となるペア数を、mod 逆元と累積カウントで数える。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。

mod = 1000000007

# a^b mod m を返す関数 (繰り返し二乗法)
def Power(a, b, m):
    p = a
    Answer = 1
    for i in range(30):
        wari = 1 << i
        if (b // wari) % 2 == 1:
            Answer = (Answer * p) % m
        p = (p * p) % m
    return Answer

# a ÷ b mod m を逆元で実現
def Division(a, b, m):
    return (a * Power(b, m - 2, m)) % m

# --- 入力 -----------------------------------------------------------
N, P = map(int, input().split())
A = [0] + list(map(int, input().split()))
for i in range(1, N + 1):
    A[i] %= mod

# --- 累積カウントで条件を満たすペアを数える ------------------------
Count = {}
Answer = 0
for i in range(1, N + 1):
    if A[i] == 0:
        # A[i] = 0 のとき A[i]*A[j] = 0。P = 0 ならすべての j が該当
        if P == 0:
            Answer += (i - 1)
    else:
        # A[i] * Goal ≡ P  ⇒  Goal = P / A[i] (mod)
        Goal = Division(P, A[i], mod)
        Answer += Count.get(Goal, 0)
    Count[A[i]] = Count.get(A[i], 0) + 1

# --- 出力 -----------------------------------------------------------
print(Answer)
