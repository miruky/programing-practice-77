# =====================================================================
# A14: 半分全列挙 (4箱問題)
# ---------------------------------------------------------------------
# 【概要】4 つの配列 A, B, C, D から 1 つずつ選んだ和が K になる
#         組合せがあるかを判定する。
# 【アルゴリズム】"半分全列挙"
#   1. 全 4 つの組合せ (N^4 通り) は爆発するので、2 つずつに分解する。
#   2. P = { A[x]+B[y] }, Q = { C[z]+D[w] } を作る (どちらも N^2 通り)。
#   3. Q をソートし、P[i] ごとに K - P[i] が Q にあるか二分探索する。
# 【計算量】O(N^2 log N)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 二分探索 / 半分全列挙 / 難易度目安: ★4
# - 要約: 4 つの集合の組合せを 2 つに分け、和の集合を二分探索で照合する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 二分探索は値を探すというより、条件が NG から OK に切り替わる境目を探す。
# - bisect_left は「その値を入れてもソートが崩れない一番左の位置」を返す。
# - LIS や座標圧縮では、配列そのものではなく順位・置き場所を管理する。
# ---------------------------------------------------------------------

import bisect
import sys

# --- 入力 -----------------------------------------------------------
N, K = map(int, input().split())
A = list(map(int, input().split()))
B = list(map(int, input().split()))
C = list(map(int, input().split()))
D = list(map(int, input().split()))

# --- 半分 1: A と B の和 (N^2 個) ---------------------------------
P = []
for x in range(N):
    for y in range(N):
        P.append(A[x] + B[y])

# --- 半分 2: C と D の和 (N^2 個) ---------------------------------
Q = []
for z in range(N):
    for w in range(N):
        Q.append(C[z] + D[w])

# 二分探索のため Q をソートしておく。
Q.sort()

# --- 各 P[i] について K - P[i] が Q に含まれるかを二分探索 --------
for i in range(len(P)):
    # bisect_left は「ソート順を保ったまま挿入できる最も左の位置」を返す。
    pos1 = bisect.bisect_left(Q, K - P[i])
    if pos1 < N * N and Q[pos1] == K - P[i]:
        print("Yes")
        sys.exit(0)  # 見つかった時点で終了

# どの P[i] についても見つからなかった
print("No")
