# =====================================================================
# B24: Many Boxes (A24 応用 ─ LIS を使った箱詰め問題)
# ---------------------------------------------------------------------
# 【概要】N 個の箱 (X_i, Y_i) がある。X と Y がともに「真に大きい」箱に
#         入れていけるとき、最大いくつ入れ子にできるかを求める。
# 【アルゴリズム】X 昇順、X が同じなら Y 降順 でソートし、Y 列の最長増加部分
#                 列 (LIS) の長さを取る ─ これが最大入れ子段数になる。
#                 Y を降順にするのは「X が同じ箱は 1 つしか入れ子にできない」ため。
# 【計算量】ソート O(N log N) + LIS O(N log N)。
# =====================================================================
import bisect

# --- 入力 -----------------------------------------------------------
N = int(input())
X = [None] * N
Y = [None] * N
for i in range(N):
    X[i], Y[i] = map(int, input().split())

# --- 配列 A の LIS の長さ (dp 配列なし版) -------------------------
def Get_LISvalue(A):
    LEN = 0
    L = []
    for i in range(N):
        pos = bisect.bisect_left(L, A[i])
        if pos == LEN:
            L.append(A[i])
            LEN += 1
        else:
            L[pos] = A[i]
    return LEN

# --- (X 昇順, Y 降順) でソート -------------------------------------
# 第二キーを降順にするため -Y を入れる。
tmp = [[X[i], -Y[i]] for i in range(N)]
tmp.sort()

# --- LIS を取るのは Y の列 (符号を戻す) ---------------------------
A = [-tmp[i][1] for i in range(N)]

# --- 出力 -----------------------------------------------------------
print(Get_LISvalue(A))
