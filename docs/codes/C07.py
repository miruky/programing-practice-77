# # C07: ALGO-MARKET (力試し問題)
#
# ## 概要
#
# N 個の商品の値段 C[1..N] と Q 件の予算 X[i] が与えられる。
#         各予算で買える商品の最大個数を求める。
#
# ## アルゴリズム
#
# C を昇順ソートし累積和 S を取れば、「k 個買うときの
#                 最小金額」が S[k]。S は単調非減少なので、「S[k] ≦ X」
#                 を満たす最大 k を二分探索 (upper_bound - 1) で得られる。
#
# ## 計算量
#
# O(N log N + Q log N)。

import bisect
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 二分探索 / 累積和 / 難易度目安: ★3
# - 要約: 値段昇順 + 累積和 + 二分探索で「予算 X 円で買える最大個数」を答える。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - S[i] は先頭から i 個分の合計。S[0]=0 を置くと端の例外処理が消える。
# - 区間和は「右端までの合計 - 左手前までの合計」と読む。
# - 入力が 1-indexed、Python 配列が 0-indexed なので添字の変換だけ注意する。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N = int(input())
C = list(map(int, input().split()))
Q = int(input())
X = [int(input()) for _ in range(Q)]

# --- 値段を昇順ソート & 累積和 -------------------------------------
C.sort()
S = [0] * (N + 1)
for i in range(1, N + 1):
    S[i] = S[i - 1] + C[i - 1]

# --- 各予算について「予算以下となる最大個数」を二分探索 -----------
# bisect_right(S, x) は「S 内で x より大きい最初の位置」 ─ そこ -1 が答え。
out = []
for x in X:
    pos = bisect.bisect_right(S, x)
    out.append(str(pos - 1))
print("\n".join(out))
