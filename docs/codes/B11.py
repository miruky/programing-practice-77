# =====================================================================
# B11: Binary Search 2 (A11 応用)
# ---------------------------------------------------------------------
# 【概要】配列 A をソートし、Q 個のクエリ X について「A の中で X 未満の
#         要素の個数」を答える。
# 【アルゴリズム】bisect.bisect_left は「X を挿入できる最も左の位置」を
#                 返す ─ これがそのまま「X 未満の要素数」になる。
# 【計算量】ソート O(N log N) + 各クエリ O(log N)。
# =====================================================================
import bisect
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 二分探索 / 難易度目安: ★3
# - 要約: ソート後に bisect_left で「X 未満の要素数」を答える典型。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 二分探索は値を探すというより、条件が NG から OK に切り替わる境目を探す。
# - bisect_left は「その値を入れてもソートが崩れない一番左の位置」を返す。
# - LIS や座標圧縮では、配列そのものではなく順位・置き場所を管理する。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))
Q = int(input())
X = [None] * Q
for i in range(Q):
    X[i] = int(input())

# --- 配列をソート (二分探索の前提条件) -----------------------------
A.sort()

# --- クエリ処理 -----------------------------------------------------
# bisect_left(A, x) = "x 未満の要素数" = "x を入れるなら左寄せでこの位置"
for i in range(Q):
    pos = bisect.bisect_left(A, X[i])
    print(pos)
