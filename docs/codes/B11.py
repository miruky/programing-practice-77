# =====================================================================
# B11: Binary Search 2 (A11 応用)
# ---------------------------------------------------------------------
# 【概要】配列 A をソートし、Q 個のクエリ X について「A の中で X 未満の
#         要素の個数」を答える。
# 【アルゴリズム】bisect.bisect_left は「X を挿入できる最も左の位置」を
#                 返す ─ これがそのまま「X 未満の要素数」になる。
# 【計算量】ソート O(N log N) + 各クエリ O(log N)。
# =====================================================================
import bisect

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))
Q = int(input())
X = [None] * Q
for i in range(Q):
    X[i] = int(input())

# --- 配列をソート (二分探索の前提条件) -----------------------------
A.sort()

# --- クエリ処理 -----------------------------------------------------
# bisect_left(A, x) = "x 未満の要素数" = "x を入れるなら左寄せでこの位置"
for i in range(Q):
    pos = bisect.bisect_left(A, X[i])
    print(pos)
