# =====================================================================
# B41: Reverse of Euclid (A41 応用 ─ 互除法の逆操作)
# ---------------------------------------------------------------------
# 【概要】(1,1) からスタートして「片方に他方を足す」を繰り返し、最終的に
#         (X, Y) になる過程を 1 つ出力する。X, Y は互いに素であることが
#         保証される。
# 【アルゴリズム】(X, Y) から逆向きに操作を辿る ─ つまり「大きい方から小さい
#                 方を引く」を、両方が 1 になるまで繰り返す。これは
#                 GCD 互除法を引き算ベースで実行しているのと同じ。
# 【計算量】O(log max(X, Y))。
# =====================================================================

# --- 入力 -----------------------------------------------------------
X, Y = map(int, input().split())

# --- 逆向きシミュレーション ----------------------------------------
Answer = []
while X >= 2 or Y >= 2:
    Answer.append([X, Y])
    if X > Y:
        X -= Y                            # 大きい方から小さい方を引く
    else:
        Y -= X
Answer.reverse()                          # 順方向に直す

# --- 出力 -----------------------------------------------------------
print(len(Answer))
for i in range(len(Answer)):
    print(str(Answer[i][0]) + ' ' + str(Answer[i][1]))
