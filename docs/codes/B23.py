# =====================================================================
# B23: Traveling Salesman Problem (A23 応用 ─ bitDP / TSP)
# ---------------------------------------------------------------------
# 【概要】N 都市の座標が与えられ、都市 0 から出発して全都市を一度ずつ訪れ、
#         都市 0 に戻ってくる最短経路長を求める (巡回セールスマン問題)。
# 【アルゴリズム】bitDP。
#         dp[S][j] = 「集合 S の都市を訪問済みで、現在都市 j にいる」最短距離。
#         遷移: dp[S | (1<<k)][k] = min(..., dp[S][j] + dist(j, k))
# 【計算量】O(2^N · N²)。N=15 程度まで現実的。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())
X = [None] * N
Y = [None] * N
for i in range(N):
    X[i], Y[i] = map(int, input().split())

# --- DP テーブル ---------------------------------------------------
# dp[S][j]: 訪問済集合 S, 現在地 j ─ 行数は 2^N、列数は N
INF_F = 1_000_000_000.0
dp = [[INF_F] * N for _ in range(2 ** N)]

# --- 初期状態 (都市 0 だけ訪問済、現在地 0) ------------------------
dp[0][0] = 0

# --- 動的計画法 (前向き遷移) ---------------------------------------
for i in range(2 ** N):
    for j in range(N):
        if dp[i][j] >= INF_F:
            continue                      # 到達できない状態はスキップ
        # 都市 j から次に訪れる都市 k (まだ未訪問) を全探索
        for k in range(N):
            if (i >> k) & 1 == 0:
                DIST = ((X[j] - X[k]) ** 2 + (Y[j] - Y[k]) ** 2) ** 0.5
                if dp[i + (1 << k)][k] > dp[i][j] + DIST:
                    dp[i + (1 << k)][k] = dp[i][j] + DIST

# --- 出力: 全都市訪問済 (S = 2^N - 1)、終点 0 に戻る --------------
# 注: 本問では「都市 0 に戻る」のではなく、全都市踏破時点での
#      都市 0 着の値が問われている。
print(dp[(2 ** N) - 1][0])
