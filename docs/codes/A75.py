# # A75: 締切付きジョブ DP
#
# ## 概要
#
# N 個のジョブ (所要時間 T, 締切 D) から、いくつかを実行して
#         最大の実行数を求める。
#
# ## アルゴリズム
#
# 貪欲ソート + DP
#   - 締切 D で昇順にソートしておくと、後の判定が単純になる。
#   - dp[i][j] = i 個目までのジョブを考え、現在時刻 j のときの最大実行数
#
# ## 計算量
#
# O(N * MAX_D)
#
# ## 読み方の地図
#
# - 問題の型: DP / 貪欲 / 難易度目安: ★4
# - 要約: 締切でソート + DP で解ける典型問題。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。

# --- 入力 -----------------------------------------------------------
N = int(input())
problems = [list(map(int, input().split())) for _ in range(N)]

# --- 締切順にソート ------------------------------------------------
problems.sort(key=lambda p: p[1])

# --- DP 配列 ----------------------------------------------------
MAX_D = max(map(lambda p: p[1], problems))  # 締切の最大値
dp = [[-(10 ** 10)] * (MAX_D + 1) for _ in range(N + 1)]
dp[0][0] = 0  # 時刻 0、何もしていない

# --- DP 遷移 -------------------------------------------------------
for i in range(N):
    t, d = problems[i]
    for j in range(MAX_D + 1):
        if j > d or j < t:
            # 時刻 j に問題 i を解けない
            dp[i + 1][j] = dp[i][j]
        else:
            # i を解かない or 解く (時刻 j-t から +1 個解く)
            dp[i + 1][j] = max(dp[i][j], dp[i][j - t] + 1)

# --- 出力 -----------------------------------------------------------
print(max(dp[N]))
