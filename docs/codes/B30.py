# # B30: Combination 2 (A30 応用 ─ mod 逆元と二項係数)
#
# ## 概要
#
# H 行 W 列のグリッド左上から右下までの単調経路数 (H+W-2)C(W-1)
#         を 10⁹+7 で割った余りで出力する。
#
# ## アルゴリズム
#
# フェルマーの小定理: 素数 m に対し b^(m-2) ≡ b⁻¹ (mod m)。
#                 これで割り算を「逆元の掛け算」に変換し、組合せ数を計算。
#
# ## 計算量
#
# O((H+W) + log m)。逆元計算は繰り返し二乗法で O(log m)。
#
# ## 読み方の地図
#
# - 問題の型: 数学 / modular逆元 / 数え上げ / 難易度目安: ★4
# - 要約: H×W グリッドの経路数 (H+W-2)C(W-1) を mod 逆元で求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。

# a^b mod m を返す関数 (繰り返し二乗法)。
def Power(a, b, m):
    p = a
    Answer = 1
    for i in range(30):
        wari = 2 ** i
        if (b // wari) % 2 == 1:
            Answer = (Answer * p) % m
        p = (p * p) % m
    return Answer

# a ÷ b mod m を、b の mod 逆元を使って実現する関数。
def Division(a, b, m):
    return (a * Power(b, m - 2, m)) % m

# nCr mod 1000000007 を返す関数。
def ncr(n, r):
    M = 1000000007

    # 分子 a = n! mod M
    a = 1
    for i in range(1, n + 1):
        a = (a * i) % M

    # 分母 b = r! · (n-r)! mod M
    b = 1
    for i in range(1, r + 1):
        b = (b * i) % M
    for i in range(1, n - r + 1):
        b = (b * i) % M

    # 答えは a ÷ b
    return Division(a, b, M)

# --- 入出力 ---------------------------------------------------------
H, W = map(int, input().split())
print(ncr(H + W - 2, W - 1))
