# =====================================================================
# B53: Job Scheduling (A53 応用 ─ 優先度付きキュー)
# ---------------------------------------------------------------------
# 【概要】N 件の仕事があり、仕事 i は X[i] 日目以降に開放され、給料は Y[i]。
#         1 日に 1 件ずつ、D 日間で得られる総給料の最大値を求める。
# 【アルゴリズム】貪欲: 日付 i について「現時点で開放済みの仕事」の中で
#                 最大給料のものを取る ─ これを優先度付きキュー (ヒープ) で
#                 効率化する。Python の heapq は最小ヒープなので、給料に
#                 -1 を掛けて入れる。
# 【計算量】O((N + D) log N)。
# =====================================================================
import heapq

# G[i] = i 日目に開放される仕事の給料リスト
G = [list() for _ in range(2005)]

# --- 入力 -----------------------------------------------------------
N, D = map(int, input().split())
for _ in range(N):
    X, Y = map(int, input().split())
    G[X].append(Y)

# --- 各日に解禁ジョブをヒープに追加し、最大給料を取る -------------
Q = []
Answer = 0
for i in range(1, D + 1):
    # i 日目に解禁された仕事をすべてキューに入れる
    for y in G[i]:
        heapq.heappush(Q, -y)            # 最大値を取り出したいので -1 倍

    # 最大給料の仕事を 1 件こなす
    if Q:
        Answer -= heapq.heappop(Q)       # -1 倍を戻して加算 (= 引き算する)

# --- 出力 -----------------------------------------------------------
print(Answer)
