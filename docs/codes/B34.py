# =====================================================================
# B34: Game 7 (A34 応用 ─ Grundy 数 (周期))
# ---------------------------------------------------------------------
# 【概要】N 山の石ゲームを 2 人で行う。1 回の手番では 1 山から 1〜4 個の石を
#         取り除ける。取れなくなった方が負け。先後判定。
# 【アルゴリズム】各山の Grundy 数を計算すると、5 で割った余りに対し
#                 [0, 0, 1, 1, 2] のパターンになる (簡易な手計算 / 後退解析で確認可)。
#                 全山の Grundy 数の XOR が 0 でなければ先手必勝。
# 【計算量】O(N)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, X, Y = map(int, input().split())
A = list(map(int, input().split()))

# --- 各山の Grundy 数を XOR で集約 ---------------------------------
XOR_Sum = 0
for i in range(N):
    Grundy = [0, 0, 1, 1, 2]              # mod 5 ごとの Grundy 値テーブル
    XOR_Sum ^= Grundy[A[i] % 5]

# --- 出力 -----------------------------------------------------------
print("First" if XOR_Sum != 0 else "Second")
