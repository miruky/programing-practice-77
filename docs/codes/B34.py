# # B34: Game 7 (A34 応用 ─ Grundy 数 (周期))
#
# ## 概要
#
# N 山の石ゲームを 2 人で行う。1 回の手番では 1 山から 1〜4 個の石を
#         取り除ける。取れなくなった方が負け。先後判定。
#
# ## アルゴリズム
#
# 各山の Grundy 数を計算すると、5 で割った余りに対し
#                 [0, 0, 1, 1, 2] のパターンになる (簡易な手計算 / 後退解析で確認可)。
#                 全山の Grundy 数の XOR が 0 でなければ先手必勝。
#
# ## 計算量
#
# O(N)。
#
# ## 読み方の地図
#
# - 問題の型: ゲーム / Grundy / 難易度目安: ★5
# - 要約: 石を 1,2,3,4 個取れるゲームの Grundy 数は 5 周期になることを利用する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。

# --- 入力 -----------------------------------------------------------
N, X, Y = map(int, input().split())
A = list(map(int, input().split()))

# --- 各山の Grundy 数を XOR で集約 ---------------------------------
XOR_Sum = 0
for i in range(N):
    Grundy = [0, 0, 1, 1, 2]              # mod 5 ごとの Grundy 値テーブル
    XOR_Sum ^= Grundy[A[i] % 5]

# --- 出力 -----------------------------------------------------------
print("First" if XOR_Sum != 0 else "Second")
