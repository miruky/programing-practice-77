# =====================================================================
# A23: bitDP (クーポン券問題)
# ---------------------------------------------------------------------
# 【概要】N 種類の品物がある。M 枚のクーポンを使って全品を無料にする
#         必要枚数を求める。各クーポンは一部の品物だけ無料にできる。
# 【アルゴリズム】bitDP
#   状態 j (0 〜 2^N - 1) は「どの品物が既に無料か」を 2 進数で表す。
#   dp[i][j] = 最初の i 枚で状態 j にするための最小クーポン使用枚数。
#   各クーポンに対して使う/使わないを試して遷移する。
# 【計算量】O(M * 2^N * N)
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, M = map(int, input().split())
A = [None] * M  # A[i] は i 番目のクーポンが各品物を無料にできるかのフラグ列
for i in range(0, M):
    A[i] = list(map(int, input().split()))

# --- DP 初期化 ------------------------------------------------------
# 到達不能を 10^9 で表現する。
dp = [[10 ** 9] * (2 ** N) for _ in range(M + 1)]
dp[0][0] = 0  # 何もクーポンを使わず、何も無料になっていない状態

# --- DP 遷移 -------------------------------------------------------
for i in range(1, M + 1):
    for j in range(0, 2 ** N):
        # already[k] = 1 ⟺ 品物 k は既に無料になっている (状態 j のビット k)
        already = [None] * N
        for k in range(0, N):
            if (j // (2 ** k)) % 2 == 0:
                already[k] = 0
            else:
                already[k] = 1

        # クーポン i を使ったあとの状態 v を計算する。
        v = 0
        for k in range(0, N):
            if already[k] == 1 or A[i - 1][k] == 1:
                v += (2 ** k)

        # 遷移
        dp[i][j] = min(dp[i][j], dp[i - 1][j])           # クーポン i を使わない
        dp[i][v] = min(dp[i][v], dp[i - 1][j] + 1)       # クーポン i を使う

# --- 出力 -----------------------------------------------------------
# 全部が 1 のビット = 2^N - 1 (全部無料の状態)。
if dp[M][2 ** N - 1] == 1000000000:
    print("-1")
else:
    print(dp[M][2 ** N - 1])
