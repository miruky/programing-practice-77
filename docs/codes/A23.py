# =====================================================================
# A23: bitDP (クーポン券問題)
# ---------------------------------------------------------------------
# 【概要】N 種類の品物に対し、M 枚のクーポンがある。各クーポン i は
#         どの品物を無料にできるかが 0/1 で与えられる。
#         全部の品物を無料にするために必要な最小枚数を求める。
# 【アルゴリズム】2 次元 bitDP
#   dp[i][j] = 「i 枚目までを処理し、無料になっている品物の集合が j」
#              を作るのに必要な最小枚数。
#   j は N ビットの整数。bit k = 1 なら品物 k が無料。
#   遷移は「i 枚目を使わない: dp[i][j] = min(dp[i][j], dp[i-1][j])」
#         「i 枚目を使う:    dp[i][v] = min(dp[i][v], dp[i-1][j] + 1)」
#   ここで v = (i 枚目を j に重ねた集合) を整数演算で作る。
# 【計算量】O(M · 2^N · N)
# 【ポイント】到達不能は十分大きい 10^9 で表す。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: DP / bitDP / 難易度目安: ★4
# - 要約: 集合の状態をビットで表す bit DP（クーポン券問題）。
# - 整数 j を 2 進数として見て、各 bit が ON/OFF の集合を表す。
# - 1 << k は k 番目だけが ON の集合。OR は追加、XOR は反転として読む。
# - 状態数は 2^N。N が小さいからこそ成立する全探索 + DP。
# ---------------------------------------------------------------------

# --- 入力 (配列 A は 0-indexed なので、i 枚目は A[i-1]) -------------
N, M = map(int, input().split())
A = [None] * M
for i in range(M):
    A[i] = list(map(int, input().split()))

# --- DP 配列初期化 (10^9 = 到達不能) -------------------------------
dp = [[10 ** 9] * (2 ** N) for _ in range(M + 1)]
dp[0][0] = 0  # 0 枚で「無料の品物なし」だけが達成可能

# --- DP 本体 -------------------------------------------------------
for i in range(1, M + 1):
    for j in range(0, 2 ** N):
        # already[k] = 1 のとき、品物 k は既に状態 j で無料になっている
        already = [0] * N
        for k in range(0, N):
            if (j // (2 ** k)) % 2 == 1:
                already[k] = 1

        # クーポン i (= A[i-1]) を使った後の整数表現 v を計算する
        v = 0
        for k in range(0, N):
            if already[k] == 1 or A[i - 1][k] == 1:
                v += (2 ** k)

        # 遷移: 使わない / 使う の両方
        dp[i][j] = min(dp[i][j], dp[i - 1][j])
        dp[i][v] = min(dp[i][v], dp[i - 1][j] + 1)

# --- 出力 (全 bit が 1 = 全品物無料 の状態を見る) -------------------
if dp[M][2 ** N - 1] == 10 ** 9:
    print(-1)
else:
    print(dp[M][2 ** N - 1])
