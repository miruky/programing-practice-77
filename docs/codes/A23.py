# =====================================================================
# A23: bitDP (クーポン券問題)
# ---------------------------------------------------------------------
# 【概要】N 種類の品物がある。M 枚のクーポンを使って全品を無料にする
#         必要枚数を求める。各クーポンは一部の品物だけ無料にできる。
# 【アルゴリズム】bitDP
#   状態 state (0 〜 2^N - 1) は「どの品物が既に無料か」を 2 進数で表す。
#   各クーポンも bitmask に変換し、使った後の状態を state | coupon_mask で作る。
#   dp[state] = その状態を作るのに必要なクーポン枚数の最小値。
# 【計算量】O(M * 2^N)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: DP / bitDP / 難易度目安: ★4
# - 要約: 集合の状態をビットで表す bit DP（クーポン券問題）。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 整数 state を 2 進数として見て、各 bit が ON/OFF の集合を表す。
# - 1 << k は k 番目だけが ON の集合。OR は追加、XOR は反転として読む。
# - 状態数は 2^N。N が小さいからこそ成立する全探索 + DP/探索。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N, M = map(int, input().split())
coupon_masks = []
for _ in range(M):
    row = list(map(int, input().split()))

    # row[k] = 1 なら「品物 k を無料にできる」。
    # それを 2 進数の k bit 目に移すと、クーポンを整数 1 個で表せる。
    mask = 0
    for k in range(N):
        if row[k] == 1:
            mask |= (1 << k)
    coupon_masks.append(mask)

# --- DP 初期化 ------------------------------------------------------
# 到達不能を 10^9 で表現する。
INF = 10 ** 9
ALL_FREE = (1 << N) - 1
dp = [INF] * (1 << N)
dp[0] = 0  # 何もクーポンを使わず、何も無料になっていない状態

# --- DP 遷移 -------------------------------------------------------
# 各クーポンについて「使う / 使わない」を考える。
# 使わない場合は dp がそのまま残る。使う場合だけ new_dp を更新する。
for coupon in coupon_masks:
    new_dp = dp[:]
    for state in range(1 << N):
        next_state = state | coupon
        new_dp[next_state] = min(new_dp[next_state], dp[state] + 1)
    dp = new_dp

# --- 出力 -----------------------------------------------------------
# 全部が 1 のビット = 2^N - 1 (全部無料の状態)。
if dp[ALL_FREE] == INF:
    print("-1")
else:
    print(dp[ALL_FREE])
