# # B29: Power Hard (A29 応用 ─ 繰り返し二乗法)
#
# ## 概要
#
# a^b mod 10⁹+7 を求める。b は最大 10¹⁸ 程度。
#
# ## アルゴリズム
#
# 繰り返し二乗法。
#         b を 2 進数表現に分解し、a^(2^0), a^(2^1), a^(2^2), ... を順に
#         2 乗で計算しながら、b の対応する bit が立っているところだけ
#         答えに掛け合わせる。
#
# ## 計算量
#
# O(log b)。b = 10¹⁸ でも 60 回ループで終わる。
#
# ## 読み方の地図
#
# - 問題の型: 数学 / 繰り返し二乗法 / 難易度目安: ★3
# - 要約: 繰り返し二乗法で a^b mod 10^9+7 を O(log b) で計算する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。

# a^b mod m を返す関数。
def Power(a, b, m):
    p = a                                  # p は a^(2^i) を保持する変数
    Answer = 1
    for i in range(60):
        wari = 2 ** i
        if (b // wari) % 2 == 1:
            Answer = (Answer * p) % m      # b の i 桁目が 1 のとき掛ける
        p = (p * p) % m                    # p を 2 乗して次の桁へ
    return Answer

# --- 入出力 ---------------------------------------------------------
a, b = map(int, input().split())
print(Power(a, b, 1000000007))
