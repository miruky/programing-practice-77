# =====================================================================
# B29: Power Hard (A29 応用 ─ 繰り返し二乗法)
# ---------------------------------------------------------------------
# 【概要】a^b mod 10⁹+7 を求める。b は最大 10¹⁸ 程度。
# 【アルゴリズム】繰り返し二乗法。
#         b を 2 進数表現に分解し、a^(2^0), a^(2^1), a^(2^2), ... を順に
#         2 乗で計算しながら、b の対応する bit が立っているところだけ
#         答えに掛け合わせる。
# 【計算量】O(log b)。b = 10¹⁸ でも 60 回ループで終わる。
# =====================================================================

# a^b mod m を返す関数。
def Power(a, b, m):
    p = a                                  # p は a^(2^i) を保持する変数
    Answer = 1
    for i in range(60):
        wari = 2 ** i
        if (b // wari) % 2 == 1:
            Answer = (Answer * p) % m      # b の i 桁目が 1 のとき掛ける
        p = (p * p) % m                    # p を 2 乗して次の桁へ
    return Answer

# --- 入出力 ---------------------------------------------------------
a, b = map(int, input().split())
print(Power(a, b, 1000000007))
