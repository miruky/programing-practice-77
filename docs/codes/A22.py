# =====================================================================
# A22: すごろく (DP)
# ---------------------------------------------------------------------
# 【概要】マス i にいるとき、A[i] へジャンプ (+100 点) または B[i] へジャンプ
#         (+150 点) のどちらかを選んで進む。マス N に到達するときの最大点数。
# 【アルゴリズム】DP (前進更新)
#   dp[i] = マス i に到達した時点の最大点数 (到達不可能なら -∞)
#   遷移: マス i から先へジャンプして dp[A[i-1]] と dp[B[i-1]] を更新する。
# 【計算量】O(N)
# 【ポイント】"後ろから更新する" のではなく "自分から進めるマスを更新する"。
# =====================================================================

# --- 入力 (A, B は 0-indexed) --------------------------------------
N = int(input())
A = list(map(int, input().split()))
B = list(map(int, input().split()))

# --- DP 初期化 ------------------------------------------------------
# dp[1] = 0 (スタート), 他は -∞ (到達不可)。
dp = [-(10 ** 9)] * (N + 1)
dp[1] = 0

# --- DP 遷移 -------------------------------------------------------
# マス i から A[i-1], B[i-1] への遷移で値を更新する。
for i in range(1, N):
    dp[A[i - 1]] = max(dp[A[i - 1]], dp[i] + 100)
    dp[B[i - 1]] = max(dp[B[i - 1]], dp[i] + 150)

# --- 出力 -----------------------------------------------------------
print(dp[N])
