# # A22: すごろく (DP)
#
# ## 概要
#
# マス i にいるとき、A[i] へジャンプ (+100 点) または B[i] へジャンプ
#         (+150 点) のどちらかを選んで進む。マス N に到達するときの最大点数。
#
# ## アルゴリズム
#
# DP (前進更新)
#   dp[i] = マス i に到達した時点の最大点数 (到達不可能なら -∞)
#   遷移: マス i から先へジャンプして dp[A[i-1]] と dp[B[i-1]] を更新する。
#
# ## 計算量
#
# O(N)
#
# ## ポイント
#
# "後ろから更新する" のではなく "自分から進めるマスを更新する"。
#
# ## 読み方の地図
#
# - 問題の型: DP / 難易度目安: ★3
# - 要約: 各マスから 2 種類のジャンプができるすごろくの最大スコア DP。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。

# --- 入力 (A, B は 0-indexed) --------------------------------------
N = int(input())
A = list(map(int, input().split()))
B = list(map(int, input().split()))

# --- DP 初期化 ------------------------------------------------------
# dp[1] = 0 (スタート), 他は -∞ (到達不可)。
dp = [-(10 ** 9)] * (N + 1)
dp[1] = 0

# --- DP 遷移 -------------------------------------------------------
# マス i から A[i-1], B[i-1] への遷移で値を更新する。
for i in range(1, N):
    dp[A[i - 1]] = max(dp[A[i - 1]], dp[i] + 100)
    dp[B[i - 1]] = max(dp[B[i - 1]], dp[i] + 150)

# --- 出力 -----------------------------------------------------------
print(dp[N])
