# # A45: 色の保存量 (不変量を発見せよ)
#
# ## 概要
#
# 白(W), 黒(B), 赤(R) の 3 色からなる文字列が、決まった操作の結果
#         指定の文字 C になるか判定する。
#
# ## アルゴリズム
#
# 不変量の発見
#   W=0, B=1, R=2 と数値化し、3 で割った余りが操作前後で保たれることに着目。
#   最終的に残る 1 文字の色は、合計 score を 3 で割った余りで決まる。
#
# ## 計算量
#
# O(N)
#
# ## 読み方の地図
#
# - 問題の型: 考察 / 不変量 / 難易度目安: ★3
# - 要約: 操作で変わらない量（不変量）を発見し答える。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。

# --- 入力 -----------------------------------------------------------
N, C = input().split()
A = input()
N = int(N)

# --- 各文字を数値に変換し、その合計を計算 ---------------------------
score = 0
for i in range(N):
    if A[i] == 'W':
        score += 0
    if A[i] == 'B':
        score += 1
    if A[i] == 'R':
        score += 2

# --- mod 3 が一致するかで判定 ---------------------------------------
if score % 3 == 0 and C == 'W':
    print("Yes")
elif score % 3 == 1 and C == 'B':
    print("Yes")
elif score % 3 == 2 and C == 'R':
    print("Yes")
else:
    print("No")
