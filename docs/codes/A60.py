# =====================================================================
# A60: スタックで「直前の自分より大きい要素」を見つける
# ---------------------------------------------------------------------
# 【概要】株価列 A から、各日 i について「i 日より前で A[j] > A[i] となる
#         直近の日 j」を求める (なければ -1)。
# 【アルゴリズム】単調スタック (Monotonic Stack)
#   スタックには (日付, 株価) を入れる。新しい日が来たら、それ以下の株価を
#   スタックの末尾から取り除く。残った末尾が答え。
# 【計算量】O(N) ─ 各要素は高々 1 回 push, 1 回 pop されるだけ。
# =====================================================================

from collections import deque

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- スタックで前日方向のスキャン ----------------------------------
answer = [None] * N
level2 = deque()  # (日付, 株価) のスタック

for i in range(N):
    if i >= 1:
        # 1 日前を追加
        level2.append((i, A[i - 1]))
        # 末尾が現在の株価以下なら除去 (上書きされうるため意味がない)
        while len(level2) >= 1:
            kabuka = level2[-1][1]
            if kabuka <= A[i]:
                level2.pop()
            else:
                break
    # 残った末尾が「自分より大きい直近の日」
    if len(level2) >= 1:
        answer[i] = level2[-1][0]
    else:
        answer[i] = -1

# --- 出力 -----------------------------------------------------------
print(*answer)
