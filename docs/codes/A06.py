# =====================================================================
# A06: 1次元累積和
# ---------------------------------------------------------------------
# 【概要】配列 A に対して Q 個の区間和クエリ [L, R] に答える。
# 【アルゴリズム】累積和 S[i] = A[0] + ... + A[i-1] を前計算。
#                 区間 [L, R] の和は S[R] - S[L-1] で O(1) で求まる。
# 【計算量】前計算 O(N) + クエリ O(Q)。各クエリ O(1)。
# 【ポイント】先頭に S[0] = 0 を入れ、1-indexed で持つと境界処理が楽。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, Q = map(int, input().split())
A = list(map(int, input().split()))

# 各クエリの左右端を読む。L[j], R[j] は 1-indexed で与えられる。
L = [None] * Q
R = [None] * Q
for j in range(Q):
    L[j], R[j] = map(int, input().split())

# --- 累積和の前計算 -------------------------------------------------
# S[i] = A[0] + A[1] + ... + A[i-1]
# 番兵 (S[0]=0) を 1 つ前に置くことで、区間和を S[R]-S[L-1] と引き算で書ける。
S = [None] * (N + 1)
S[0] = 0
for i in range(N):
    S[i + 1] = S[i] + A[i]

# --- クエリ処理 -----------------------------------------------------
# 区間 [L[j], R[j]] の総和を O(1) で返す。
for j in range(Q):
    print(S[R[j]] - S[L[j] - 1])
