# # A06: 1次元累積和
#
# ## 概要
#
# 配列 A に対して Q 個の区間和クエリ [L, R] に答える。
#
# ## アルゴリズム
#
# 累積和 S[i] = A[0] + ... + A[i-1] を前計算。
#                 区間 [L, R] の和は S[R] - S[L-1] で O(1) で求まる。
#
# ## 計算量
#
# 前計算 O(N) + クエリ O(Q)。各クエリ O(1)。
#
# ## ポイント
#
# 先頭に S[0] = 0 を入れ、1-indexed で持つと境界処理が楽。
#
# ## 読み方の地図
#
# - 問題の型: 累積和 / 難易度目安: ★2
# - 要約: 累積和を前計算しておくことで区間和クエリを O(1) で答える。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - S[i] は先頭から i 個分の合計。S[0]=0 を置くと端の例外処理が消える。
# - 区間和は「右端までの合計 - 左手前までの合計」と読む。
# - 入力が 1-indexed、Python 配列が 0-indexed なので添字の変換だけ注意する。

# --- 入力 -----------------------------------------------------------
N, Q = map(int, input().split())
A = list(map(int, input().split()))

# 各クエリの左右端を読む。L[j], R[j] は 1-indexed で与えられる。
L = [None] * Q
R = [None] * Q
for j in range(Q):
    L[j], R[j] = map(int, input().split())

# --- 累積和の前計算 -------------------------------------------------
# S[i] = A[0] + A[1] + ... + A[i-1]
# 番兵 (S[0]=0) を 1 つ前に置くことで、区間和を S[R]-S[L-1] と引き算で書ける。
S = [None] * (N + 1)
S[0] = 0
for i in range(N):
    S[i + 1] = S[i] + A[i]

# --- クエリ処理 -----------------------------------------------------
# 区間 [L[j], R[j]] の総和を O(1) で返す。
for j in range(Q):
    print(S[R[j]] - S[L[j] - 1])
