# # B09: Papers (A09 応用 ─ 2 次元いもす法)
#
# ## 概要
#
# N 枚の長方形の紙を平面に置く。少なくとも 1 枚以上の紙で覆われて
#         いるマスの個数 (= 重ね合わせた図形の総面積) を求める。
#
# ## アルゴリズム
#
# 2 次元いもす法 ─ 各長方形の四隅で ±1 を打ち、横→縦の
#                 累積和を取れば各マスの被覆枚数になる。1 以上のマスを数える。
#
# ## 計算量
#
# O(N + 座標範囲²)。
#
# ## 読み方の地図
#
# - 問題の型: 累積和 / いもす法 / 2次元 / 難易度目安: ★4
# - 要約: 2 次元いもす法で、複数の長方形が覆う総面積を求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 長方形の全マスを直接更新せず、四隅に +1/-1 の印だけ置く。
# - 印を置いた時点では答えではない。横方向・縦方向の累積和で面に戻す。
# - 閉区間か半開区間かで、-1 を置く座標が 1 つずれるので最初に確認する。

# --- 入力 -----------------------------------------------------------
N = int(input())
A = [None] * N
B = [None] * N
C = [None] * N
D = [None] * N
for i in range(N):
    A[i], B[i], C[i], D[i] = map(int, input().split())

# --- 四隅に ±1 を打つ (いもす法) ----------------------------------
# 長方形 [a,b]〜[c,d] に +1 を加算 ⇔ 4 つの角に +1/-1/-1/+1 を置く。
T = [[0] * 1501 for _ in range(1501)]
for i in range(N):
    T[A[i]][B[i]] += 1
    T[A[i]][D[i]] -= 1
    T[C[i]][B[i]] -= 1
    T[C[i]][D[i]] += 1

# --- 2 次元累積和で被覆枚数に復元 ----------------------------------
# T[x][y] を「セル (x,y) が何枚の紙で覆われているか」に戻す。
# いもすの四隅の印は、累積和を取って初めて面の値になる。
for x in range(1501):
    for y in range(1, 1501):
        T[x][y] += T[x][y - 1]
for x in range(1, 1501):
    for y in range(1501):
        T[x][y] += T[x - 1][y]

# --- 1 枚以上覆われているマス数を数える ----------------------------
# 紙が覆うセルは 0..1499 の 1500×1500 個。1500 番目の境界線は面積に含めない。
Answer = 0
for x in range(1500):
    for y in range(1500):
        if T[x][y] >= 1:
            Answer += 1

# --- 出力 -----------------------------------------------------------
print(Answer)
