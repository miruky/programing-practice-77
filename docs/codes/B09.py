# =====================================================================
# B09: Papers (A09 応用 ─ 2 次元いもす法)
# ---------------------------------------------------------------------
# 【概要】N 枚の長方形の紙を平面に置く。少なくとも 1 枚以上の紙で覆われて
#         いるマスの個数 (= 重ね合わせた図形の総面積) を求める。
# 【アルゴリズム】2 次元いもす法 ─ 各長方形の四隅で ±1 を打ち、横→縦の
#                 累積和を取れば各マスの被覆枚数になる。1 以上のマスを数える。
# 【計算量】O(N + 座標範囲²)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())
A = [None] * N
B = [None] * N
C = [None] * N
D = [None] * N
for i in range(N):
    A[i], B[i], C[i], D[i] = map(int, input().split())

# --- 四隅に ±1 を打つ (いもす法) ----------------------------------
# 長方形 [a,b]〜[c,d] に +1 を加算 ⇔ 4 つの角に +1/-1/-1/+1 を置く。
T = [[0] * 1501 for _ in range(1501)]
for i in range(N):
    T[A[i]][B[i]] += 1
    T[A[i]][D[i]] -= 1
    T[C[i]][B[i]] -= 1
    T[C[i]][D[i]] += 1

# --- 横方向 → 縦方向の累積和 --------------------------------------
for i in range(0, 1501):
    for j in range(1, 1501):
        T[i][j] = T[i][j - 1] + T[i][j]
for i in range(1, 1501):
    for j in range(0, 1501):
        T[i][j] = T[i - 1][j] + T[i][j]

# --- 1 枚以上覆われているマス数を数える ----------------------------
Answer = 0
for i in range(1501):
    for j in range(1501):
        if T[i][j] >= 1:
            Answer += 1

# --- 出力 -----------------------------------------------------------
print(Answer)
