# # B42: Two Faced Cards (A42 応用 ─ 全探索 + 貪欲)
#
# ## 概要
#
# N 枚のカードはそれぞれ表 A[i] と裏 B[i] の値を持つ。任意の枚数の
#         カードを選んで「表の総和」「裏の総和」を作るとき、(|表の総和| +
#         |裏の総和|) を最大化する。
#
# ## アルゴリズム
#
# 「表の総和の符号」「裏の総和の符号」の 4 通り (++/+-/-+/--)
#                 を全探索する。各場合、カード i を選ぶか否かは独立に決められ、
#                 「(符号付き)表 + (符号付き)裏 ≧ 0 なら選ぶ」貪欲で最適。
#
# ## 計算量
#
# O(4N)。
#
# ## 読み方の地図
#
# - 問題の型: 全探索 / 考察 / 難易度目安: ★3
# - 要約: 「表の総和の符号」「裏の総和の符号」の 2×2 通りを全探索して最大値を求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。

# 与えられた符号 (1 = 正, 2 = 負) の組み合わせで最大化する関数。
def solve(omote, ura, A, B):
    s = 0
    for i in range(N):
        card1 = A[i] if omote == 1 else -A[i]
        card2 = B[i] if ura   == 1 else -B[i]
        # 寄与が非負なら選ぶ、それ以外は捨てる ─ これが貪欲の本質
        if card1 + card2 >= 0:
            s += (card1 + card2)
    return s

# --- 入力 -----------------------------------------------------------
N = int(input())
A = [None] * N
B = [None] * N
for i in range(N):
    A[i], B[i] = map(int, input().split())

# --- 4 通りを全探索 ------------------------------------------------
Answer1 = solve(1, 1, A, B)
Answer2 = solve(1, 2, A, B)
Answer3 = solve(2, 1, A, B)
Answer4 = solve(2, 2, A, B)

# --- 出力 -----------------------------------------------------------
print(max(Answer1, Answer2, Answer3, Answer4))
