# =====================================================================
# A36: ちょうど K 歩 (偶奇考察)
# ---------------------------------------------------------------------
# 【概要】N×N の盤面で、左上 (1,1) から右下 (N,N) まで丁度 K 歩で
#         移動できるかを判定する。
# 【アルゴリズム】考察 (上界 + 偶奇)
#   - 最短歩数は 2N-2。これより K が小さければ不可能。
#   - 1 歩進む → 戻るで歩数を 2 ずつ増やせるので、K - (2N-2) は偶数。
#   ⇔ K >= 2N-2 かつ K と 2N-2 の偶奇が同じ (= K が偶数)
# 【計算量】O(1)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 考察 / 偶奇 / 難易度目安: ★2
# - 要約: 偶奇と最小歩数の上界に注目すれば必要十分条件が見える。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

N, K = map(int, input().split())

# 必要十分条件: K >= 最短歩数 かつ K が偶数
if K >= 2 * N - 2 and K % 2 == 0:
    print("Yes")
else:
    print("No")
