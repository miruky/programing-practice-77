# =====================================================================
# A63: 幅優先探索 (BFS) ─ 重みなし最短距離
# ---------------------------------------------------------------------
# 【概要】頂点 1 から各頂点までの最短距離を求める。
# 【アルゴリズム】BFS
#   キューから頂点を取り出し、未訪問の隣接頂点に距離 +1 を入れる。
#   重みなしグラフでは BFS が最短距離を保証する。
# 【計算量】O(N + M)
# 【ポイント】未訪問判定に dist[i] == -1 を使うと初期化と兼ねられる。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: グラフ / BFS / 難易度目安: ★2
# - 要約: BFS で重みなしグラフの最短距離を求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力をまず隣接リストやグリッドの隣接関係に変換している部分を見る。
# - visited や dist は「もう見たか」「何手で来たか」を記録するための配列。
# - DFS は深く進む、BFS は近い順に広がる。この違いだけ先に押さえる。
# ---------------------------------------------------------------------

from collections import deque

# --- 入力 -----------------------------------------------------------
N, M = map(int, input().split())
edges = [list(map(int, input().split())) for _ in range(M)]

# --- 隣接リスト ---------------------------------------------------
G = [list() for _ in range(N + 1)]
for a, b in edges:
    G[a].append(b)
    G[b].append(a)

# --- BFS の初期化 -------------------------------------------------
dist = [-1] * (N + 1)
dist[1] = 0
Q = deque()
Q.append(1)

# --- BFS -----------------------------------------------------------
while len(Q) >= 1:
    pos = Q.popleft()  # 先頭から取り出す
    for nex in G[pos]:
        if dist[nex] == -1:
            dist[nex] = dist[pos] + 1  # 1 ステップ進めて距離 +1
            Q.append(nex)

# --- 出力 (頂点 1 → i の最短距離) ----------------------------------
for i in range(1, N + 1):
    print(dist[i])
