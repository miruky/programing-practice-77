# # B62: Print a Path (A62 応用 ─ DFS で経路出力)
#
# ## 概要
#
# 無向グラフで、頂点 1 から頂点 N までの単純パスを 1 つ出力する。
#
# ## アルゴリズム
#
# DFS で訪問しながらパスをスタックに積み、ゴールに着いたら
#                 そのまま出力して終了。バックトラック時は pop する。
#
# ## 計算量
#
# O(N + M)。
#
# ## 注意
#
# Python の再帰デフォルト上限は 1000 なので、N が大きいときは
#         setrecursionlimit を引き上げる。

import sys
sys.setrecursionlimit(1 << 20)
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: グラフ / DFS / 経路復元 / 難易度目安: ★3
# - 要約: DFS でゴールまでの経路を 1 つ求めて出力する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力をまず隣接リストやグリッドの隣接関係に変換している部分を見る。
# - visited や dist は「もう見たか」「何手で来たか」を記録するための配列。
# - DFS は深く進む、BFS は近い順に広がる。この違いだけ先に押さえる。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N, M = map(int, input().split())
g = [[] for _ in range(N)]
for _ in range(M):
    A, B = map(int, input().split())
    A -= 1
    B -= 1
    g[A].append(B)
    g[B].append(A)

# --- DFS の状態 -----------------------------------------------------
visited = [False] * (N + 1)
path = []


def dfs(i: int):
    path.append(i)
    # ゴール (N-1) に到達 ─ そのままパスを出力して全プロセス終了
    if i == N - 1:
        for x in path:
            print(x + 1)                 # 1-indexed に戻す
        exit(0)

    # 通常の DFS
    visited[i] = True
    for j in g[i]:
        if not visited[j]:
            dfs(j)
    path.pop()                            # バックトラック


dfs(0)
