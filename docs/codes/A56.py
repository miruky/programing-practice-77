# =====================================================================
# A56: ローリングハッシュ
# ---------------------------------------------------------------------
# 【概要】文字列 S の部分列 S[l..r] と S[c..d] が一致するかをクエリで判定。
# 【アルゴリズム】ローリングハッシュ
#   H[i] = ハッシュ(S[0..i-1])  とすると、
#   部分列 S[l..r] のハッシュは H[r] - H[l-1] * 100^(r-l+1)  (mod M)
#   各部分列を 1 つの数値で比較できるため、各クエリ O(1)。
# 【計算量】前計算 O(N) + クエリ O(Q)。
# 【注意】M を素数、底 (= 100) を 1 より大きい値にする。衝突確率は 1/M。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, Q = map(int, input().split())
S = input()
queries = [list(map(int, input().split())) for _ in range(Q)]

# 文字 → 整数 (1〜26)。ord('a') が文字 'a' のコード。
T = list(map(lambda c: ord(c) - ord('a') + 1, S))

# --- 前計算 -------------------------------------------------------
MOD = 2147483647  # メルセンヌ素数 2^31 - 1
power100 = [None] * (N + 1)
power100[0] = 1
for i in range(N):
    power100[i + 1] = power100[i] * 100 % MOD

# プレフィックスハッシュ H[i]
H = [None] * (N + 1)
H[0] = 0
for i in range(N):
    H[i + 1] = (H[i] * 100 + T[i]) % MOD


def hash_value(l, r):
    """S[l..r] (1-indexed) のハッシュ値"""
    # Python の % は負の値でも 0 以上の結果になるので、補正不要
    return (H[r] - H[l - 1] * power100[r - l + 1]) % MOD


# --- クエリ処理 ---------------------------------------------------
for a, b, c, d in queries:
    hash1 = hash_value(a, b)
    hash2 = hash_value(c, d)
    if hash1 == hash2:
        print("Yes")
    else:
        print("No")
