# =====================================================================
# A07: いもす法 (区間加算 → 各日の参加人数)
# ---------------------------------------------------------------------
# 【概要】N 人の参加者が [L[i], R[i]] の期間中に参加する。
#         各日 d (1〜D) の参加人数を求める。
# 【アルゴリズム】"いもす法"
#   1. 差分配列 B に L[i] で +1、R[i]+1 で -1 を加える。
#   2. B の累積和を取れば、各日の参加人数になる。
# 【計算量】O(N + D)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 累積和 / いもす法 / 難易度目安: ★2
# - 要約: 区間加算を端点だけで処理し、累積和で復元するテクニック。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 区間全体を更新せず、始点で +、終点の次で - を置く。
# - 最後に左から累積和を取ると、各位置に実際の加算結果が広がる。
# - 右端を含む区間か、半開区間かで - を置く場所が変わる。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
D = int(input())
N = int(input())
L = [None] * N
R = [None] * N
for i in range(N):
    L[i], R[i] = map(int, input().split())

# --- 差分配列に区間加算をエンコード -------------------------------
# 配列を D+2 確保しておくと R[i]+1 へのアクセスでも安全。
B = [0] * (D + 2)
for i in range(N):
    B[L[i]] += 1      # 参加開始日: +1
    B[R[i] + 1] -= 1  # 参加終了日の翌日: -1 (打ち消し)

# --- 累積和で各日の参加人数を復元 -----------------------------------
Answer = [None] * (D + 2)
Answer[0] = 0
for d in range(1, D + 1):
    Answer[d] = Answer[d - 1] + B[d]

# --- 出力 -----------------------------------------------------------
for d in range(1, D + 1):
    print(Answer[d])
