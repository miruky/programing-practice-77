# =====================================================================
# A39: 区間スケジューリング
# ---------------------------------------------------------------------
# 【概要】N 個の区間 [l, r] から、できるだけ多くの非重複区間を選ぶ。
# 【アルゴリズム】終了時刻でソートして貪欲に選択する古典問題。
#   "終了時刻が早い区間ほど後の選択肢を残せる" という直観が根拠。
# 【計算量】O(N log N)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 貪欲 / 区間スケジューリング / 難易度目安: ★2
# - 要約: 終了時刻でソートし貪欲に選ぶ古典問題。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 貪欲法は、何を基準に並べるか・どの候補を先に取るかが本体。
# - コードより先に「その選択をしても後で損しない理由」をコメントで確認する。
# - ソート後は、左から順に決めて戻らない構造になっているかを見る。
# ---------------------------------------------------------------------

# --- 入力 (タプル順を (r, l) にして終了時刻で昇順ソートしやすくする) -----
N = int(input())
A = []
for i in range(N):
    l, r = map(int, input().split())
    A.append([r, l])

# 終了時刻 r の昇順にソート (l は 2 番目の要素なので副次的)
A.sort()

# --- 貪欲: 直前の終了時刻以降に始まる区間を順に採用 ----------------
CurrentTime = 0
Answer = 0
for i in range(N):
    if CurrentTime <= A[i][1]:  # i 番目の区間の開始時刻 >= 現在時刻
        CurrentTime = A[i][0]   # 区間の終了時刻に更新
        Answer += 1

# --- 出力 -----------------------------------------------------------
print(Answer)
