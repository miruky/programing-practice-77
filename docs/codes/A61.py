# =====================================================================
# A61: グラフの表現 (隣接リスト)
# ---------------------------------------------------------------------
# 【概要】N 頂点 M 辺の無向グラフを入力し、各頂点の隣接頂点を出力する。
# 【データ構造】隣接リスト G[i] = 頂点 i に隣接する頂点のリスト
# 【計算量】O(N + M)
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, M = map(int, input().split())
edges = [list(map(int, input().split())) for _ in range(M)]

# --- 隣接リストの構築 (1-indexed) -----------------------------------
G = [list() for _ in range(N + 1)]
for a, b in edges:
    # 無向グラフなので双方向に登録
    G[a].append(b)
    G[b].append(a)

# --- 出力 -----------------------------------------------------------
for i in range(1, N + 1):
    output = ''
    output += str(i)
    output += ': {'
    # 隣接頂点を ", " 区切りで連結
    output += ', '.join(map(str, G[i]))
    output += '}'
    print(output)
