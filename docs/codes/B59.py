# =====================================================================
# B59: Number of Inversions (A59 応用 ─ セグメント木 RSQ で転倒数)
# ---------------------------------------------------------------------
# 【概要】配列 A の転倒数 (i < j で A[i] > A[j] となる組の数) を求める。
# 【アルゴリズム】要素を左から見ていき、「自分より大きい要素のうち、
#                 既に処理済みの個数」を逐次足し合わせる。
#                 これは値 a が現れたら seg[a] += 1 を、各時点での
#                 query(a+1, MAX) (= a より大きい値の出現回数) を見ること
#                 で実現できる。
# 【計算量】O(N log V)。V は値の上限 (本問では 2^18 程度)。
# =====================================================================

siz = 1 << 18
dat = [0] * (siz * 2)


# seg[i] = v への代入と祖先の和の更新
def update(i: int, v: int) -> None:
    i += siz
    dat[i] = v
    while i > 1:
        i >>= 1
        dat[i] = dat[i * 2] + dat[i * 2 + 1]


# seg[l..r-1] の総和を取得
def query(l: int, r: int) -> int:
    l += siz
    r += siz
    answer = 0
    while l < r:
        if l & 1:
            answer += dat[l]
            l += 1
        if r & 1:
            r -= 1
            answer += dat[r]
        l >>= 1
        r >>= 1
    return answer


# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- 転倒数の計算 ---------------------------------------------------
Answer = 0
for a in A:
    # 自分より大きな数 (a+1 以上) が、これまで出た回数
    Answer += query(a + 1, siz)
    # 今回の値 a が 1 回現れたので登録
    update(a, 1)

# --- 出力 -----------------------------------------------------------
print(Answer)
