# # B59: Number of Inversions (A59 応用 ─ セグメント木 RSQ で転倒数)
#
# ## 概要
#
# 配列 A の転倒数 (i < j で A[i] > A[j] となる組の数) を求める。
#
# ## アルゴリズム
#
# 要素を左から見ていき、「自分より大きい要素のうち、
#                 既に処理済みの個数」を逐次足し合わせる。
#                 これは値 a が現れたら seg[a] += 1 を、各時点での
#                 query(a+1, MAX) (= a より大きい値の出現回数) を見ること
#                 で実現できる。
#
# ## 計算量
#
# O(N log V)。V は値の上限 (本問では 2^18 程度)。
#
# ## 読み方の地図
#
# - 問題の型: データ構造 / セグメント木 / 転倒数 / 難易度目安: ★5
# - 要約: Fenwick 風セグメント木で「自分より大きい要素の個数」を加算し転倒数を求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 葉が元配列、親が子 2 つの集約値。max / sum など、何を集約するかだけ変わる。
# - 更新は葉を書き換えてから親へ戻る。クエリは関係ない区間を捨てる。
# - 半開区間 [l,r) に統一すると、右端のズレを避けやすい。

siz = 1 << 18
dat = [0] * (siz * 2)


# seg[i] = v への代入と祖先の和の更新
def update(i: int, v: int) -> None:
    i += siz
    dat[i] = v
    while i > 1:
        i >>= 1
        dat[i] = dat[i * 2] + dat[i * 2 + 1]


# seg[l..r-1] の総和を取得
def query(l: int, r: int) -> int:
    l += siz
    r += siz
    answer = 0
    while l < r:
        if l & 1:
            answer += dat[l]
            l += 1
        if r & 1:
            r -= 1
            answer += dat[r]
        l >>= 1
        r >>= 1
    return answer


# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- 転倒数の計算 ---------------------------------------------------
Answer = 0
for a in A:
    # 自分より大きな数 (a+1 以上) が、これまで出た回数
    Answer += query(a + 1, siz)
    # 今回の値 a が 1 回現れたので登録
    update(a, 1)

# --- 出力 -----------------------------------------------------------
print(Answer)
