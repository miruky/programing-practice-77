# =====================================================================
# B43: Quiz Contest (A43 応用 ─ 集計 + 引き算)
# ---------------------------------------------------------------------
# 【概要】N 問のクイズが M 回出題され、A[k] は k 回目に出された問題番号。
#         各問題 i について「正解者数 = M - 不正解者数」を出力したい。
#         (本問では A[k] が不正解だったクイズ番号として与えられる)
# 【アルゴリズム】不正解数 Incorrect[i] を集計し、M から引く。
# 【計算量】O(N + M)。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 集計 / 考察 / 難易度目安: ★3
# - 要約: 各問題の不正解数を集計し、合計回答数から引くことで正解数を求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N, M = map(int, input().split())
A = list(map(int, input().split()))

# --- 不正解数を集計 ------------------------------------------------
Incorrect = [0] * (N + 1)
for i in range(M):
    Incorrect[A[i]] += 1

# --- 出力 (各クイズの正解数) ---------------------------------------
for i in range(1, N + 1):
    print(M - Incorrect[i])
