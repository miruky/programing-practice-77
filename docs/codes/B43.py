# =====================================================================
# B43: Quiz Contest (A43 応用 ─ 集計 + 引き算)
# ---------------------------------------------------------------------
# 【概要】N 問のクイズが M 回出題され、A[k] は k 回目に出された問題番号。
#         各問題 i について「正解者数 = M - 不正解者数」を出力したい。
#         (本問では A[k] が不正解だったクイズ番号として与えられる)
# 【アルゴリズム】不正解数 Incorrect[i] を集計し、M から引く。
# 【計算量】O(N + M)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, M = map(int, input().split())
A = list(map(int, input().split()))

# --- 不正解数を集計 ------------------------------------------------
Incorrect = [0] * (N + 1)
for i in range(M):
    Incorrect[A[i]] += 1

# --- 出力 (各クイズの正解数) ---------------------------------------
for i in range(1, N + 1):
    print(M - Incorrect[i])
