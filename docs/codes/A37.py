# =====================================================================
# A37: 貢献度の独立計算
# ---------------------------------------------------------------------
# 【概要】N+M+1 種類のコストを組合せで合計するときの総和を求める。
# 【アルゴリズム】"独立に数える" 考察
#   全 (i, j) ペアの和を直接計算すると O(NM) だが、
#   各要素ごとに「自分が何回足されるか」を求めると O(N + M) で済む。
# 【計算量】O(N + M)
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, M, B = map(int, input().split())
A = list(map(int, input().split()))
C = list(map(int, input().split()))

# --- 各要素の貢献度を計算 -------------------------------------------
# - A[i] は M 回足される (相手側 j を選ぶ数だけ)
# - C[j] は N 回足される
# - B は N*M 回足される (組合せ数だけ)
Answer = 0
for i in range(N):
    Answer += A[i] * M
Answer += B * N * M
for j in range(M):
    Answer += C[j] * N

# --- 出力 -----------------------------------------------------------
print(Answer)
