# =====================================================================
# A37: 貢献度の独立計算
# ---------------------------------------------------------------------
# 【概要】N+M+1 種類のコストを組合せで合計するときの総和を求める。
# 【アルゴリズム】"独立に数える" 考察
#   全 (i, j) ペアの和を直接計算すると O(NM) だが、
#   各要素ごとに「自分が何回足されるか」を求めると O(N + M) で済む。
# 【計算量】O(N + M)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 考察 / 貢献度 / 難易度目安: ★2
# - 要約: 各要素の貢献度を独立に計算し合計する典型。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N, M, B = map(int, input().split())
A = list(map(int, input().split()))
C = list(map(int, input().split()))

# --- 各要素の貢献度を計算 -------------------------------------------
# - A[i] は M 回足される (相手側 j を選ぶ数だけ)
# - C[j] は N 回足される
# - B は N*M 回足される (組合せ数だけ)
Answer = 0
for i in range(N):
    Answer += A[i] * M
Answer += B * N * M
for j in range(M):
    Answer += C[j] * N

# --- 出力 -----------------------------------------------------------
print(Answer)
