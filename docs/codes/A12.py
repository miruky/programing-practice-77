# =====================================================================
# A12: 答えで二分探索 (印刷機問題)
# ---------------------------------------------------------------------
# 【概要】N 台のプリンタがそれぞれ A[i] 分に 1 枚印刷する。
#         合計 K 枚印刷するのに最短何分かかるかを求める。
# 【アルゴリズム】"答えで二分探索" (めぐる式二分探索)
#   時間 x 以内に K 枚以上印刷できるか? という判定問題に帰着。
#   各プリンタは x 分で x // A[i] 枚印刷できるので、その総和で判定可能。
#   x が単調 (時間を増やすほど印刷可能枚数も増える) なので二分探索が効く。
# 【計算量】O(N log(maxAnswer))
# =====================================================================

# x 分以内に合計 K 枚以上印刷できるかを判定する関数。
def check(x, N, K, A):
    sum = 0
    for i in range(N):
        sum += x // A[i]  # プリンタ i は x 分で x // A[i] 枚作る
    return sum >= K       # K 枚以上ならば「OK」


# --- 入力 -----------------------------------------------------------
N, K = map(int, input().split())
A = list(map(int, input().split()))

# --- 答えで二分探索 -------------------------------------------------
# Left, Right は「答え (時間)」の範囲。
# ループ不変条件: 答えは [Left, Right] の区間内に存在する。
Left = 1
Right = 10 ** 9
while Left < Right:
    Mid = (Left + Right) // 2
    Answer = check(Mid, N, K, A)
    if Answer == False:
        Left = Mid + 1   # Mid 分では足りない → 答え >= Mid+1
    if Answer == True:
        Right = Mid      # Mid 分で OK → 答え <= Mid

# Left == Right となった時点でその値が答え。
print(Left)
