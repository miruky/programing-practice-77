# =====================================================================
# B52: Ball Simulation (A52 応用 ─ キュー / BFS シミュレーション)
# ---------------------------------------------------------------------
# 【概要】1 次元上に並んだマス列 (床) と、初期位置 X にあるボールを考える。
#         隣接する '.' (空きマス) に向かってボールが拡散していく様子を
#         シミュレートし、最終的にボールが到達したマスを '@' にして出力する。
# 【アルゴリズム】BFS。キューに「処理待ちのマス位置」を入れ、両隣の '.' を
#                 訪れて '@' に塗り直してキューに追加する。
# 【計算量】O(N)。
# =====================================================================
from collections import deque

# --- 入力 -----------------------------------------------------------
N, X = map(int, input().split())
X -= 1                                     # 1-indexed → 0-indexed
A = list(input())

# --- BFS ------------------------------------------------------------
Q = deque([X])
Q.append(X)                                # 元コードに合わせて 2 回追加 (重複は無害)
A[X] = '@'
while Q:
    pos = Q.popleft()
    # 左隣
    if pos - 1 >= 0 and A[pos - 1] == '.':
        A[pos - 1] = '@'
        Q.append(pos - 1)
    # 右隣
    if pos + 1 < N and A[pos + 1] == '.':
        A[pos + 1] = '@'
        Q.append(pos + 1)

# --- 出力 -----------------------------------------------------------
print("".join(A))
