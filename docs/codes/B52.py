# # B52: Ball Simulation (A52 応用 ─ キュー / BFS シミュレーション)
#
# ## 概要
#
# 1 次元上に並んだマス列 (床) と、初期位置 X にあるボールを考える。
#         隣接する '.' (空きマス) に向かってボールが拡散していく様子を
#         シミュレートし、最終的にボールが到達したマスを '@' にして出力する。
#
# ## アルゴリズム
#
# BFS。キューに「処理待ちのマス位置」を入れ、両隣の '.' を
#                 訪れて '@' に塗り直してキューに追加する。
#
# ## 計算量
#
# O(N)。

from collections import deque
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: データ構造 / キュー / BFS / 難易度目安: ★2
# - 要約: ボールが転がる範囲を BFS でシミュレートする (キューの応用)。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力をまず隣接リストやグリッドの隣接関係に変換している部分を見る。
# - visited や dist は「もう見たか」「何手で来たか」を記録するための配列。
# - DFS は深く進む、BFS は近い順に広がる。この違いだけ先に押さえる。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N, X = map(int, input().split())
X -= 1                                     # 1-indexed → 0-indexed
A = list(input())

# --- BFS ------------------------------------------------------------
Q = deque([X])
Q.append(X)                                # 元コードに合わせて 2 回追加 (重複は無害)
A[X] = '@'
while Q:
    pos = Q.popleft()
    # 左隣
    if pos - 1 >= 0 and A[pos - 1] == '.':
        A[pos - 1] = '@'
        Q.append(pos - 1)
    # 右隣
    if pos + 1 < N and A[pos + 1] == '.':
        A[pos + 1] = '@'
        Q.append(pos + 1)

# --- 出力 -----------------------------------------------------------
print("".join(A))
