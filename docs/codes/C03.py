# =====================================================================
# C03: Stock Queries (力試し問題)
# ---------------------------------------------------------------------
# 【概要】初日の株価 X と、2 日目以降の前日比 A[2..D] が与えられる。
#         Q 個のクエリ (S, T) について、S 日目と T 日目の株価を比べて
#         どちらが高いか (同じなら "Same") を答える。
# 【アルゴリズム】Price[i] = X + sum(A[2..i]) を累積和で前計算 → クエリ O(1)。
# 【計算量】前計算 O(D) + クエリ O(Q)。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 累積和 / クエリ / 難易度目安: ★2
# - 要約: 前日比からの累積和で各日の株価を求め、2 日の比較クエリに答える。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - S[i] は先頭から i 個分の合計。S[0]=0 を置くと端の例外処理が消える。
# - 区間和は「右端までの合計 - 左手前までの合計」と読む。
# - 入力が 1-indexed、Python 配列が 0-indexed なので添字の変換だけ注意する。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
D, X = map(int, input().split())
# A[1] はダミー、A[2..D] が前日比
A = [0] * (D + 1)
vals = list(map(int, input().split()))
for i, v in enumerate(vals, start=2):
    A[i] = v

Q = int(input())
S = [0] * (Q + 1)
T = [0] * (Q + 1)
for i in range(1, Q + 1):
    S[i], T[i] = map(int, input().split())

# --- 各日の株価を累積和で計算 ---------------------------------------
Price = [0] * (D + 1)
Price[1] = X
for i in range(2, D + 1):
    Price[i] = Price[i - 1] + A[i]

# --- クエリ処理 -----------------------------------------------------
out = []
for i in range(1, Q + 1):
    if Price[S[i]] > Price[T[i]]:
        out.append(str(S[i]))
    elif Price[S[i]] < Price[T[i]]:
        out.append(str(T[i]))
    else:
        out.append("Same")
print("\n".join(out))
