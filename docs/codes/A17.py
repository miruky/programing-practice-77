# # A17: ダンジョン2 (DP + 経路復元)
#
# ## 概要
#
# A16 と同じ問題で、さらに最短経路 (通過した部屋の列) を出力する。
#
# ## アルゴリズム
#
# DP 後にゴール側から逆方向にトレースバックする経路復元。
#   ゴールから出発し、dp の値と一致する遷移をたどり直す。
#
# ## 計算量
#
# O(N)
#
# ## 実装メモ
#
# A16 と同じく A, B にダミーを詰めて 1-indexed 化することで、
#             dp[i] = min(dp[i-1]+A[i], dp[i-2]+B[i]) と書籍の数式に揃える。
#
# ## 読み方の地図
#
# - 問題の型: DP / 経路復元 / 難易度目安: ★3
# - 要約: DP で求めた最短距離をもとに、経路を復元する。
# - dp[i] の意味は A16 と同じ。
# - 復元では「dp[i-1] + A[i] == dp[i] なら 1 個前から来た」を判定して
#   遡るだけでよい (= が 2 つあるときは A 側 (1 個進む) を優先しても OK)。

# --- 入力 + DP (A16 と同じ) -----------------------------------------
N = int(input())
A = [0, 0] + list(map(int, input().split()))      # A[2..N]
B = [0, 0, 0] + list(map(int, input().split()))   # B[3..N]

dp = [0] * (N + 1)
dp[1] = 0
dp[2] = A[2]
for i in range(3, N + 1):
    dp[i] = min(dp[i - 1] + A[i], dp[i - 2] + B[i])

# --- 経路復元 ------------------------------------------------------
# Place は現在位置。ゴール N から始めて部屋 1 まで遡る。
# 例: 入力例 1 の場合、Place は 5 → 4 → 2 → 1 と変化する。
Answer = []
Place = N
while True:
    Answer.append(Place)
    if Place == 1:
        break
    # 「どちらの遷移で来たか」を dp 値から逆算する。
    # dp[Place-1] + A[Place] == dp[Place] ならば 1 個前から来たと分かる。
    if dp[Place - 1] + A[Place] == dp[Place]:
        Place = Place - 1
    else:
        Place = Place - 2
Answer.reverse()  # スタート→ゴール の順に並べ直す

# --- 出力 -----------------------------------------------------------
print(len(Answer))
print(" ".join(str(i) for i in Answer))
