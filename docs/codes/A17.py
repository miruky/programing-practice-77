# =====================================================================
# A17: ダンジョン2 (DP + 経路復元)
# ---------------------------------------------------------------------
# 【概要】A16 と同じ問題で、さらに最短経路 (通過した部屋の列) を求める。
# 【アルゴリズム】DP 後に逆方向にトレースバックする経路復元の典型手法。
#   ゴールから出発し、dp の値と一致する遷移をたどり直す。
# 【計算量】O(N)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: DP / 経路復元 / 難易度目安: ★3
# - 要約: DP で求めた最短距離をもとに、経路を復元する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。
# ---------------------------------------------------------------------

# --- 入力・DP (A16 と同じ) -----------------------------------------
N = int(input())
A = list(map(int, input().split()))
B = list(map(int, input().split()))

dp = [None] * (N + 1)
dp[1] = 0
dp[2] = A[0]
for i in range(3, N + 1):
    dp[i] = min(dp[i - 1] + A[i - 2], dp[i - 2] + B[i - 3])

# --- 経路復元 ------------------------------------------------------
# Place は現在位置。ゴール N から始めて部屋 1 まで遡る。
# 例: 入力例 1 の場合、Place は 5 → 4 → 2 → 1 と変化する。
Answer = []
Place = N
while True:
    Answer.append(Place)
    if Place == 1:
        break
    # 「どちらの遷移で来たか」を dp 値から逆算する。
    # dp[Place-1] + A[Place-2] == dp[Place] ならば 1 個前から来た。
    if dp[Place - 1] + A[Place - 2] == dp[Place]:
        Place = Place - 1
    else:
        Place = Place - 2
Answer.reverse()  # スタート→ゴール の順に並べ直す

# --- 出力 -----------------------------------------------------------
Answer2 = [str(i) for i in Answer]
print(len(Answer))
print(" ".join(Answer2))
