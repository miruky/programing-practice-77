# # A38: 区間の上限値
#
# ## 概要
#
# 各日に対して複数の上限制約があり、各日の許容最大値の合計を答える。
#
# ## アルゴリズム
#
# 各日について全制約の min を取って最終的な上限を計算。
#
# ## 計算量
#
# O(ND)
#
# ## 読み方の地図
#
# - 問題の型: 考察 / 難易度目安: ★2
# - 要約: 各時刻の上界を区間ごとに更新し、上界の合計を答える。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。

# --- 入力 -----------------------------------------------------------
D, N = map(int, input().split())
L = [None] * N
R = [None] * N
H = [None] * N
for i in range(N):
    L[i], R[i], H[i] = map(int, input().split())

# --- 各日の上限値を求める -------------------------------------------
# 初期値は 24 (1 日の上限)。
LIM = [24] * (D + 1)
for i in range(N):
    for j in range(L[i], R[i] + 1):
        LIM[j] = min(LIM[j], H[i])

# --- 合計を出力 -----------------------------------------------------
Answer = 0
for i in range(1, D + 1):
    Answer += LIM[i]
print(Answer)
