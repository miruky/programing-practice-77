# =====================================================================
# A38: 区間の上限値
# ---------------------------------------------------------------------
# 【概要】各日に対して複数の上限制約があり、各日の許容最大値の合計を答える。
# 【アルゴリズム】各日について全制約の min を取って最終的な上限を計算。
# 【計算量】O(ND)
# =====================================================================

# --- 入力 -----------------------------------------------------------
D, N = map(int, input().split())
L = [None] * N
R = [None] * N
H = [None] * N
for i in range(N):
    L[i], R[i], H[i] = map(int, input().split())

# --- 各日の上限値を求める -------------------------------------------
# 初期値は 24 (1 日の上限)。
LIM = [24] * (D + 1)
for i in range(N):
    for j in range(L[i], R[i] + 1):
        LIM[j] = min(LIM[j], H[i])

# --- 合計を出力 -----------------------------------------------------
Answer = 0
for i in range(1, D + 1):
    Answer += LIM[i]
print(Answer)
