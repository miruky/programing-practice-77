# =====================================================================
# A04: 10進数を2進数へ
# ---------------------------------------------------------------------
# 【概要】0 以上 1023 以下の整数 N を 10 桁の 2 進数表記で出力する。
# 【アルゴリズム】2^x で割って 2 で割った余りを取り、上位桁から並べる。
# 【計算量】O(log N) = O(10)。
# 【Pythonic 別解】 `print(format(N, "010b"))` で同じ結果が得られる。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: ビット / 進法 / 難易度目安: ★1
# - 要約: 整数を 10 桁の 2 進法表記で出力する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N = int(input())

# --- 各桁を上から下へ計算 -------------------------------------------
# x = 9, 8, ..., 0 の順に "2^x の位" を取り出して連結出力する。
#   wari = 2^x として N を wari で割った商の偶奇 (=その桁のビット) を求める。
# print の end='' により改行せず連結出力する。
for x in [9, 8, 7, 6, 5, 4, 3, 2, 1, 0]:
    wari = (2 ** x)
    print((N // wari) % 2, end='')

# 最後に改行する (end='' で改行を抑制しているため明示的に必要)。
print("")
