# # A29: 繰り返し二乗法
#
# ## 概要
#
# a^b mod m を高速に求める。
#
# ## アルゴリズム
#
# 繰り返し二乗法 (Fast Exponentiation)
#   b を 2 進数表記すると b = Σ (2^i * bit_i)。
#   a^(2^i) を二乗で次々作り、bit_i = 1 のときだけ掛け合わせる。
#
# ## 計算量
#
# O(log b)
#
# ## 補足
#
# Python 標準には pow(a, b, m) があり、これと同じ動作。
#
# ## 読み方の地図
#
# - 問題の型: 数学 / 繰り返し二乗法 / 難易度目安: ★2
# - 要約: a^b mod m を O(log b) で求める繰り返し二乗法。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。

def Power(a, b, m):
    p = a       # 現在 a の 2^i 乗を保持する変数
    Answer = 1  # 結果。初期値は乗法単位元の 1
    for i in range(30):
        wari = 2 ** i
        if (b // wari) % 2 == 1:
            # b の 2^i の位が 1 のとき、答えに a^(2^i) を掛ける。
            Answer = (Answer * p) % m
        # 次のループのため p を二乗して p = a^(2^(i+1)) にする。
        p = (p * p) % m
    return Answer


# --- 入力・出力 -----------------------------------------------------
a, b = map(int, input().split())
print(Power(a, b, 1000000007))
