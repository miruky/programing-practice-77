# =====================================================================
# A29: 繰り返し二乗法
# ---------------------------------------------------------------------
# 【概要】a^b mod m を高速に求める。
# 【アルゴリズム】繰り返し二乗法 (Fast Exponentiation)
#   b を 2 進数表記すると b = Σ (2^i * bit_i)。
#   a^(2^i) を二乗で次々作り、bit_i = 1 のときだけ掛け合わせる。
# 【計算量】O(log b)
# 【補足】Python 標準には pow(a, b, m) があり、これと同じ動作。
# =====================================================================

def Power(a, b, m):
    p = a       # 現在 a の 2^i 乗を保持する変数
    Answer = 1  # 結果。初期値は乗法単位元の 1
    for i in range(30):
        wari = 2 ** i
        if (b // wari) % 2 == 1:
            # b の 2^i の位が 1 のとき、答えに a^(2^i) を掛ける。
            Answer = (Answer * p) % m
        # 次のループのため p を二乗して p = a^(2^(i+1)) にする。
        p = (p * p) % m
    return Answer


# --- 入力・出力 -----------------------------------------------------
a, b = map(int, input().split())
print(Power(a, b, 1000000007))
