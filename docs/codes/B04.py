# 【概要】2 進数として与えられた文字列 N を 10 進数に変換して出力する。
# 【アルゴリズム】先頭の桁から見ていき、その桁が '1' なら 2^(残り桁数)
#                 を加算する手書きの基数変換。
# 【計算量】O(len(N))。
# 【別解】Python なら int(N, 2) 一発で済むが、ここでは仕組み理解のため手で。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: ビット / 進法 / 難易度目安: ★2
# - 要約: 2 進数文字列を読み込んで 10 進数に変換して出力。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
# input() は文字列のまま受け取る (int() を噛ませない)。
N = input()

# --- 2 進数 → 10 進数の変換 ----------------------------------------
Answer = 0
for i in range(len(N)):
    keta = 0                            # この桁が立っているなら 1、そうでなければ 0
    kurai = 2 ** (len(N) - 1 - i)       # この桁の重み (左端が最上位)
    if N[i] == '1':
        keta = 1
    Answer += keta * kurai              # 立っている桁の重みだけを足し込む

# --- 出力 -----------------------------------------------------------
print(Answer)
