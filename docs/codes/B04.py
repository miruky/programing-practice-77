# 【概要】2 進数として与えられた文字列 N を 10 進数に変換して出力する。
# 【アルゴリズム】先頭の桁から見ていき、その桁が '1' なら 2^(残り桁数)
#                 を加算する手書きの基数変換。
# 【計算量】O(len(N))。
# 【別解】Python なら int(N, 2) 一発で済むが、ここでは仕組み理解のため手で。
# =====================================================================

# --- 入力 -----------------------------------------------------------
# input() は文字列のまま受け取る (int() を噛ませない)。
N = input()

# --- 2 進数 → 10 進数の変換 ----------------------------------------
Answer = 0
for i in range(len(N)):
    keta = 0                            # この桁が立っているなら 1、そうでなければ 0
    kurai = 2 ** (len(N) - 1 - i)       # この桁の重み (左端が最上位)
    if N[i] == '1':
        keta = 1
    Answer += keta * kurai              # 立っている桁の重みだけを足し込む

# --- 出力 -----------------------------------------------------------
print(Answer)
