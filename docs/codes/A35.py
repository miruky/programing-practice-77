# # A35: 交互最善ゲーム DP (二人ゲーム木)
#
# ## 概要
#
# N 段のピラミッドを上から下へ進み、最終的に達した値を競う。
#         奇数段は先手 (max を選ぶ)、偶数段は後手 (min を選ぶ)。
#
# ## アルゴリズム
#
# 後退解析 DP
#   dp[i][j] = 段 i, 位置 j のときに到達する最終値 (互いに最善を尽くす)
#   下段から上に積み上げる。
#
# ## 計算量
#
# O(N^2)
#
# ## 読み方の地図
#
# - 問題の型: ゲーム / DP / 難易度目安: ★3
# - 要約: 2 人交互の最善手 DP。階層ごとに min と max を切り替える。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- DP 配列 -------------------------------------------------------
dp = [[None] * (N + 1) for _ in range(N + 1)]

# 最下段は値そのもの。
for j in range(1, N + 1):
    dp[N][j] = A[j - 1]

# 下段 → 上段 へ計算する。
for i in reversed(range(1, N)):
    for j in range(1, i + 1):
        if i % 2 == 1:
            # 奇数段は先手: 大きい方を選ぶ (max)
            dp[i][j] = max(dp[i + 1][j], dp[i + 1][j + 1])
        if i % 2 == 0:
            # 偶数段は後手: 小さい方を選ぶ (min)
            dp[i][j] = min(dp[i + 1][j], dp[i + 1][j + 1])

# --- 出力 -----------------------------------------------------------
print(dp[1][1])
