# =====================================================================
# A35: 交互最善ゲーム DP (二人ゲーム木)
# ---------------------------------------------------------------------
# 【概要】N 段のピラミッドを上から下へ進み、最終的に達した値を競う。
#         奇数段は先手 (max を選ぶ)、偶数段は後手 (min を選ぶ)。
# 【アルゴリズム】後退解析 DP
#   dp[i][j] = 段 i, 位置 j のときに到達する最終値 (互いに最善を尽くす)
#   下段から上に積み上げる。
# 【計算量】O(N^2)
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- DP 配列 -------------------------------------------------------
dp = [[None] * (N + 1) for _ in range(N + 1)]

# 最下段は値そのもの。
for j in range(1, N + 1):
    dp[N][j] = A[j - 1]

# 下段 → 上段 へ計算する。
for i in reversed(range(1, N)):
    for j in range(1, i + 1):
        if i % 2 == 1:
            # 奇数段は先手: 大きい方を選ぶ (max)
            dp[i][j] = max(dp[i + 1][j], dp[i + 1][j + 1])
        if i % 2 == 0:
            # 偶数段は後手: 小さい方を選ぶ (min)
            dp[i][j] = min(dp[i + 1][j], dp[i + 1][j + 1])

# --- 出力 -----------------------------------------------------------
print(dp[1][1])
