# =====================================================================
# A33: Nim ゲーム
# ---------------------------------------------------------------------
# 【概要】N 個の山。各手番でいずれかの山から 1 個以上取る。取れない方が負け。
# 【アルゴリズム】Nim 定理 (Sprague-Grundy)
#   全部 XOR (ニム和) ≠ 0 ⇔ 先手必勝
#   全部 XOR = 0          ⇔ 後手必勝
# 【計算量】O(N)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: ゲーム / Nim / 難易度目安: ★3
# - 要約: Nim ゲームは XOR が 0 でないとき先手必勝。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 整数 state を 2 進数として見て、各 bit が ON/OFF の集合を表す。
# - 1 << k は k 番目だけが ON の集合。OR は追加、XOR は反転として読む。
# - 状態数は 2^N。N が小さいからこそ成立する全探索 + DP/探索。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- ニム和を計算 --------------------------------------------------
# ^ は Python の XOR 演算子。
XOR_Sum = A[0]
for i in range(1, N):
    XOR_Sum = (XOR_Sum ^ A[i])

# --- 出力 -----------------------------------------------------------
if XOR_Sum >= 1:
    print("First")
else:
    print("Second")
