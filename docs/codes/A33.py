# =====================================================================
# A33: Nim ゲーム
# ---------------------------------------------------------------------
# 【概要】N 個の山。各手番でいずれかの山から 1 個以上取る。取れない方が負け。
# 【アルゴリズム】Nim 定理 (Sprague-Grundy)
#   全部 XOR (ニム和) ≠ 0 ⇔ 先手必勝
#   全部 XOR = 0          ⇔ 後手必勝
# 【計算量】O(N)
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- ニム和を計算 --------------------------------------------------
# ^ は Python の XOR 演算子。
XOR_Sum = A[0]
for i in range(1, N):
    XOR_Sum = (XOR_Sum ^ A[i])

# --- 出力 -----------------------------------------------------------
if XOR_Sum >= 1:
    print("First")
else:
    print("Second")
