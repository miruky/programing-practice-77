# =====================================================================
# C08: ALGO4 (力試し問題)
# ---------------------------------------------------------------------
# 【概要】4 桁の数字 (0000〜9999) に対し、N 件のヒント「S[i] を当てたら
#         T[i] 等だった」が与えられる。当選番号として一意に決まるか判定し、
#         そうでなければ "Can't Solve" と出力する。
#         判定: 4 桁全部一致 = 1 等、1 桁違い = 2 等、それ以外 = 3 等。
# 【アルゴリズム】0000〜9999 の 10000 通りを全探索し、各候補について
#                 すべてのヒントと整合するかをチェック。
# 【計算量】O(10000 · N)。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 全探索 / シミュレーション / 難易度目安: ★3
# - 要約: 4 桁数字くじの当選番号を、0000〜9999 の全探索 + 情報整合性チェックで一意に決定する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

# 当選番号が A2 のとき、A1 は何等になるか
def hantei(A1, A2):
    diff = sum(1 for i in range(4) if A1[i] != A2[i])
    if diff == 0:
        return 1
    if diff == 1:
        return 2
    return 3

# --- 入力 -----------------------------------------------------------
N = int(input())
S = [None] * (N + 1)
T = [0] * (N + 1)
for i in range(1, N + 1):
    s, t = input().split()
    S[i], T[i] = s, int(t)

# --- 0000〜9999 を全探索 -------------------------------------------
Answer = []
for num in range(10000):
    ID = f"{num:04d}"                    # 4 桁ゼロ埋め
    ok = True
    for i in range(1, N + 1):
        if hantei(S[i], ID) != T[i]:
            ok = False
            break
    if ok:
        Answer.append(ID)

# --- 出力 (一意に決まるかで分岐) ------------------------------------
if len(Answer) != 1:
    print("Can't Solve")
else:
    print(Answer[0])
