# =====================================================================
# C08: ALGO4 (力試し問題)
# ---------------------------------------------------------------------
# 【概要】4 桁の数字 (0000〜9999) に対し、N 件のヒント「S[i] を当てたら
#         T[i] 等だった」が与えられる。当選番号として一意に決まるか判定し、
#         そうでなければ "Can't Solve" と出力する。
#         判定: 4 桁全部一致 = 1 等、1 桁違い = 2 等、それ以外 = 3 等。
# 【アルゴリズム】0000〜9999 の 10000 通りを全探索し、各候補について
#                 すべてのヒントと整合するかをチェック。
# 【計算量】O(10000 · N)。
# =====================================================================

# 当選番号が A2 のとき、A1 は何等になるか
def hantei(A1, A2):
    diff = sum(1 for i in range(4) if A1[i] != A2[i])
    if diff == 0:
        return 1
    if diff == 1:
        return 2
    return 3

# --- 入力 -----------------------------------------------------------
N = int(input())
S = [None] * (N + 1)
T = [0] * (N + 1)
for i in range(1, N + 1):
    s, t = input().split()
    S[i], T[i] = s, int(t)

# --- 0000〜9999 を全探索 -------------------------------------------
Answer = []
for num in range(10000):
    ID = f"{num:04d}"                    # 4 桁ゼロ埋め
    ok = True
    for i in range(1, N + 1):
        if hantei(S[i], ID) != T[i]:
            ok = False
            break
    if ok:
        Answer.append(ID)

# --- 出力 (一意に決まるかで分岐) ------------------------------------
if len(Answer) != 1:
    print("Can't Solve")
else:
    print(Answer[0])
