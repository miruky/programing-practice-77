# # A25: 経路数えあげ (DP)
#
# ## 概要
#
# H×W のグリッドにおいて、左上 (0,0) から右下 (H-1,W-1) までの
#         経路の数を求める。'.' は通れる、'#' は通れない。
#         移動は右または下のみ。
#
# ## アルゴリズム
#
# 2 次元 DP
#   dp[i][j] = (0,0) から (i,j) に到達する経路数
#   遷移: 上のマス (i-1,j) + 左のマス (i,j-1) のうち通れる方向から加算。
#
# ## 計算量
#
# O(HW)
#
# ## 読み方の地図
#
# - 問題の型: DP / 数え上げ / 難易度目安: ★2
# - 要約: 格子状の経路数を 2 次元 DP で数える。障害物あり。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。

# --- 入力 -----------------------------------------------------------
H, W = map(int, input().split())
c = [None] * H
for i in range(H):
    c[i] = input()

# --- DP テーブル ----------------------------------------------------
dp = [[0] * (W + 1) for _ in range(H + 1)]

for i in range(H):
    for j in range(W):
        if i == 0 and j == 0:
            dp[i][j] = 1  # 出発点
        else:
            dp[i][j] = 0
            # 上から来る分を加算 (上のマスが通れる場合)
            if i >= 1 and c[i - 1][j] == '.':
                dp[i][j] += dp[i - 1][j]
            # 左から来る分を加算 (左のマスが通れる場合)
            if j >= 1 and c[i][j - 1] == '.':
                dp[i][j] += dp[i][j - 1]

# --- 出力 -----------------------------------------------------------
print(dp[H - 1][W - 1])
