# =====================================================================
# A25: 経路数えあげ (DP)
# ---------------------------------------------------------------------
# 【概要】H×W のグリッドにおいて、左上 (0,0) から右下 (H-1,W-1) までの
#         経路の数を求める。'.' は通れる、'#' は通れない。
#         移動は右または下のみ。
# 【アルゴリズム】2 次元 DP
#   dp[i][j] = (0,0) から (i,j) に到達する経路数
#   遷移: 上のマス (i-1,j) + 左のマス (i,j-1) のうち通れる方向から加算。
# 【計算量】O(HW)
# =====================================================================

# --- 入力 -----------------------------------------------------------
H, W = map(int, input().split())
c = [None] * H
for i in range(H):
    c[i] = input()

# --- DP テーブル ----------------------------------------------------
dp = [[0] * (W + 1) for _ in range(H + 1)]

for i in range(H):
    for j in range(W):
        if i == 0 and j == 0:
            dp[i][j] = 1  # 出発点
        else:
            dp[i][j] = 0
            # 上から来る分を加算 (上のマスが通れる場合)
            if i >= 1 and c[i - 1][j] == '.':
                dp[i][j] += dp[i - 1][j]
            # 左から来る分を加算 (左のマスが通れる場合)
            if j >= 1 and c[i][j - 1] == '.':
                dp[i][j] += dp[i][j - 1]

# --- 出力 -----------------------------------------------------------
print(dp[H - 1][W - 1])
