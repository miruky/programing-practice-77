# =====================================================================
# A11: 二分探索 (1から始まる位置を返す)
# ---------------------------------------------------------------------
# 【概要】昇順にソートされた配列 A の中から、整数 X の位置を求める。
#         位置は 1-indexed で出力する。
# 【アルゴリズム】古典的な二分探索 (binary search)
#   範囲 [L, R] を半分に絞り続け、A[M] と x を比較する。
# 【計算量】O(log N) ─ 配列を毎回半分にするため。
# 【補足】Python では bisect モジュールでも同じことができる。
# =====================================================================

# 整数 x の位置 (0-indexed) を返す。見つからなければ -1。
def search(x, A):
    L = 0
    R = N - 1
    while L <= R:
        M = (L + R) // 2  # 中央のインデックス
        if x < A[M]:
            R = M - 1     # 探索範囲を左半分に
        if x == A[M]:
            return M      # 見つかった!
        if x > A[M]:
            L = M + 1     # 探索範囲を右半分に
    return -1             # 見つからなかった (この問題では発生しない)


# --- 入力 -----------------------------------------------------------
N, X = map(int, input().split())
A = list(map(int, input().split()))

# --- 二分探索の呼び出し ---------------------------------------------
# 関数は 0-indexed で位置を返すので、最後に +1 して 1-indexed にする。
Answer = search(X, A)
print(Answer + 1)
