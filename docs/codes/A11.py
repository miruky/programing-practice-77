# A11: 二分探索 (1から始まる位置を返す)
# ---------------------------------------------------------------------
# 【概要】昇順にソートされた配列 A の中から、整数 X の位置を求める。
#         位置は 1-indexed で出力する。
# 【アルゴリズム】古典的な二分探索 (binary search)
#   範囲 [L, R] を半分に絞り続け、A[M] と x を比較する。
# 【計算量】O(log N) ─ 配列を毎回半分にするため。
# 【補足】Python では bisect モジュールでも同じことができる。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 二分探索 / 難易度目安: ★2
# - 要約: ソート済み配列から目的の整数の位置を二分探索で求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 二分探索は値を探すというより、条件が NG から OK に切り替わる境目を探す。
# - bisect_left は「その値を入れてもソートが崩れない一番左の位置」を返す。
# - LIS や座標圧縮では、配列そのものではなく順位・置き場所を管理する。
# ---------------------------------------------------------------------

# 整数 x の位置 (0-indexed) を返す。見つからなければ -1。
def search(x, A):
    L = 0
    R = N - 1
    while L <= R:
        M = (L + R) // 2  # 中央のインデックス
        if x < A[M]:
            R = M - 1     # 探索範囲を左半分に
        if x == A[M]:
            return M      # 見つかった!
        if x > A[M]:
            L = M + 1     # 探索範囲を右半分に
    return -1             # 見つからなかった (この問題では発生しない)


# --- 入力 -----------------------------------------------------------
N, X = map(int, input().split())
A = list(map(int, input().split()))

# --- 二分探索の呼び出し ---------------------------------------------
# 関数は 0-indexed で位置を返すので、最後に +1 して 1-indexed にする。
Answer = search(X, A)
print(Answer + 1)
