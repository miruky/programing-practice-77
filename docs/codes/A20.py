# =====================================================================
# A20: 最長共通部分列 (LCS)
# ---------------------------------------------------------------------
# 【概要】2 つの文字列 S, T の最長共通部分列 (Longest Common Subsequence)
#         の長さを求める。
# 【アルゴリズム】2 次元 DP
#   dp[i][j] = S の先頭 i 文字 と T の先頭 j 文字 の LCS 長
#   S[i-1] == T[j-1] のとき: dp[i][j] = dp[i-1][j-1] + 1 を取れる。
#   それ以外: dp[i][j] = max(dp[i-1][j], dp[i][j-1])
# 【計算量】O(NM)
# =====================================================================

# --- 入力 -----------------------------------------------------------
S = input()
T = input()
N = len(S)
M = len(T)

# --- DP -------------------------------------------------------------
dp = [[None] * (M + 1) for _ in range(N + 1)]
dp[0][0] = 0  # 両方とも空文字列のとき LCS は 0

for i in range(0, N + 1):
    for j in range(0, M + 1):
        if i >= 1 and j >= 1 and S[i - 1] == T[j - 1]:
            # 文字一致: 斜め遷移 (+1) も候補
            dp[i][j] = max(dp[i - 1][j], dp[i][j - 1], dp[i - 1][j - 1] + 1)
        elif i >= 1 and j >= 1:
            # 文字不一致: 上 or 左の大きい方
            dp[i][j] = max(dp[i - 1][j], dp[i][j - 1])
        elif i >= 1:
            dp[i][j] = dp[i - 1][j]  # 一方が空文字列
        elif j >= 1:
            dp[i][j] = dp[i][j - 1]

# --- 出力 -----------------------------------------------------------
print(dp[N][M])
