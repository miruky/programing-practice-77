# =====================================================================
# A15: 座標圧縮
# ---------------------------------------------------------------------
# 【概要】配列 A の各要素について、A 全体での "順位" (1 始まり) を求める。
# 【アルゴリズム】座標圧縮
#   1. A から重複を取り除いてソートした配列 T を作る。
#   2. T 内での A[i] の位置 (二分探索) +1 がその順位となる。
# 【計算量】O(N log N)
# 【用途】後段でセグメント木・BIT を使う前処理として頻出。
# =====================================================================

import bisect

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- 重複を除去してソート -----------------------------------------
# set() で重複除去、その後 sort() で昇順に並べる。
T = list(set(A))
T.sort()

# --- 各値の順位を二分探索で求める ---------------------------------
B = [None] * N
for i in range(N):
    B[i] = bisect.bisect_left(T, A[i])  # T 内での位置 (0-indexed)
    B[i] += 1                           # 1-indexed に直す

# --- 空白区切りで出力 ------------------------------------------------
Answer = [str(i) for i in B]
print(" ".join(Answer))
