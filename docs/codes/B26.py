# # B26: Output Prime Numbers (A26 応用 ─ エラトステネスのふるい)
#
# ## 概要
#
# N 以下の素数を昇順にすべて出力する。
#
# ## アルゴリズム
#
# エラトステネスのふるい。
#         2 から √N までの各 i について、「まだ消されていなければ」その
#         倍数 2i, 3i, ... を消していく。残った数が素数。
#
# ## 計算量
#
# O(N log log N)。N = 10⁶ までほぼ瞬時。
#
# ## 読み方の地図
#
# - 問題の型: 数学 / 素数 / エラトステネス / 難易度目安: ★2
# - 要約: エラトステネスのふるいで N 以下の素数を全列挙する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。

# --- 入力 -----------------------------------------------------------
N = int(input())

# --- ふるいの本体 --------------------------------------------------
# Deleted[x] = True ⇔ x は素数でない
Deleted = [False] * 1000009
LIMIT = int(N ** 0.5)
for i in range(2, LIMIT + 1):
    if not Deleted[i]:
        # i 自身は素数。その倍数 (2i, 3i, ...) を全て消していく。
        for j in range(i * 2, N + 1, i):
            Deleted[j] = True

# --- 残った数を素数として出力 --------------------------------------
for i in range(2, N + 1):
    if not Deleted[i]:
        print(i)
