# # B32: Game 5 (A32 応用 ─ ゲームの後退解析)
#
# ## 概要
#
# 石が N 個。2 人交互に A[0]..A[K-1] のいずれかの個数だけ取り、
#         取れなくなった方が負け。先手必勝か後手必勝かを判定する。
#
# ## アルゴリズム
#
# 後退解析。dp[x] = 「石 x 個の局面で手番側が勝てるか」。
#                 1 手で 必ず "負け局面" に遷移できれば勝ち、できなければ負け。
#
# ## 計算量
#
# O(N · K)。
#
# ## 読み方の地図
#
# - 問題の型: ゲーム / DP / 難易度目安: ★3
# - 要約: K 通りの取り方を持つ石取りゲームの勝敗を後退解析する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。

# --- 入力 -----------------------------------------------------------
N, K = map(int, input().split())
A = list(map(int, input().split()))

# --- DP (石の個数 i ごとに勝ち/負けを決める) -----------------------
dp = [None] * (N + 1)
for i in range(N + 1):
    dp[i] = False                          # 仮: 負け
    for j in range(K):
        if i >= A[j] and dp[i - A[j]] == False:
            # 「相手を負け状態に追い込める」⇒ 自分は勝ち
            dp[i] = True

# --- 出力 -----------------------------------------------------------
print("First" if dp[N] else "Second")
