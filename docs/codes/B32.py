# =====================================================================
# B32: Game 5 (A32 応用 ─ ゲームの後退解析)
# ---------------------------------------------------------------------
# 【概要】石が N 個。2 人交互に A[0]..A[K-1] のいずれかの個数だけ取り、
#         取れなくなった方が負け。先手必勝か後手必勝かを判定する。
# 【アルゴリズム】後退解析。dp[x] = 「石 x 個の局面で手番側が勝てるか」。
#                 1 手で 必ず "負け局面" に遷移できれば勝ち、できなければ負け。
# 【計算量】O(N · K)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, K = map(int, input().split())
A = list(map(int, input().split()))

# --- DP (石の個数 i ごとに勝ち/負けを決める) -----------------------
dp = [None] * (N + 1)
for i in range(N + 1):
    dp[i] = False                          # 仮: 負け
    for j in range(K):
        if i >= A[j] and dp[i - A[j]] == False:
            # 「相手を負け状態に追い込める」⇒ 自分は勝ち
            dp[i] = True

# --- 出力 -----------------------------------------------------------
print("First" if dp[N] else "Second")
