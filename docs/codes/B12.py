# =====================================================================
# B12: Equation (A12 応用 ─ 実数の二分探索)
# ---------------------------------------------------------------------
# 【概要】方程式 f(x) = x³ + x = N を満たす実数 x を 0 ≦ x ≦ 100 で求める。
# 【アルゴリズム】f は単調増加なので二分探索が効く。20 回反復で誤差は
#                 (100/2^20) ≒ 10⁻⁴ となり、要求精度 10⁻⁶ にも近づく
#                 (本問は誤差 0.001 以下で AC)。
# 【計算量】O(反復回数) = O(log(範囲 / 精度))。
# 【ポイント】「答えで二分探索」の最も素朴な形 ─ x の値そのものを探す。
# =====================================================================

# f(x) = x³ + x。x が増えると単調に増える。
def f(x):
    return x * x * x + x

N = int(input())

# --- 実数の二分探索 -------------------------------------------------
Left = 0.0
Right = 100.0
for _ in range(20):
    Mid = (Left + Right) / 2
    val = f(Mid)
    if val > N:
        Right = Mid                      # 中央が大きすぎる ⇒ 左半分に絞る
    else:
        Left = Mid                       # 中央以下に答え ⇒ 右半分に絞る

# --- 出力 -----------------------------------------------------------
# Mid は最後に計算した中央値で、収束した近似解。
print(Mid)
