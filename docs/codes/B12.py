# # B12: Equation (A12 応用 ─ 実数の二分探索)
#
# ## 概要
#
# 方程式 f(x) = x³ + x = N を満たす実数 x を 0 ≦ x ≦ 100 で求める。
#
# ## アルゴリズム
#
# f は単調増加なので二分探索が効く。20 回反復で誤差は
#                 (100/2^20) ≒ 10⁻⁴ となり、要求精度 10⁻⁶ にも近づく
#                 (本問は誤差 0.001 以下で AC)。
#
# ## 計算量
#
# O(反復回数) = O(log(範囲 / 精度))。
#
# ## ポイント
#
# 「答えで二分探索」の最も素朴な形 ─ x の値そのものを探す。
#
# ## 読み方の地図
#
# - 問題の型: 二分探索 / 実数 / 難易度目安: ★4
# - 要約: x³+x=N となる実数 x を二分探索で求める。実数二分探索の練習。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 二分探索は値を探すというより、条件が NG から OK に切り替わる境目を探す。
# - bisect_left は「その値を入れてもソートが崩れない一番左の位置」を返す。
# - LIS や座標圧縮では、配列そのものではなく順位・置き場所を管理する。

# f(x) = x³ + x。x が増えると単調に増える。
def f(x):
    return x * x * x + x

N = int(input())

# --- 実数の二分探索 -------------------------------------------------
Left = 0.0
Right = 100.0
for _ in range(20):
    Mid = (Left + Right) / 2
    val = f(Mid)
    if val > N:
        Right = Mid                      # 中央が大きすぎる ⇒ 左半分に絞る
    else:
        Left = Mid                       # 中央以下に答え ⇒ 右半分に絞る

# --- 出力 -----------------------------------------------------------
# Mid は最後に計算した中央値で、収束した近似解。
print(Mid)
