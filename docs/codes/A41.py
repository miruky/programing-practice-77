# =====================================================================
# A41: 3 連続パターンの検出
# ---------------------------------------------------------------------
# 【概要】文字列 S 内に "RRR" または "BBB" が部分文字列として現れるか。
# 【アルゴリズム】長さ 3 の窓で線形に走査する。
# 【計算量】O(N)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 考察 / 鳩ノ巣 / 難易度目安: ★2
# - 要約: 鳩ノ巣原理を背景に同じ文字の 3 連続を探す。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N = int(input())
S = input()

# --- 走査 -----------------------------------------------------------
Answer = False
for i in range(0, N - 2):
    if S[i] == 'R' and S[i + 1] == 'R' and S[i + 2] == 'R':
        Answer = True
    if S[i] == 'B' and S[i + 1] == 'B' and S[i + 2] == 'B':
        Answer = True

# --- 出力 -----------------------------------------------------------
if Answer == True:
    print("Yes")
else:
    print("No")
