# # B44: Grid Operations (A44 応用 ─ 遅延処理 / 添字配列)
#
# ## 概要
#
# N 行 N 列の行列 A と、行を 2 本入れ替えるクエリ・特定マスを
#         参照するクエリが与えられる。それらを順に処理する。
#
# ## アルゴリズム
#
# 行を物理的にコピーすると 1 回あたり O(N) かかる。
#                 代わりに「論理的な行番号 → 物理的な行番号」の対応表 T を
#                 持って、入れ替えクエリは T 上の 2 要素だけスワップ。
#                 参照クエリは T 経由で実行行を選ぶ。
#
# ## 計算量
#
# 各クエリ O(1)、初期化 O(N²)。
#
# ## 読み方の地図
#
# - 問題の型: 考察 / 遅延 / 難易度目安: ★3
# - 要約: 行の入れ替えクエリをインデックス配列でだけ操作し、物理コピーを避ける。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。

# --- 入力 (行列) ---------------------------------------------------
N = int(input())
A = [None] * N
for i in range(N):
    A[i] = list(map(int, input().split()))

# --- 論理行 → 物理行 の対応表 (恒等写像で初期化) -----------------
T = [None] * N
for i in range(N):
    T[i] = i

# --- クエリ処理 -----------------------------------------------------
Q = int(input())
for _ in range(Q):
    Query = list(map(int, input().split()))
    if Query[0] == 1:
        # 行 a と行 b を入れ替える ⇔ T 上で対応する物理番号を swap
        T[Query[1] - 1], T[Query[2] - 1] = T[Query[2] - 1], T[Query[1] - 1]
    if Query[0] == 2:
        # 論理行 Query[1]、列 Query[2] のマスを参照
        print(A[T[Query[1] - 1]][Query[2] - 1])
