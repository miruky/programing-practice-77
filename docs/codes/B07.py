# =====================================================================
# B07: Convenience Store 2 (A07 応用 ─ いもす法)
# ---------------------------------------------------------------------
# 【概要】時刻 0..T-1 において、それぞれの時刻に滞在中の人数を出力する。
#         入店時刻 L[i]、退店時刻 R[i] (退店の瞬間にはいない) が N 件。
# 【アルゴリズム】「差分配列」B に対し B[L]+=1, B[R]-=1 を加え、最後に
#                 累積和を取れば各時刻の人数が分かる ─ これがいもす法。
# 【計算量】O(N + T)。区間加算が定数時間でできるのが肝。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 累積和 / いもす法 / 難易度目安: ★3
# - 要約: いもす法で各時刻に滞在中の客数を求める。A07 の練習。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 区間全体を更新せず、始点で +、終点の次で - を置く。
# - 最後に左から累積和を取ると、各位置に実際の加算結果が広がる。
# - 右端を含む区間か、半開区間かで - を置く場所が変わる。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
T = int(input())
N = int(input())
L = [None] * N
R = [None] * N
for i in range(N):
    L[i], R[i] = map(int, input().split())

# --- 差分配列に区間操作を畳む --------------------------------------
# B[d] は「時刻 d で人数が前時刻からどれだけ変化したか」を表す。
B = [0] * (T + 1)
for i in range(N):
    B[L[i]] += 1                # L 時刻で +1 人入店
    B[R[i]] -= 1                # R 時刻で -1 人退店

# --- 累積和を取って実際の人数を復元 --------------------------------
Answer = [None] * (T + 1)
Answer[0] = B[0]
for d in range(1, T + 1):
    Answer[d] = Answer[d - 1] + B[d]

# --- 出力 -----------------------------------------------------------
for d in range(T):
    print(Answer[d])
