# =====================================================================
# B18: Subset Sum with Restoration (A18 応用 ─ 部分和 DP + 経路復元)
# ---------------------------------------------------------------------
# 【概要】N 枚のカード A[1..N] からいくつか選んで合計を S にできるかを
#         判定し、できるなら選んだカード番号も出力する。
# 【アルゴリズム】2 次元 bool DP: dp[i][j] = 「i 枚目までで合計 j を作れるか」。
#                 復元時は「dp[i-1][Place] が True ⇒ i 枚目は選んでない」
#                 と逆向きに辿る。
# 【計算量】O(N · S)。
# =====================================================================
import sys
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: DP / 部分和 / 経路復元 / 難易度目安: ★4
# - 要約: 部分和 DP に加え、選んだカードの番号も復元して出力する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N, S = map(int, input().split())
A = list(map(int, input().split()))

# --- DP 初期化 (0 枚使ったときは合計 0 のみ可能) -------------------
dp = [[None] * (S + 1) for _ in range(N + 1)]
dp[0][0] = True
for i in range(1, S + 1):
    dp[0][i] = False

# --- DP 本体 --------------------------------------------------------
# i 枚目を選ばない場合 / 選ぶ場合の論理 OR で更新。
for i in range(1, N + 1):
    for j in range(0, S + 1):
        if j < A[i - 1]:
            dp[i][j] = dp[i - 1][j]
        else:
            dp[i][j] = dp[i - 1][j] or dp[i - 1][j - A[i - 1]]

# --- そもそも作れないなら -1 を出して終了 --------------------------
if dp[N][S] == False:
    print("-1")
    sys.exit(0)

# --- 経路復元 (どのカードを選んだかを逆向きに復元) ----------------
Answer = []
Place = S
for i in reversed(range(1, N + 1)):
    if dp[i - 1][Place]:
        pass                              # i を選ばずに到達可能だった
    else:
        Place -= A[i - 1]                 # i を選ぶしかなかった
        Answer.append(i)
Answer.reverse()

# --- 出力 -----------------------------------------------------------
print(len(Answer))
print(" ".join(map(str, Answer)))
