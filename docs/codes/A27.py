# =====================================================================
# A27: 最大公約数 (ユークリッド互除法)
# ---------------------------------------------------------------------
# 【概要】2 つの正整数 A, B の最大公約数 (GCD) を求める。
# 【アルゴリズム】ユークリッド互除法
#   gcd(A, B) = gcd(B, A mod B)  という性質を使い、
#   どちらかが 0 になるまで A, B を更新し続ける。
# 【計算量】O(log min(A, B))
# 【補足】Python 標準には math.gcd(A, B) があるが、勉強のため手書き実装。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 数学 / GCD / 難易度目安: ★1
# - 要約: ユークリッド互除法で GCD を求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。
# ---------------------------------------------------------------------

def GCD(A, B):
    # どちらも 1 以上の間、大きい方を mod で小さく更新し続ける。
    while A >= 1 and B >= 1:
        if A >= B:
            A = A % B
        else:
            B = B % A
    # 片方が 0 になった時点で、もう片方が GCD。
    if A >= 1:
        return A
    return B


# --- 入力・出力 -----------------------------------------------------
A, B = map(int, input().split())
print(GCD(A, B))
