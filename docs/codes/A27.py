# =====================================================================
# A27: 最大公約数 (ユークリッド互除法)
# ---------------------------------------------------------------------
# 【概要】2 つの正整数 A, B の最大公約数 (GCD) を求める。
# 【アルゴリズム】ユークリッド互除法
#   gcd(A, B) = gcd(B, A mod B)  という性質を使い、
#   どちらかが 0 になるまで A, B を更新し続ける。
# 【計算量】O(log min(A, B))
# 【補足】Python 標準には math.gcd(A, B) があるが、勉強のため手書き実装。
# =====================================================================

def GCD(A, B):
    # どちらも 1 以上の間、大きい方を mod で小さく更新し続ける。
    while A >= 1 and B >= 1:
        if A >= B:
            A = A % B
        else:
            B = B % A
    # 片方が 0 になった時点で、もう片方が GCD。
    if A >= 1:
        return A
    return B


# --- 入力・出力 -----------------------------------------------------
A, B = map(int, input().split())
print(GCD(A, B))
