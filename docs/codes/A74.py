# =====================================================================
# A74: 転倒数に分解 (スライドパズル)
# ---------------------------------------------------------------------
# 【概要】N×N の盤面上のスライドパズルで、目標配置にする最小操作回数を求める。
# 【アルゴリズム】考察 (分解)
#   - 操作は行と列で独立: 行を並べ替える操作と列を並べ替える操作。
#   - 各操作は隣接スワップなので、各方向の最小操作数は "転倒数"。
#   - 行と列に分け、転倒数を独立に計算して合計するのが答え。
# 【計算量】O(N^2)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 転倒数 / 難易度目安: ★3
# - 要約: 行と列の独立な並べ替えに分解し、転倒数の和が答え。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N = int(input())
P = [list(map(int, input().split())) for _ in range(N)]

# --- 行・列に分解 -------------------------------------------------
# 0 でない値を持つ行・列が一意に決まる (一直線に並んでいる構造)
X = [None] * N
Y = [None] * N
for i in range(N):
    for j in range(N):
        if P[i][j] != 0:
            X[i] = P[i][j]  # 行 i の代表値
            Y[j] = P[i][j]  # 列 j の代表値


def inversion(A):
    """配列 A の転倒数 (i<j で A[i]>A[j] となる組数)"""
    answer = 0
    for i in range(len(A)):
        for j in range(i + 1, len(A)):
            if A[i] > A[j]:
                answer += 1
    return answer


# --- 行転倒数 + 列転倒数 = 必要操作回数 ----------------------------
print(inversion(X) + inversion(Y))
