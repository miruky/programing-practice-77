# =====================================================================
# A51: スタック (LIFO)
# ---------------------------------------------------------------------
# 【概要】3 種類のクエリを処理するスタック実装。
#   "1 x": 文字列 x を積む / "2": 最上位を表示 / "3": 最上位を取り除く
# 【データ構造】collections.deque
#   list でも O(1) で push/pop できるが、deque の方がメモリ効率も良い。
# 【計算量】各クエリ O(1)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: データ構造 / スタック / 難易度目安: ★1
# - 要約: LIFO のスタックを deque で実装。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

from collections import deque

# --- 入力 -----------------------------------------------------------
Q = int(input())
queries = [input().split() for _ in range(Q)]

# --- クエリ処理 -----------------------------------------------------
S = deque()
for q in queries:
    if q[0] == "1":
        S.append(q[1])    # push 末尾に追加
    if q[0] == "2":
        print(S[-1])      # top 末尾の参照
    if q[0] == "3":
        S.pop()           # pop 末尾を削除
