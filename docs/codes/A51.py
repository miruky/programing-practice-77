# A51: スタック (LIFO)
# ---------------------------------------------------------------------
# 【概要】3 種類のクエリを処理するスタック実装。
#   "1 x": 文字列 x を積む / "2": 最上位を表示 / "3": 最上位を取り除く
# 【データ構造】collections.deque
#   list でも O(1) で push/pop できるが、deque の方がメモリ効率も良い。
# 【計算量】各クエリ O(1)
# =====================================================================

from collections import deque

# --- 入力 -----------------------------------------------------------
Q = int(input())
queries = [input().split() for _ in range(Q)]

# --- クエリ処理 -----------------------------------------------------
S = deque()
for q in queries:
    if q[0] == "1":
        S.append(q[1])    # push 末尾に追加
    if q[0] == "2":
        print(S[-1])      # top 末尾の参照
    if q[0] == "3":
        S.pop()           # pop 末尾を削除
