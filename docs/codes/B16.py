# =====================================================================
# B16: Frog 1 (A16 応用 / EDPC-A の典型 DP)
# ---------------------------------------------------------------------
# 【概要】足場 0..N-1 の高さ H[i] が与えられる。0 から N-1 まで、1 個 or
#         2 個先のマスに飛び移れるとき、移動コストの最小値を求める。
#         コストは |H[i] - H[j]|。
# 【アルゴリズム】古典 1 次元 DP。dp[i] = 足場 i に到達する最小コスト。
#         dp[i] = min(dp[i-1] + |H[i-1] - H[i]|, dp[i-2] + |H[i-2] - H[i]|)
# 【計算量】O(N)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())
H = list(map(int, input().split()))

# --- DP 配列の初期化 ------------------------------------------------
dp = [None] * N

# --- 動的計画法 -----------------------------------------------------
# 0 番目はスタートなのでコスト 0、1 番目は必ず 1 個ジャンプして来る。
dp[0] = 0
dp[1] = abs(H[0] - H[1])
for i in range(2, N):
    # 1 個前から来るか、2 個前から来るかの安い方を取る。
    dp[i] = min(dp[i - 1] + abs(H[i - 1] - H[i]),
                dp[i - 2] + abs(H[i - 2] - H[i]))

# --- 出力 -----------------------------------------------------------
print(dp[N - 1])
