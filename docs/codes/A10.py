# # A10: 区間最大値クエリ (累積max)
#
# ## 概要
#
# 配列 A に対して、ある 1 区間 [L, R] を抜いた残りの最大値を
#         Q 個の (L, R) に対して答える。
#
# ## アルゴリズム
#
# "前方累積 max" P と "後方累積 max" Q を前計算しておく。
#   - P[i] = A[0..i] の最大値
#   - Q[i] = A[i..N-1] の最大値
#   削除区間 [L, R] の外側の最大値は max(P[L-2], Q[R]) で求められる。
#
# ## 計算量
#
# 前計算 O(N) + クエリ O(D)。
#
# ## 読み方の地図
#
# - 問題の型: 累積和 / 区間クエリ / 難易度目安: ★2
# - 要約: 左右からの累積最大値を前計算し、抜き取り後の最大値を求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - S[i] は先頭から i 個分の合計。S[0]=0 を置くと端の例外処理が消える。
# - 区間和は「右端までの合計 - 左手前までの合計」と読む。
# - 入力が 1-indexed、Python 配列が 0-indexed なので添字の変換だけ注意する。

N = int(input())
A = list(map(int, input().split()))
D = int(input())
L = [None] * D
R = [None] * D
for d in range(D):
    L[d], R[d] = map(int, input().split())

# --- 前方累積 max: P[i] = max(A[0..i]) ----------------------------
P = [None] * N
P[0] = A[0]
for i in range(1, N):
    P[i] = max(P[i - 1], A[i])

# --- 後方累積 max: Q[i] = max(A[i..N-1]) --------------------------
# reversed(range(0, N-1)) で i = N-2, N-3, ..., 0 を逆順に回す。
Q = [None] * N
Q[N - 1] = A[N - 1]
for i in reversed(range(0, N - 1)):
    Q[i] = max(Q[i + 1], A[i])

# --- クエリ処理 ---------------------------------------------------
# 入力は 1-indexed なので、配列の 0-indexed に合わせて -1 する。
# 削除区間の左外側: P[(L-1)-1] = P[L-2]
# 削除区間の右外側: Q[(R+1)-1] = Q[R]
for d in range(D):
    print(max(P[(L[d] - 1) - 1], Q[(R[d] + 1) - 1]))
