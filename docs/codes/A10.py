# =====================================================================
# A10: 区間最大値クエリ (累積max)
# ---------------------------------------------------------------------
# 【概要】配列 A に対して、ある 1 区間 [L, R] を抜いた残りの最大値を
#         Q 個の (L, R) に対して答える。
# 【アルゴリズム】"前方累積 max" P と "後方累積 max" Q を前計算しておく。
#   - P[i] = A[0..i] の最大値
#   - Q[i] = A[i..N-1] の最大値
#   削除区間 [L, R] の外側の最大値は max(P[L-2], Q[R]) で求められる。
# 【計算量】前計算 O(N) + クエリ O(D)。
# =====================================================================

N = int(input())
A = list(map(int, input().split()))
D = int(input())
L = [None] * D
R = [None] * D
for d in range(D):
    L[d], R[d] = map(int, input().split())

# --- 前方累積 max: P[i] = max(A[0..i]) ----------------------------
P = [None] * N
P[0] = A[0]
for i in range(1, N):
    P[i] = max(P[i - 1], A[i])

# --- 後方累積 max: Q[i] = max(A[i..N-1]) --------------------------
# reversed(range(0, N-1)) で i = N-2, N-3, ..., 0 を逆順に回す。
Q = [None] * N
Q[N - 1] = A[N - 1]
for i in reversed(range(0, N - 1)):
    Q[i] = max(Q[i + 1], A[i])

# --- クエリ処理 ---------------------------------------------------
# 入力は 1-indexed なので、配列の 0-indexed に合わせて -1 する。
# 削除区間の左外側: P[(L-1)-1] = P[L-2]
# 削除区間の右外側: Q[(R+1)-1] = Q[R]
for d in range(D):
    print(max(P[(L[d] - 1) - 1], Q[(R[d] + 1) - 1]))
