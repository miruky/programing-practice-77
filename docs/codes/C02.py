# =====================================================================
# C02: Two Balls (力試し問題)
# ---------------------------------------------------------------------
# 【概要】N 個のボールから異なる 2 つを選んで合計を最大化する。
# 【アルゴリズム】二重ループで全 (i, j) (i < j) を試す全探索。
# 【計算量】O(N²)。N ≦ 100 なら一瞬。
# 【別解】O(N) ─ 配列の最大値と 2 番目を求めて足すだけでも解ける。
#         ここでは「全探索の練習」として O(N²) で書いている。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 全探索 / 難易度目安: ★1
# - 要約: N 個のボールから 2 つ選んで合計を最大化 (全探索 O(N²))。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- 全探索 --------------------------------------------------------
Answer = 0
for i in range(N):
    for j in range(i + 1, N):
        Answer = max(Answer, A[i] + A[j])

# --- 出力 -----------------------------------------------------------
print(Answer)
