# =====================================================================
# B65: Road to Promotion Hard (A65 応用 ─ 木 DP)
# ---------------------------------------------------------------------
# 【概要】N 頂点の木で頂点 T を「社長」とする。各頂点 i について、
#         「i 以下にある部下のうち、i から最も遠い人までの距離 (= rank[i])」
#         を全頂点について求める。
# 【アルゴリズム】T を根とする DFS で、子の rank に +1 した値の最大を
#                 自分の rank に集約する典型木 DP。
# 【計算量】O(N)。
# =====================================================================
import sys
sys.setrecursionlimit(1 << 30)
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: グラフ / 木DP / 難易度目安: ★4
# - 要約: 根を T として、各頂点の部下数 (= 深さの最大) を木 DP で集計する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N, T = map(int, input().split())
T -= 1
g = [[] for _ in range(N)]
for _ in range(N - 1):
    A, B = map(int, input().split())
    A -= 1
    B -= 1
    g[A].append(B)
    g[B].append(A)

rank = [0] * N


# 親方向に戻らないように parent を引き回す再帰 DFS
def dfs(parent: int, i: int) -> int:
    for j in g[i]:
        if j == parent:
            continue
        r = dfs(i, j) + 1
        if rank[i] < r:
            rank[i] = r
    return rank[i]


dfs(-1, T)

# --- 出力 -----------------------------------------------------------
print(*rank)
