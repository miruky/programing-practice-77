# =====================================================================
# B38: Heights of Grass (A38 応用 ─ 左右の連続長)
# ---------------------------------------------------------------------
# 【概要】文字列 S は 'A' と 'B' からなる。各位置 i について、
#         (左から見て「直前まで A が連続した長さ + 1」) と
#         (右から見て「直後まで B が連続した長さ + 1」) の大きい方を
#         合計したものを答える。
# 【アルゴリズム】左から & 右から の 2 つの走査で前計算 ret1, ret2 を持ち、
#                 位置 i ごとに max を取る。
# 【計算量】O(N)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())
S = input()

# --- 左から: 直前まで A が連続している回数 + 1 --------------------
ret1 = [None] * N
streak1 = 1
ret1[0] = 1
for i in range(N - 1):
    if S[i] == 'A':
        streak1 += 1
    if S[i] == 'B':
        streak1 = 1
    ret1[i + 1] = streak1

# --- 右から: 直後まで B が連続している回数 + 1 --------------------
ret2 = [None] * N
streak2 = 1
ret2[N - 1] = 1
for i in reversed(range(N - 1)):
    if S[i] == 'A':
        streak2 = 1
    if S[i] == 'B':
        streak2 += 1
    ret2[i] = streak2

# --- 各位置で max を取り合計 --------------------------------------
Answer = 0
for i in range(N):
    Answer += max(ret1[i], ret2[i])
print(Answer)
