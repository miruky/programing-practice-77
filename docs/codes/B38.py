# # B38: Heights of Grass (A38 応用 ─ 左右の連続長)
#
# ## 概要
#
# 文字列 S は 'A' と 'B' からなる。各位置 i について、
#         (左から見て「直前まで A が連続した長さ + 1」) と
#         (右から見て「直後まで B が連続した長さ + 1」) の大きい方を
#         合計したものを答える。
#
# ## アルゴリズム
#
# 左から & 右から の 2 つの走査で前計算 ret1, ret2 を持ち、
#                 位置 i ごとに max を取る。
#
# ## 計算量
#
# O(N)。
#
# ## 読み方の地図
#
# - 問題の型: 考察 / 前計算 / 難易度目安: ★3
# - 要約: 左右からの連続 A/B 長を求め、各位置の最大を合算する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。

# --- 入力 -----------------------------------------------------------
N = int(input())
S = input()

# --- 左から: 直前まで A が連続している回数 + 1 --------------------
ret1 = [None] * N
streak1 = 1
ret1[0] = 1
for i in range(N - 1):
    if S[i] == 'A':
        streak1 += 1
    if S[i] == 'B':
        streak1 = 1
    ret1[i + 1] = streak1

# --- 右から: 直後まで B が連続している回数 + 1 --------------------
ret2 = [None] * N
streak2 = 1
ret2[N - 1] = 1
for i in reversed(range(N - 1)):
    if S[i] == 'A':
        streak2 = 1
    if S[i] == 'B':
        streak2 += 1
    ret2[i] = streak2

# --- 各位置で max を取り合計 --------------------------------------
Answer = 0
for i in range(N):
    Answer += max(ret1[i], ret2[i])
print(Answer)
