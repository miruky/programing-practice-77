# B03: Supermarket 1 (A03 応用)
# ---------------------------------------------------------------------
# 【概要】N 個の値段 A[0..N-1] の中から異なる 3 つを選んで合計を 1000 に
#         できるかを判定する。
# 【アルゴリズム】三重ループで i<j<k を満たす全組合せを試す。
# 【計算量】O(N³)。N ≦ 100 なら 10^6 で十分高速。
# 【ポイント】「異なる」3 つにするには i<j<k と添字を厳密に大小制約する。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 全探索 / 難易度目安: ★2
# - 要約: N 個の値段から異なる 3 つを選んで合計が 1000 になるかを三重ループで判定。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- 三重ループ全探索 -----------------------------------------------
Answer = False
for i in range(N):
    for j in range(i + 1, N):          # j は i より大きい添字のみ
        for k in range(j + 1, N):      # k は j より大きい添字のみ
            if A[i] + A[j] + A[k] == 1000:
                Answer = True

# --- 出力 -----------------------------------------------------------
print("Yes" if Answer else "No")
