# =====================================================================
# A01: 2乗を計算しよう
# ---------------------------------------------------------------------
# 【概要】整数 N を読み込み、N×N を出力するだけの最も基本的な問題。
# 【目的】Python での標準入出力 (input / print) の練習。
# 【計算量】O(1) ─ 入力 1 行と出力 1 行のみ。
# =====================================================================

# input() は標準入力から 1 行を文字列として受け取る関数。
# 競技プログラミングでは整数入力が多いので int() で囲むイディオムが頻出。
N = int(input())  # 例: 入力が "5" なら、N に整数 5 が代入される

# print() は標準出力に値を書き込み、最後に改行を入れる。
# N * N は Python の整数同士のかけ算（オーバーフローは発生しない）。
print(N * N)
