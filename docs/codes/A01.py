# =====================================================================
# A01: 2乗を計算しよう
# ---------------------------------------------------------------------
# 【概要】整数 N を読み込み、N×N を出力するだけの最も基本的な問題。
# 【目的】Python での標準入出力 (input / print) の練習。
# 【計算量】O(1) ─ 入力 1 行と出力 1 行のみ。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 入出力 / 演習 / 難易度目安: ★1
# - 要約: 整数 N を読み込み、N×N を出力する。標準入出力の練習。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

# input() は標準入力から 1 行を文字列として受け取る関数。
# 競技プログラミングでは整数入力が多いので int() で囲むイディオムが頻出。
N = int(input())  # 例: 入力が "5" なら、N に整数 5 が代入される

# print() は標準出力に値を書き込み、最後に改行を入れる。
# N * N は Python の整数同士のかけ算（オーバーフローは発生しない）。
print(N * N)
