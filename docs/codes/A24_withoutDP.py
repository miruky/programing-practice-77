# =====================================================================
# A24 (補足版): LIS ─ dp 配列なし版
# ---------------------------------------------------------------------
# 【概要】A24 のシンプル実装版。dp 配列を使わず、配列 L のみで完結する。
#         長さ LEN を返すだけなら、これで十分。
# 【アルゴリズム】O(N log N) LIS (Patience sorting の考え方)
# 【計算量】O(N log N)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: DP / LIS / 難易度目安: ★4
# - 要約: dp 配列を使わない更にシンプルな LIS 実装。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 二分探索は値を探すというより、条件が NG から OK に切り替わる境目を探す。
# - bisect_left は「その値を入れてもソートが崩れない一番左の位置」を返す。
# - LIS や座標圧縮では、配列そのものではなく順位・置き場所を管理する。
# ---------------------------------------------------------------------

import bisect

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- 準備 -----------------------------------------------------------
LEN = 0  # 現在の LIS 長
L = []   # L[k] = 長さ k+1 の LIS の末尾の最小値

# --- LIS の構築 ---------------------------------------------------
for i in range(N):
    pos = bisect.bisect_left(L, A[i])
    if pos >= LEN:
        L.append(A[i])  # L を伸ばす
        LEN += 1
    else:
        L[pos] = A[i]   # より小さい末尾に置換

# --- 答えを出力 ---------------------------------------------------
print(LEN)
