# =====================================================================
# A24 (補足版): LIS ─ dp 配列なし版
# ---------------------------------------------------------------------
# 【概要】A24 のシンプル実装版。dp 配列を使わず、配列 L のみで完結する。
#         長さ LEN を返すだけなら、これで十分。
# 【アルゴリズム】O(N log N) LIS (Patience sorting の考え方)
# 【計算量】O(N log N)
# =====================================================================

import bisect

# --- 入力 -----------------------------------------------------------
N = int(input())
A = list(map(int, input().split()))

# --- 準備 -----------------------------------------------------------
LEN = 0  # 現在の LIS 長
L = []   # L[k] = 長さ k+1 の LIS の末尾の最小値

# --- LIS の構築 ---------------------------------------------------
for i in range(N):
    pos = bisect.bisect_left(L, A[i])
    if pos >= LEN:
        L.append(A[i])  # L を伸ばす
        LEN += 1
    else:
        L[pos] = A[i]   # より小さい末尾に置換

# --- 答えを出力 ---------------------------------------------------
print(LEN)
