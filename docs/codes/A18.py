# =====================================================================
# A18: 部分和問題
# ---------------------------------------------------------------------
# 【概要】N 枚のカード (値 A[i]) から、合計を S にできるかを判定する。
# 【アルゴリズム】2 次元 DP
#   dp[i][j] = 最初の i 枚から合計 j を作れるか (True/False)
#   遷移: dp[i][j] = dp[i-1][j]  (i 枚目を選ばない)
#         dp[i][j] = dp[i-1][j-A[i-1]]  (i 枚目を選ぶ; j>=A[i-1])
# 【計算量】O(NS)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: DP / 部分和 / 難易度目安: ★3
# - 要約: 選んだカードの合計を S にできるかを 2 次元 DP で判定する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - dp の添字が何を意味するかを最初に日本語で固定する。ここが一番重要。
# - 初期状態は「何も選んでいない」「スタート地点」など、手でわかる状態だけ入れる。
# - 遷移は「使う/使わない」「前から来る/上から来る」など、直前の状態から読む。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
N, S = map(int, input().split())
A = list(map(int, input().split()))

# --- DP テーブル初期化 ----------------------------------------------
# dp[i][j] : i 枚から合計 j を作れるか
dp = [[None] * (S + 1) for _ in range(N + 1)]

# 0 枚で 0 は作れる (空和)、それ以外は不可。
dp[0][0] = True
for i in range(1, S + 1):
    dp[0][i] = False

# --- DP 遷移 -------------------------------------------------------
for i in range(1, N + 1):
    for j in range(0, S + 1):
        if j < A[i - 1]:
            # j が A[i-1] より小さい → i 番目は選べない
            dp[i][j] = dp[i - 1][j]
        if j >= A[i - 1]:
            # 選ぶ・選ばないの両方を試して OR を取る
            if dp[i - 1][j] == True or dp[i - 1][j - A[i - 1]] == True:
                dp[i][j] = True
            else:
                dp[i][j] = False

# --- 出力 -----------------------------------------------------------
if dp[N][S] == True:
    print("Yes")
else:
    print("No")
