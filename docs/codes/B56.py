# =====================================================================
# B56: Palindrome Queries (A56 応用 ─ ローリングハッシュ)
# ---------------------------------------------------------------------
# 【概要】文字列 S と Q 個のクエリ [L,R] が与えられる。S[L..R] が回文かを
#         O(1) で判定する。
# 【アルゴリズム】「前から読んだハッシュ列」H と「後ろから読んだハッシュ列」
#                 HRev を 2 種類前計算しておき、両者の部分ハッシュが一致する
#                 ⇔ 部分文字列が回文。
# 【計算量】前計算 O(N) + 各クエリ O(1)。
# =====================================================================

mod = 2147483647


# S の l〜r 番目 (1-indexed) を前から読んだときのハッシュ値
def GetHashLeft(l: int, r: int) -> int:
    val = H[r] - Power100[r - l + 1] * H[l - 1]
    return val % mod


# S の l〜r 番目を後ろから読んだときのハッシュ値 (= SRev 上の対応区間のハッシュ)
def GetHashRight(l: int, r: int) -> int:
    # SRev における対応位置に翻訳
    l, r = N + 1 - r, N + 1 - l
    val = HRev[r] - Power100[r - l + 1] * HRev[l - 1]
    return val % mod


# --- 入力 -----------------------------------------------------------
N, Q = map(int, input().split())
S = input()
Query = [tuple(map(int, input().split())) for _ in range(Q)]

# --- 文字を 1〜26 の数値に変換 (基数 100 のハッシュに乗せる) -------
S = list(S)
for i in range(N):
    S[i] = ord(S[i]) - ord('a') + 1
SRev = S[::-1]

# --- 100 の n 乗を前計算 -------------------------------------------
Power100 = [1] * (N + 1)
for i in range(N):
    Power100[i + 1] = Power100[i] * 100 % mod

# --- 累積ハッシュ (前向き) -----------------------------------------
H = [1] * (N + 1)
for i in range(N):
    H[i + 1] = (H[i] * 100 + S[i]) % mod

# --- 累積ハッシュ (後ろ向き) ---------------------------------------
HRev = [1] * (N + 1)
for i in range(N):
    HRev[i + 1] = (HRev[i] * 100 + SRev[i]) % mod

# --- クエリ処理 -----------------------------------------------------
for L, R in Query:
    v1 = GetHashLeft(L, R)
    v2 = GetHashRight(L, R)
    print("Yes" if v1 == v2 else "No")
