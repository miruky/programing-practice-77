# # A31: 3 か 5 の倍数 (包除原理)
#
# ## 概要
#
# 1 から N までの整数のうち、3 か 5 で割り切れるものの個数を求める。
#
# ## アルゴリズム
#
# 包除原理 (Inclusion-Exclusion Principle)
#   |3 の倍数| + |5 の倍数| - |3 と 5 両方の倍数|
#   = N//3 + N//5 - N//15
#
# ## 計算量
#
# O(1)
#
# ## 読み方の地図
#
# - 問題の型: 数学 / 包除 / 難易度目安: ★1
# - 要約: 包除原理で 3 または 5 の倍数の個数を求める。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 全探索ではなく、式・余り・周期・不変量に置き換えて一気に答えるタイプ。
# - 小さい例で 1 回手計算すると、なぜその式になるかがかなり見えやすい。
# - mod が出る問題では、足す・引く・掛けるたびに余りを取って値を小さく保つ。

N = int(input())
A1 = (N //  3)  # 3 の倍数の個数
A2 = (N //  5)  # 5 の倍数の個数
A3 = (N // 15)  # 3 かつ 5 の倍数 (= 15 の倍数) の個数
# 包除で 2 重カウントぶんを引く
print(A1 + A2 - A3)
