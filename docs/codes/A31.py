# =====================================================================
# A31: 3 か 5 の倍数 (包除原理)
# ---------------------------------------------------------------------
# 【概要】1 から N までの整数のうち、3 か 5 で割り切れるものの個数を求める。
# 【アルゴリズム】包除原理 (Inclusion-Exclusion Principle)
#   |3 の倍数| + |5 の倍数| - |3 と 5 両方の倍数|
#   = N//3 + N//5 - N//15
# 【計算量】O(1)
# =====================================================================

N = int(input())
A1 = (N //  3)  # 3 の倍数の個数
A2 = (N //  5)  # 5 の倍数の個数
A3 = (N // 15)  # 3 かつ 5 の倍数 (= 15 の倍数) の個数
# 包除で 2 重カウントぶんを引く
print(A1 + A2 - A3)
