# =====================================================================
# A52: キュー (FIFO)
# ---------------------------------------------------------------------
# 【概要】3 種類のクエリ:
#   "1 x": 末尾に追加 / "2": 先頭を表示 / "3": 先頭を取り除く
# 【データ構造】collections.deque (両端から O(1) で操作可能)
#   list は popleft が O(N) で遅いため deque を使う。
# 【計算量】各クエリ O(1)
# =====================================================================

from collections import deque

# --- 入力 -----------------------------------------------------------
Q = int(input())
queries = [input().split() for _ in range(Q)]

# --- クエリ処理 -----------------------------------------------------
T = deque()
for q in queries:
    if q[0] == "1":
        T.append(q[1])    # enqueue (末尾追加)
    if q[0] == "2":
        print(T[0])       # front (先頭参照)
    if q[0] == "3":
        T.popleft()       # dequeue (先頭削除)
