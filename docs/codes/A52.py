# =====================================================================
# A52: キュー (FIFO)
# ---------------------------------------------------------------------
# 【概要】3 種類のクエリ:
#   "1 x": 末尾に追加 / "2": 先頭を表示 / "3": 先頭を取り除く
# 【データ構造】collections.deque (両端から O(1) で操作可能)
#   list は popleft が O(N) で遅いため deque を使う。
# 【計算量】各クエリ O(1)
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: データ構造 / キュー / 難易度目安: ★1
# - 要約: FIFO のキューを deque で実装。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

from collections import deque

# --- 入力 -----------------------------------------------------------
Q = int(input())
queries = [input().split() for _ in range(Q)]

# --- クエリ処理 -----------------------------------------------------
T = deque()
for q in queries:
    if q[0] == "1":
        T.append(q[1])    # enqueue (末尾追加)
    if q[0] == "2":
        print(T[0])       # front (先頭参照)
    if q[0] == "3":
        T.popleft()       # dequeue (先頭削除)
