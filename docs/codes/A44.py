# =====================================================================
# A44: 操作の遅延 (反転フラグの利用)
# ---------------------------------------------------------------------
# 【概要】配列に対して 3 種類のクエリ:
#   1: 位置 x の値を y に変更
#   2: 配列を反転
#   3: 位置 x の値を出力
# 【アルゴリズム】考察 (遅延処理)
#   反転を毎回実際に行うと O(N) かかってしまうが、
#   "現在反転状態かどうか" をフラグ State で持てば、
#   各クエリは添字を読み替えるだけで O(1) で処理できる。
# 【計算量】O(N + Q)
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, Q = map(int, input().split())
State = 1            # 1: 正方向, 2: 反転中
E = [None] * (N + 2)
for i in range(1, N + 1):
    E[i] = i

# --- クエリの処理 ---------------------------------------------------
for i in range(Q):
    Query = input().split()

    # [1] 変更
    if int(Query[0]) == 1:
        x = int(Query[1])
        y = int(Query[2])
        if State == 1:
            E[x] = y
        if State == 2:
            E[N + 1 - x] = y  # 反転中: 配列を裏返したときの位置に書き込む

    # [2] 反転 (フラグだけ切り替える)
    if int(Query[0]) == 2:
        if State == 1:
            State = 2
        else:
            State = 1

    # [3] 取得
    if int(Query[0]) == 3:
        x = int(Query[1])
        if State == 1:
            print(E[x])
        if State == 2:
            print(E[N + 1 - x])
