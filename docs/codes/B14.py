# =====================================================================
# B14: Another Subset Sum (A14 応用 ─ 半分全列挙)
# ---------------------------------------------------------------------
# 【概要】N 枚のカード A[0..N-1] からいくつか選んで合計を K にできるかを
#         判定する。N が 30〜40 程度のとき素朴な 2^N は重い。
# 【アルゴリズム】N 枚を半分に分け、それぞれの「あり得る合計」を 2^(N/2)
#                 通り全列挙し、片方をソートして二分探索で K - 合計 を探す。
# 【計算量】O(2^(N/2) · N)。N = 40 でも 2^20 ≒ 10^6 で十分高速。
# =====================================================================
import bisect
import sys
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 二分探索 / 半分全列挙 / 難易度目安: ★5
# - 要約: 半分全列挙でカードの合計が K にできるかを判定する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 二分探索は値を探すというより、条件が NG から OK に切り替わる境目を探す。
# - bisect_left は「その値を入れてもソートが崩れない一番左の位置」を返す。
# - LIS や座標圧縮では、配列そのものではなく順位・置き場所を管理する。
# ---------------------------------------------------------------------

# A の中から好きな部分集合を取った時の「あり得る合計」を 2^len(A) 通り全列挙。
# ビット全探索: i の二進表現が部分集合に対応する。
def Enumerate(A):
    SumList = []
    for i in range(2 ** len(A)):
        s = 0
        for j in range(len(A)):
            if (i >> j) & 1:             # j 番目を選ぶ
                s += A[j]
        SumList.append(s)
    return SumList

# --- 入力 -----------------------------------------------------------
N, K = map(int, input().split())
A = list(map(int, input().split()))

# --- 半分に分ける ---------------------------------------------------
L1 = A[0:(N // 2)]
L2 = A[(N // 2):N]

# --- それぞれで合計を全列挙し、片方をソート -----------------------
Sum1 = Enumerate(L1)
Sum2 = Enumerate(L2)
Sum1.sort()
Sum2.sort()

# --- 二分探索で Sum1[i] + Sum2[j] == K となる組を探す -------------
# 各 Sum1[i] に対し、必要な相方は K - Sum1[i]。これを Sum2 から二分探索。
for i in range(len(Sum1)):
    pos = bisect.bisect_left(Sum2, K - Sum1[i])
    if pos < len(Sum2) and Sum2[pos] == K - Sum1[i]:
        print("Yes")
        sys.exit(0)

# 見つからなかった場合
print("No")
