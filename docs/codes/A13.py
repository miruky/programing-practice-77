# # A13: しゃくとり法 (近いペアの数え上げ)
#
# ## 概要
#
# ソート済み配列 A から、差 A[j]-A[i] が K 以下となる (i, j) (i<j)
#         のペア数を求める。
#
# ## アルゴリズム
#
# しゃくとり法 (Two pointers / Sliding window)
#   i が右に進むほど、対応する R[i] (= 条件を満たす最大 j) も単調に増える。
#   したがって R は戻さずに前進だけする → 全体 O(N)。
#
# ## 計算量
#
# O(N)
#
# ## ポイント
#
# L を i, R を「i から見て条件を満たす一番遠い右端」とする。
#
# ## 読み方の地図
#
# - 問題の型: しゃくとり法 / 難易度目安: ★3
# - 要約: 右端を尺取り虫のように動かして区間長を効率的に列挙する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 左端 l を固定し、条件を満たす限り右端 r を前に進める。
# - r は戻さない。各要素が高々 1 回ずつ進むので全体 O(N) になる。
# - 現在の窓が何を満たしているかを、ループの不変条件として読む。

# --- 入力 -----------------------------------------------------------
N, K = map(int, input().split())
A = list(map(int, input().split()))

# R[i] := i から見て A[R[i]] - A[i] <= K を満たす最大の R
R = [None] * N

# --- しゃくとり法 ---------------------------------------------------
for i in range(0, N - 1):
    # R は単調なので、R[i-1] からスタートしてよい (戻る必要がない)。
    if i == 0:
        R[i] = 0
    else:
        R[i] = R[i - 1]

    # 限界まで右端を進める。
    while R[i] < N - 1 and A[R[i] + 1] - A[i] <= K:
        R[i] += 1

# --- 答え --------------------------------------------------------
# 各 i に対して、相手は i+1 〜 R[i] の (R[i] - i) 個。
Answer = 0
for i in range(0, N - 1):
    Answer += (R[i] - i)
print(Answer)
