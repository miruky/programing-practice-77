# =====================================================================
# A13: しゃくとり法 (近いペアの数え上げ)
# ---------------------------------------------------------------------
# 【概要】ソート済み配列 A から、差 A[j]-A[i] が K 以下となる (i, j) (i<j)
#         のペア数を求める。
# 【アルゴリズム】しゃくとり法 (Two pointers / Sliding window)
#   i が右に進むほど、対応する R[i] (= 条件を満たす最大 j) も単調に増える。
#   したがって R は戻さずに前進だけする → 全体 O(N)。
# 【計算量】O(N)
# 【ポイント】L を i, R を「i から見て条件を満たす一番遠い右端」とする。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N, K = map(int, input().split())
A = list(map(int, input().split()))

# R[i] := i から見て A[R[i]] - A[i] <= K を満たす最大の R
R = [None] * N

# --- しゃくとり法 ---------------------------------------------------
for i in range(0, N - 1):
    # R は単調なので、R[i-1] からスタートしてよい (戻る必要がない)。
    if i == 0:
        R[i] = 0
    else:
        R[i] = R[i - 1]

    # 限界まで右端を進める。
    while R[i] < N - 1 and A[R[i] + 1] - A[i] <= K:
        R[i] += 1

# --- 答え --------------------------------------------------------
# 各 i に対して、相手は i+1 〜 R[i] の (R[i] - i) 個。
Answer = 0
for i in range(0, N - 1):
    Answer += (R[i] - i)
print(Answer)
