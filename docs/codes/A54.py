# =====================================================================
# A54: 連想配列 (Map / dict)
# ---------------------------------------------------------------------
# 【概要】2 種類のクエリ:
#   "1 name score": name の点数を score にセット
#   "2 name":       name の点数を出力
# 【データ構造】Python の dict (ハッシュテーブル)
# 【計算量】各クエリ平均 O(1)
# =====================================================================

# --- 入力 -----------------------------------------------------------
Q = int(input())
queries = [input().split() for _ in range(Q)]

# --- クエリ処理 -----------------------------------------------------
Map = {}
for q in queries:
    if q[0] == "1":
        Map[q[1]] = q[2]   # 登録または更新
    if q[0] == "2":
        print(Map[q[1]])   # 参照
