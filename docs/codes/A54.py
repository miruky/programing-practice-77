# # A54: 連想配列 (Map / dict)
#
# ## 概要
#
# 2 種類のクエリ:
#   "1 name score": name の点数を score にセット
#   "2 name":       name の点数を出力
#
# ## データ構造
#
# Python の dict (ハッシュテーブル)
#
# ## 計算量
#
# 各クエリ平均 O(1)
#
# ## 読み方の地図
#
# - 問題の型: データ構造 / 辞書 / 難易度目安: ★1
# - 要約: 辞書で名前→点数の対応を保持する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。

# --- 入力 -----------------------------------------------------------
Q = int(input())
queries = [input().split() for _ in range(Q)]

# --- クエリ処理 -----------------------------------------------------
Map = {}
for q in queries:
    if q[0] == "1":
        Map[q[1]] = q[2]   # 登録または更新
    if q[0] == "2":
        print(Map[q[1]])   # 参照
