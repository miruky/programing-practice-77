# =====================================================================
# C06: Regular Graph (力試し問題)
# ---------------------------------------------------------------------
# 【概要】N 頂点 N 辺、すべての頂点の次数が 2 となるグラフ (=正則グラフ
#         のうち単純な閉路) を 1 つ出力する。
# 【アルゴリズム】サイクル 1-2-3-...-N-1 を出力する。
#                 つまり辺は (i, i+1) (i = 1..N-1) と (N, 1)。
# 【計算量】O(N)。
# 【ポイント】i % N + 1 のイディオムで「i の次の頂点」を表現できる
#             (N の次は 1 に戻る)。
# =====================================================================

# --- 入力 -----------------------------------------------------------
N = int(input())

# --- 出力 -----------------------------------------------------------
out = [str(N)]                            # 辺の本数 (= 頂点数)
for i in range(1, N + 1):
    out.append(f"{i} {i % N + 1}")
print("\n".join(out))
