# # C06: Regular Graph (力試し問題)
#
# ## 概要
#
# N 頂点 N 辺、すべての頂点の次数が 2 となるグラフ (=正則グラフ
#         のうち単純な閉路) を 1 つ出力する。
#
# ## アルゴリズム
#
# サイクル 1-2-3-...-N-1 を出力する。
#                 つまり辺は (i, i+1) (i = 1..N-1) と (N, 1)。
#
# ## 計算量
#
# O(N)。
#
# ## ポイント
#
# i % N + 1 のイディオムで「i の次の頂点」を表現できる
#             (N の次は 1 に戻る)。
#
# ## 読み方の地図
#
# - 問題の型: 構築 / グラフ / 難易度目安: ★2
# - 要約: 全頂点の次数が 2 となるサイクルを 1 つ構成して出力する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
#
# ## このコードで特に見る場所
#
# - 入力をまず隣接リストやグリッドの隣接関係に変換している部分を見る。
# - visited や dist は「もう見たか」「何手で来たか」を記録するための配列。
# - DFS は深く進む、BFS は近い順に広がる。この違いだけ先に押さえる。

# --- 入力 -----------------------------------------------------------
N = int(input())

# --- 出力 -----------------------------------------------------------
out = [str(N)]                            # 辺の本数 (= 頂点数)
for i in range(1, N + 1):
    out.append(f"{i} {i % N + 1}")
print("\n".join(out))
