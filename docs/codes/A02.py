# =====================================================================
# A02: N枚のカード (線形探索)
# ---------------------------------------------------------------------
# 【概要】長さ N の数列 A の中に整数 X が含まれるかを判定し
#         含まれていれば "Yes" を、そうでなければ "No" を出力する。
# 【アルゴリズム】先頭から順に 1 つずつ調べる "線形探索"。
# 【計算量】O(N) ─ 配列を一度だけ走査する。
# =====================================================================
# ---------------------------------------------------------------------
# 【読み方の地図】
# - 問題の型: 全探索 / 線形探索 / 難易度目安: ★1
# - 要約: 配列に X が含まれるかを線形探索で判定する。
# - まず入力ブロックで、どの変数・配列に何が入るかを確認する。
# - 次に中心となる配列・状態・データ構造の意味を 1 行で言語化する。
# - 最後に答えを出す式や出力ループだけを、入力例と照らして追う。
# 【このコードで特に見る場所】
# - 入力、前処理、判定・更新、出力の 4 ブロックに分けて読む。
# - 各変数・配列には「何を保存しているか」という役割があるので、名前だけで追わない。
# - 小さい入力例を 1 つ作り、ループが 1 回進むごとに値がどう変わるかを見る。
# ---------------------------------------------------------------------

# --- 入力 -----------------------------------------------------------
# 1 行目: 配列長 N と探したい整数 X が空白区切りで与えられる。
# map(int, ...) で各トークンを int に変換する典型イディオム。
N, X = map(int, input().split())

# 2 行目: A[0], A[1], ..., A[N-1] が空白区切りで与えられる。
# list(map(int, input().split())) は競プロで最頻出の配列入力パターン。
A = list(map(int, input().split()))

# --- 線形探索 -------------------------------------------------------
# 「答えが見つかったか」を表すフラグ変数。初期値は False。
Answer = False

# A を頭から順に確認していく。i は 0, 1, ..., N-1 を取る。
for i in range(N):
    if A[i] == X:
        # 一度でも一致すれば Answer を True にする。
        # break しないのは「全要素を確認する」という説明的な実装にするため。
        # 効率を求めるなら break しても同じ結果になる。
        Answer = True

# --- 出力 -----------------------------------------------------------
# bool を直接 if で判定する Pythonic な書き方も可: `if Answer:`
if Answer == True:
    print("Yes")
else:
    print("No")
